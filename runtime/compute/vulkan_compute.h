/* vulkan_compute.h - Vulkan/WebGPU Compute Backend */
#ifndef VULKAN_COMPUTE_H
#define VULKAN_COMPUTE_H

#include <stdint.h>

/* Vulkan compute dispatch types */
typedef enum {
    VK_SHADER_MATMUL     = 0,
    VK_SHADER_ADD        = 1,
    VK_SHADER_RELU       = 2,
    VK_SHADER_SOFTMAX    = 3,
    VK_SHADER_LAYERNORM  = 4,
    VK_SHADER_ATTENTION  = 5,
    VK_SHADER_GELU       = 6,
    VK_SHADER_CONV2D     = 7,
    VK_SHADER_EMBEDDING  = 8,
    VK_SHADER_TRANSPOSE  = 9,
    VK_SHADER_COUNT,
} vk_shader_type_t;

/* Compute buffer descriptor */
typedef struct {
    uint64_t  device_addr;   /* GPU-side address */
    uint64_t  size;          /* Size in bytes */
    void     *host_map;      /* Host-mapped pointer (if mappable) */
    uint32_t  usage;         /* Bitmask: 1=storage, 2=uniform, 4=staging */
    int       bound_set;     /* Descriptor set binding index */
} vk_buffer_t;

/* Compute pipeline */
typedef struct {
    vk_shader_type_t shader;
    uint32_t  spirv_offset;  /* Offset into SPIR-V module */
    uint32_t  spirv_size;
    uint32_t  local_size[3]; /* Workgroup size (x, y, z) */
    int       n_buffers;
    int       push_const_size;
} vk_pipeline_t;

/* Dispatch parameters */
typedef struct {
    vk_pipeline_t *pipeline;
    uint32_t       group_count[3]; /* Dispatch size (x, y, z) */
    vk_buffer_t   *buffers;
    int            n_buffers;
    void          *push_constants;
    int            push_const_size;
} vk_dispatch_t;

/* Vulkan compute context */
typedef struct {
    uint64_t  device_base;   /* PCI BAR base */
    uint64_t  vram_base;     /* VRAM base address */
    uint64_t  vram_size;
    uint64_t  vram_used;
    uint32_t  queue_family;  /* Compute queue family index */
    uint32_t  vendor_id;
    uint32_t  device_id;
    int       initialized;
    vk_pipeline_t pipelines[VK_SHADER_COUNT];
} vk_compute_ctx_t;

int  vk_compute_init(vk_compute_ctx_t *ctx);
int  vk_buffer_create(vk_compute_ctx_t *ctx, vk_buffer_t *buf, uint64_t size, uint32_t usage);
int  vk_buffer_upload(vk_compute_ctx_t *ctx, vk_buffer_t *buf, const void *data, uint64_t size);
int  vk_buffer_download(vk_compute_ctx_t *ctx, const vk_buffer_t *buf, void *data, uint64_t size);
void vk_buffer_destroy(vk_compute_ctx_t *ctx, vk_buffer_t *buf);
int  vk_dispatch(vk_compute_ctx_t *ctx, const vk_dispatch_t *dispatch);
int  vk_matmul(vk_compute_ctx_t *ctx, const float *A, const float *B, float *C,
               int M, int K, int N);
int  vk_attention(vk_compute_ctx_t *ctx, const float *Q, const float *K,
                  const float *V, float *out, int seq_len, int head_dim, int n_heads);
void vk_compute_selftest(void);

#endif /* VULKAN_COMPUTE_H */
