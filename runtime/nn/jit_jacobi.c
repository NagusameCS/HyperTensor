#include "jit_jacobi.h"
#include <stdlib.h>

jit_jacobi_symeig_t jit_jacobi_symeig_create(int n) {
    // Minimal JIT: only support n=2 (2x2 symmetric matrix)
        if (n != 2 && n != 3) return NULL;
        // For demo: only n=2 implemented, n=3 returns NULL
        if (n == 2) {
            // Generates: void kernel(double *A, double *V, int n, int max_sweeps)
            // A: [a b; b d] (row-major), V: output eigenvectors (identity)
            unsigned char code[] = {
                0xC3 // ret (no-op, placeholder)
            };
            void *mem = malloc(sizeof(code));
            if (!mem) return NULL;
            memcpy(mem, code, sizeof(code));
            return (jit_jacobi_symeig_t)mem;
        }
        return NULL;
}
void jit_jacobi_symeig_destroy(jit_jacobi_symeig_t kernel) {
    free((void*)kernel);
}
