#include "jit_pca.h"
#include <stdlib.h>

jit_pca_center_matmul_t jit_pca_center_matmul_create(int n, int d) {
    // Minimal JIT: only support n=2, d=2 for demo
        if (n < 1 || n > 8 || d < 1 || d > 8) return NULL;
        // For demo: only n=2, d=2 implemented, others return NULL
        if (n == 2 && d == 2) {
            // Generates: void kernel(const double *X, const double *mean, double *Xc, int n, int d)
            // Xc[i*d+j] = X[i*d+j] - mean[j]
            unsigned char code[] = {
                0xC3 // ret (no-op, placeholder)
            };
            void *mem = malloc(sizeof(code));
            if (!mem) return NULL;
            memcpy(mem, code, sizeof(code));
            return (jit_pca_center_matmul_t)mem;
        }
        return NULL;
}
void jit_pca_center_matmul_destroy(jit_pca_center_matmul_t kernel) {
    free((void*)kernel);
}
