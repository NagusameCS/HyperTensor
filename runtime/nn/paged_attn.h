/* =============================================================================
 * TensorOS - Paged Attention (vLLM-style) Interface
 * Block-based KV cache management for efficient multi-request serving
 * =============================================================================*/

#ifndef TENSOROS_PAGED_ATTN_H
#define TENSOROS_PAGED_ATTN_H

#include "kernel/core/kernel.h"

/* KV cache block configuration */
#define PA_BLOCK_SIZE       16      /* Tokens per block */
#define PA_MAX_BLOCKS       1024    /* Maximum blocks in pool */
#define PA_MAX_SEQUENCES    64      /* Maximum concurrent sequences */
#define PA_MAX_BLOCKS_PER_SEQ 256   /* Maximum blocks per sequence */

/* Block status */
typedef enum {
    PA_BLOCK_FREE = 0,
    PA_BLOCK_ALLOCATED,
    PA_BLOCK_SHARED,        /* Copy-on-write shared block */
} pa_block_status_t;

/* A single KV cache block: stores PA_BLOCK_SIZE tokens of K and V */
typedef struct {
    uint32_t            block_id;
    pa_block_status_t   status;
    uint32_t            ref_count;      /* For copy-on-write sharing */
    uint32_t            n_filled;       /* Tokens written in this block (0..BLOCK_SIZE) */
    float              *k_data;         /* [PA_BLOCK_SIZE  n_kv_heads  head_dim] */
    float              *v_data;         /* [PA_BLOCK_SIZE  n_kv_heads  head_dim] */
} pa_block_t;

/* Per-sequence block table: maps logical block index → physical block */
typedef struct {
    uint32_t    seq_id;
    bool        active;
    int         n_tokens;               /* Total tokens in this sequence */
    int         n_blocks;               /* Number of blocks allocated */
    uint32_t    block_table[PA_MAX_BLOCKS_PER_SEQ]; /* Logical → physical block ID */
} pa_sequence_t;

/* Paged attention engine state */
typedef struct {
    int         head_dim;
    int         n_kv_heads;
    int         n_heads;
    float       scale;

    pa_block_t      blocks[PA_MAX_BLOCKS];
    pa_sequence_t   sequences[PA_MAX_SEQUENCES];
    uint32_t        n_blocks_used;
    uint32_t        n_sequences_active;

    /* Statistics */
    uint64_t    cache_hits;
    uint64_t    blocks_allocated;
    uint64_t    blocks_freed;
    uint64_t    cow_copies;             /* Copy-on-write events */
} paged_attn_engine_t;

/* =============================================================================
 * API
 * =============================================================================*/

int  paged_attn_init(int head_dim, int n_kv_heads, int n_heads);
void paged_attn_destroy(void);

/* Sequence management */
int  pa_sequence_create(void);
void pa_sequence_destroy(int seq_id);

/* Append a token's KV to the sequence (allocates new blocks as needed) */
int  pa_append_kv(int seq_id, const float *k, const float *v);

/* Run paged attention: compute output for query tokens using block table */
int  pa_attention_forward(
    float *output,          /* [n_heads  head_dim] for single token */
    const float *query,     /* [n_heads  head_dim] */
    int seq_id
);

/* Copy-on-write: fork a sequence (for beam search) */
int  pa_sequence_fork(int parent_seq_id);

/* Preemption: swap blocks to CPU memory to free GPU/cache space */
int  pa_sequence_swap_out(int seq_id);
int  pa_sequence_swap_in(int seq_id);

/* Statistics */
void pa_print_stats(void);
void pa_selftest(void);

#endif /* TENSOROS_PAGED_ATTN_H */
