typedef struct { unsigned char *buf; size_t len, cap; } jit_buf_t;
static void jit_emit(jit_buf_t *b, unsigned char v) { if (b->len < b->cap) b->buf[b->len++] = v; }
static void jit_modrm_mem(jit_buf_t *b, int reg, int base, int disp) {
    int r3 = reg & 7, m3 = base & 7;
    int sib = (m3 == 4);
    if (disp == 0 && m3 != 5 && m3 != 13) {
        jit_emit(b, (unsigned char)((0<<6)|(r3<<3)|m3));
        if (sib) jit_emit(b, 0x24);
    } else if (disp >= -128 && disp <= 127) {
        jit_emit(b, (unsigned char)((1<<6)|(r3<<3)|m3));
        if (sib) jit_emit(b, 0x24);
        jit_emit(b, (unsigned char)(disp & 0xFF));
    } else {
        jit_emit(b, (unsigned char)((2<<6)|(r3<<3)|m3));
        if (sib) jit_emit(b, 0x24);
        jit_emit(b, (unsigned char)(disp & 0xFF));
        jit_emit(b, (unsigned char)((disp>>8) & 0xFF));
        jit_emit(b, (unsigned char)((disp>>16) & 0xFF));
        jit_emit(b, (unsigned char)((disp>>24) & 0xFF));
    }
}
static void jit_movss_load(jit_buf_t *b, int xmm, int base, int disp) {
    jit_emit(b, 0xF3);
    int R = xmm >= 8, B = base >= 8;
    if (R||B) jit_emit(b, (unsigned char)(0x40|(R?4:0)|(B?1:0)));
    jit_emit(b, 0x0F); jit_emit(b, 0x10);
    jit_modrm_mem(b, xmm, base, disp);
}
static void jit_movss_store(jit_buf_t *b, int base, int disp, int xmm) {
    jit_emit(b, 0xF3);
    int R = xmm >= 8, B = base >= 8;
    if (R||B) jit_emit(b, (unsigned char)(0x40|(R?4:0)|(B?1:0)));
    jit_emit(b, 0x0F); jit_emit(b, 0x11);
    jit_modrm_mem(b, xmm, base, disp);
}
static void jit_mulss(jit_buf_t *b, int dst, int src) {
    jit_emit(b, 0xF3);
    int R = dst >= 8, B = src >= 8;
    if (R||B) jit_emit(b, (unsigned char)(0x40|(R?4:0)|(B?1:0)));
    jit_emit(b, 0x0F); jit_emit(b, 0x59);
    jit_emit(b, (unsigned char)((3<<6)|((dst&7)<<3)|(src&7)));
}
static void jit_addss(jit_buf_t *b, int dst, int src) {
    jit_emit(b, 0xF3);
    int R = dst >= 8, B = src >= 8;
    if (R||B) jit_emit(b, (unsigned char)(0x40|(R?4:0)|(B?1:0)));
    jit_emit(b, 0x0F); jit_emit(b, 0x58);
    jit_emit(b, (unsigned char)((3<<6)|((dst&7)<<3)|(src&7)));
}
static void jit_xorps(jit_buf_t *b, int xmm) {
    jit_emit(b, 0x0F); jit_emit(b, 0x57);
    jit_emit(b, (unsigned char)((3<<6)|((xmm&7)<<3)|(xmm&7)));
}
/* mulss xmm_dst, [base_reg + disp]  — scalar single multiply from memory */
static void jit_mulss_mem(jit_buf_t *b, int xmm_dst, int base_reg, int disp) {
    jit_emit(b, 0xF3);
    int R = xmm_dst >= 8, B = base_reg >= 8;
    if (R || B) jit_emit(b, (unsigned char)(0x40 | (R ? 4 : 0) | (B ? 1 : 0)));
    jit_emit(b, 0x0F); jit_emit(b, 0x59);
    jit_modrm_mem(b, xmm_dst, base_reg, disp);
}

static void *jit_alloc(size_t sz) {
#ifdef _WIN32
    return VirtualAlloc(NULL, sz, MEM_COMMIT|MEM_RESERVE, PAGE_EXECUTE_READWRITE);
#else
    void *p = mmap(NULL, sz, PROT_READ|PROT_WRITE|PROT_EXEC, MAP_PRIVATE|MAP_ANONYMOUS, -1, 0);
    return p == MAP_FAILED ? NULL : p;
#endif
}

jit_gemm_f32_t jit_gemm_f32_create(int M, int N, int K) {
    if (M < 1 || N < 1 || K < 1 || M > 8 || N > 8 || K > 8) return NULL;
    /* Worst-case code size for 888: 64  (3 + 8(5+8+4) + 10) + 1 ≈ 11KB */
    size_t cap = 16384;
    unsigned char *mem = (unsigned char*)jit_alloc(cap);
    if (!mem) return NULL;
    jit_buf_t b = {mem, 0, cap};

    /* Windows x64 ABI: A=RCX(1), B=RDX(2), C=R8(8)
     * Only XMM0 (accumulator) and XMM1 (temp) used — both volatile.
     * No callee-saved registers touched; no prologue/epilogue needed.
     *
     * Emits a fully-unrolled scalar SSE GEMM:
     *   for i in [0,M): for j in [0,N):
     *     xmm0 = 0;
     *     for k in [0,K): xmm0 += A[i*K+k] * B[k*N+j];
     *     C[i*N+j] = xmm0;
     */
    for (int i = 0; i < M; i++) {
        for (int j = 0; j < N; j++) {
            jit_xorps(&b, 0);  /* xorps xmm0, xmm0 */
            for (int k = 0; k < K; k++) {
                int a_off = (i * K + k) * 4;
                int b_off = (k * N + j) * 4;
                jit_movss_load(&b, 1, 1 /* RCX */, a_off);  /* movss xmm1, [rcx+a_off] */
                jit_mulss_mem (&b, 1, 2 /* RDX */, b_off);  /* mulss xmm1, [rdx+b_off] */
                jit_addss     (&b, 0, 1);                    /* addss xmm0, xmm1        */
            }
            int c_off = (i * N + j) * 4;
            jit_movss_store(&b, 8 /* R8 */, c_off, 0);      /* movss [r8+c_off], xmm0  */
        }
    }
    jit_emit(&b, 0xC3);  /* ret */
    return (jit_gemm_f32_t)mem;
}

void jit_gemm_f32_destroy(jit_gemm_f32_t kernel) {
#ifdef _WIN32
    VirtualFree((void*)kernel, 0, MEM_RELEASE);
#else
    munmap((void*)kernel, 4096);
#endif
}
