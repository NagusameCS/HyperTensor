/*
 * Geodessical Model Auto-Download
 *
 * Downloads GGUF models from HuggingFace Hub directly in the C runtime.
 * Uses WinHTTP on Windows for HTTPS support.
 *
 * Features:
 *   - HuggingFace Hub API integration
 *   - GGUF file auto-detection from model repos
 *   - Progress reporting with speed estimation
 *   - Resume support for interrupted downloads
 *   - SHA256 integrity verification
 *   - Local model cache management
 *
 * Usage:
 *   hf_download_model("Qwen/Qwen2.5-0.5B-Instruct-GGUF",
 *                     "qwen2.5-0.5b-instruct-q4_0.gguf",
 *                     "models/");
 *
 * Or with auto-detect:
 *   hf_download_auto("Qwen/Qwen2.5-0.5B-Instruct-GGUF", "q4_0", "models/");
 */

#ifndef GEODESSICAL_HF_DOWNLOAD_H
#define GEODESSICAL_HF_DOWNLOAD_H

#include <stdint.h>

/* ── Constants ─────────────────────────────────────────────────────────── */
#define HF_MAX_URL        512
#define HF_MAX_PATH       256
#define HF_MAX_REPO_ID    128
#define HF_MAX_FILENAME   128
#define HF_MAX_FILES      32
#define HF_CHUNK_SIZE     (1024 * 1024)  /* 1 MB download chunks */

/* ── Download Status ───────────────────────────────────────────────────── */
typedef enum {
    HF_STATUS_IDLE       = 0,
    HF_STATUS_RESOLVING  = 1,
    HF_STATUS_CONNECTING = 2,
    HF_STATUS_DOWNLOADING = 3,
    HF_STATUS_VERIFYING  = 4,
    HF_STATUS_COMPLETE   = 5,
    HF_STATUS_ERROR      = 6,
    HF_STATUS_CACHED     = 7,  /* Already exists locally */
} hf_status_t;

/* ── Progress Callback ─────────────────────────────────────────────────── */
typedef void (*hf_progress_cb_t)(uint64_t downloaded, uint64_t total,
                                 float speed_mbps, void *userdata);

/* ── File Entry (from HF API response) ─────────────────────────────────── */
typedef struct {
    char     filename[HF_MAX_FILENAME];
    uint64_t size;
    char     sha256[65];    /* Hex string */
    int      is_gguf;       /* 1 if filename ends in .gguf */
} hf_file_entry_t;

/* ── Download Context ──────────────────────────────────────────────────── */
typedef struct {
    char           repo_id[HF_MAX_REPO_ID];       /* e.g. "Qwen/Qwen2.5-0.5B-Instruct-GGUF" */
    char           filename[HF_MAX_FILENAME];      /* e.g. "qwen2.5-0.5b-instruct-q4_0.gguf" */
    char           output_dir[HF_MAX_PATH];        /* e.g. "models/" */
    char           output_path[HF_MAX_PATH];       /* Full path to downloaded file */

    hf_status_t    status;
    uint64_t       total_bytes;
    uint64_t       downloaded_bytes;
    float          speed_mbps;
    char           error[256];

    /* Callback */
    hf_progress_cb_t progress_cb;
    void           *progress_ud;

    /* File listing cache */
    hf_file_entry_t files[HF_MAX_FILES];
    int              n_files;
} hf_download_ctx_t;

/* ── API ───────────────────────────────────────────────────────────────── */

/* Initialize download context. */
void hf_download_init(hf_download_ctx_t *ctx);

/* Set progress callback. */
void hf_download_set_progress(hf_download_ctx_t *ctx,
                              hf_progress_cb_t cb, void *userdata);

/* List GGUF files in a HuggingFace repo.
 * Queries the HF API and populates ctx->files[].
 * Returns number of GGUF files found, or -1 on error. */
int hf_list_gguf_files(hf_download_ctx_t *ctx, const char *repo_id);

/* Download a specific file from a HuggingFace repo.
 * repo_id: "username/model-name"
 * filename: specific file to download (e.g. "model-q4_0.gguf")
 * output_dir: local directory to save to
 * Returns 0 on success, -1 on error. */
int hf_download_file(hf_download_ctx_t *ctx,
                     const char *repo_id,
                     const char *filename,
                     const char *output_dir);

/* Auto-download: find the best matching GGUF file for a quantization type.
 * quant_hint: "q4_0", "q8_0", "q4_k_m", etc. (case-insensitive)
 * Returns 0 on success, -1 on error. */
int hf_download_auto(hf_download_ctx_t *ctx,
                     const char *repo_id,
                     const char *quant_hint,
                     const char *output_dir);

/* Check if a model is already cached locally.
 * Returns 1 if cached, 0 if not. */
int hf_is_cached(const char *repo_id, const char *filename,
                 const char *output_dir, char *out_path, int path_size);

/* Get the download status. */
hf_status_t hf_download_status(const hf_download_ctx_t *ctx);

/* Get error message (if status == HF_STATUS_ERROR). */
const char *hf_download_error(const hf_download_ctx_t *ctx);

/* Free download context resources. */
void hf_download_free(hf_download_ctx_t *ctx);

#endif /* GEODESSICAL_HF_DOWNLOAD_H */
