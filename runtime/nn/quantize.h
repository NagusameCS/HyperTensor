/* =============================================================================
 * TensorOS - INT16 Quantized Neural Network Inference
 *
 * Quantizes FP32 neural networks to INT16 (symmetric per-tensor quantization)
 * and runs inference using SSE2 PMADDWD for ~2 throughput.
 *
 * The key insight: PMADDWD multiplies 8 packed int16 values and accumulates
 * adjacent pairs into 4 int32 results — processing 8 multiply-accumulates
 * per SSE2 instruction vs. 4 for FP32. Combined with 2 memory reduction,
 * quantized inference approaches 2 wall-clock speedup.
 *
 * Adaptive precision: if quantization error exceeds threshold, automatically
 * falls back to FP32 for that layer. Zero-cost when all layers quantize well.
 * =============================================================================*/

#ifndef TENSOROS_NN_QUANTIZE_H
#define TENSOROS_NN_QUANTIZE_H

#include <stdint.h>
#include "runtime/nn/inference.h"

/* Quantization parameters for symmetric quantization */
typedef struct {
    float scale;        /* float_val = int_val * scale */
    float inv_scale;    /* int_val = float_val * inv_scale */
} quant_params_t;

/* Quantized dense layer */
typedef struct {
    int16_t *weights_q;     /* [out_dim  in_dim] quantized, 16-byte aligned */
    int16_t *bias_q;        /* [out_dim] quantized, or NULL */
    quant_params_t w_qp;    /* Weight quantization params */
    quant_params_t b_qp;    /* Bias quantization params */
    int in_dim;
    int out_dim;
    int activation;         /* NN_ACT_* */
} nn_qlayer_t;

#define NN_QMAX_LAYERS 16

/* Quantized model */
typedef struct {
    nn_qlayer_t layers[NN_QMAX_LAYERS];
    int num_layers;
    int max_dim;
} nn_qmodel_t;

/* =============================================================================
 * API
 * =============================================================================*/

/* Reset the quantization weight pool (call before quantizing a new model) */
void nn_quant_reset_pool(void);

/* Quantize a floating-point model to INT16 (symmetric per-tensor).
 * Returns 0 on success, -1 on failure. */
int nn_quantize_model(nn_qmodel_t *qm, const nn_model_t *fm);

/* Quantized forward pass: INT16 compute with FP32 activations.
 * Uses SSE2 PMADDWD for ~2 throughput on dot products. */
void nn_qforward(nn_qmodel_t *model, float *output, const float *input);

/* Run quantization benchmarks and demos */
void nn_quant_demos(void);

#endif /* TENSOROS_NN_QUANTIZE_H */
