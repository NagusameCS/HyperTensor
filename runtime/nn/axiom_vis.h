/*
 * Geodessical Manifold Visualization Protocol
 *
 * Captures Riemannian manifold data at each phase of the Axiom pipeline
 * and writes JSON snapshot files to a directory. An HTML renderer reads
 * these files and provides interactive 2D/3D visualization of:
 *
 *   Phase 1: PCA embedding cloud, scree plot, intrinsic dimension
 *   Phase 2: Head similarity heatmap, symmetry clusters
 *   Phase 3: Metric field, scalar curvature heatmap, Christoffel norms
 *   Phase 4: Axiom confidence distribution, type breakdown
 *   Phase 5: Geodesic trajectories, endpoint accuracy, MRR
 *
 * Usage:
 *   axiom_vis_init("vis_output/");
 *   // ... run axiom pipeline phases, calling axiom_vis_emit_* after each ...
 *   axiom_vis_finalize();
 *
 * Enable via CLI: --vis [output_dir]
 */

#ifndef GEODESSICAL_AXIOM_VIS_H
#define GEODESSICAL_AXIOM_VIS_H

#include "runtime/nn/axiom_beta.h"
#include "runtime/nn/axiom_geo.h"
#include "runtime/nn/axiom_linalg.h"

/* Initialize the visualization system. output_dir will be created if needed.
 * Returns 0 on success, -1 on failure. */
int axiom_vis_init(const char *output_dir);

/* Set model metadata for the current visualization run. */
void axiom_vis_set_model(const char *name, const char *arch,
                         int dim, int layers, int vocab, int heads,
                         const char *quant_type);

/* Phase 1: Emit PCA embedding cloud and dimensionality data.
 * pca: the PCA decomposition; proj_data: [n_samples × n_components] projected embeddings */
int axiom_vis_emit_phase1(const axiom_phase1_t *p1,
                          const axpca_t *pca,
                          const double *proj_data,
                          int n_samples);

/* Phase 2: Emit head similarity matrix and symmetry data. */
int axiom_vis_emit_phase2(const axiom_phase2_t *p2,
                          const double *head_sims,
                          int n_heads);

/* Phase 3: Emit metric field, scalar curvature, Christoffel norms.
 * mf: metric field; curv_scalar: [N] scalar curvature per point */
int axiom_vis_emit_phase3(const axiom_phase3_t *p3,
                          const axgeo_metric_field_t *mf,
                          const axgeo_christoffel_t *ch,
                          const double *curv_scalar,
                          int n_points, int dim);

/* Phase 4: Emit axiom candidates and their confidence/type data. */
int axiom_vis_emit_phase4(const axiom_phase4_t *p4);

/* Phase 5: Emit geodesic trajectories.
 * trajectories: array of n_test trajectory arrays, each [steps × dim]
 * traj_steps: actual step count for each trajectory
 * endpoints_true: [n_test × dim] true target positions
 * cosine_sims: [n_test] per-trajectory cosine similarity */
int axiom_vis_emit_phase5(const axiom_phase5_t *p5,
                          const double **trajectories,
                          const int *traj_steps,
                          const double *endpoints_true,
                          const double *cosine_sims,
                          int n_test, int dim);

/* Write the index.html renderer to the output directory. */
int axiom_vis_finalize(void);

/* Write a combined manifold_export.json for external 3D viewers. */
int axiom_vis_export(const char *output_path);

/* Query whether visualization is active. */
int axiom_vis_active(void);

#endif /* GEODESSICAL_AXIOM_VIS_H */
