/* =============================================================================
 * TensorOS - Tensor Execution Engine
 *
 * The core compute engine that dispatches tensor operations to the
 * appropriate hardware backend (CPU/GPU/TPU). Provides:
 * - Automatic backend selection based on tensor size and device state
 * - Operator fusion for common patterns
 * - Asynchronous execution with dependency tracking
 * - Memory-efficient gradient computation
 * =============================================================================*/

#ifndef TENSOROS_TENSOR_ENGINE_H
#define TENSOROS_TENSOR_ENGINE_H

#include "kernel/core/kernel.h"
#include "runtime/pseudocode/pseudocode_jit.h"

/* =============================================================================
 * Compute Graph
 * Represents a computation as a DAG of tensor operations
 * =============================================================================*/

typedef enum {
    ENGINE_BACKEND_CPU  = 0,
    ENGINE_BACKEND_GPU  = 1,
    ENGINE_BACKEND_TPU  = 2,
    ENGINE_BACKEND_AUTO = 3,  /* Let engine decide */
} engine_backend_t;

typedef struct compute_node {
    uint32_t            id;
    tir_opcode_t        op;    /* Uses TIR opcodes from JIT */
    tensor_desc_t       output;
    tensor_desc_t       inputs[4];
    uint32_t            num_inputs;
    engine_backend_t    backend;
    bool                completed;
    uint64_t            exec_time_ns;
    struct compute_node *deps[4];  /* Dependencies */
    uint32_t            num_deps;
} compute_node_t;

#define MAX_GRAPH_NODES 1024

typedef struct {
    compute_node_t nodes[MAX_GRAPH_NODES];
    uint32_t       node_count;
    bool           compiled;
    engine_backend_t default_backend;
} compute_graph_t;

/* =============================================================================
 * Tensor Engine API
 * =============================================================================*/

/* Initialization */
int  tensor_engine_init(void);

/* Eager execution (immediate) */
int  tensor_matmul(tensor_desc_t *C, const tensor_desc_t *A,
                    const tensor_desc_t *B);
int  tensor_add(tensor_desc_t *C, const tensor_desc_t *A,
                 const tensor_desc_t *B);
int  tensor_relu(tensor_desc_t *output, const tensor_desc_t *input);
int  tensor_softmax(tensor_desc_t *output, const tensor_desc_t *input, int axis);
int  tensor_layernorm(tensor_desc_t *output, const tensor_desc_t *input,
                       const tensor_desc_t *gamma, const tensor_desc_t *beta,
                       float epsilon);
int  tensor_attention(tensor_desc_t *output,
                       const tensor_desc_t *Q, const tensor_desc_t *K,
                       const tensor_desc_t *V, float scale);

/* Graph-based execution (deferred, optimized) */
compute_graph_t *tensor_graph_create(void);
compute_node_t  *tensor_graph_add_op(compute_graph_t *graph, tir_opcode_t op,
                                       tensor_desc_t *inputs, uint32_t num_inputs);
int  tensor_graph_compile(compute_graph_t *graph);
int  tensor_graph_execute(compute_graph_t *graph);
void tensor_graph_destroy(compute_graph_t *graph);

/* Autograd (automatic differentiation) */
int  tensor_backward(tensor_desc_t *loss);
int  tensor_zero_grad(tensor_desc_t *params, uint32_t count);

/* Utility */
int  tensor_fill(tensor_desc_t *tensor, float value);
int  tensor_random(tensor_desc_t *tensor, float min, float max);
int  tensor_copy(tensor_desc_t *dst, const tensor_desc_t *src);
int  tensor_to_device(tensor_desc_t *tensor, uint32_t device_id);
int  tensor_to_dtype(tensor_desc_t *tensor, tensor_dtype_t new_dtype);

#endif /* TENSOROS_TENSOR_ENGINE_H */
