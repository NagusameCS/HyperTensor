# ================================================================
#  Install-Geod.ps1 — Install Geodessical CLI globally as 'geod'
#
#  Run once (as user, no admin required for per-user install):
#    .\Install-Geod.ps1
#
#  What it does:
#    1. Builds geodessical.exe if not already built
#    2. Copies geod.ps1 + geod.cmd + geodessical.exe to ~/.geod/bin/
#    3. Adds ~/.geod/bin to your user PATH
#    4. Creates a models directory at ~/.geod/models/
#    5. Verifies 'geod version' works from any terminal
# ================================================================

$ErrorActionPreference = "Stop"
$ScriptDir = $PSScriptRoot
$InstallDir = Join-Path $env:USERPROFILE ".geod\bin"
$ModelDir   = Join-Path $env:USERPROFILE ".geod\models"

Write-Host ""
Write-Host "  Geodessical Installer" -ForegroundColor Cyan
Write-Host "  Installing 'geod' CLI to $InstallDir" -ForegroundColor Cyan
Write-Host ""

# ---- 1. Ensure executable is built -----------------------------------------
$ExeSrc = Join-Path $ScriptDir "build_host\geodessical.exe"
if (-not (Test-Path $ExeSrc)) {
    Write-Host "[install] Building geodessical.exe..." -ForegroundColor Yellow
    Push-Location $ScriptDir
    .\build_host.ps1
    Pop-Location
    if (-not (Test-Path $ExeSrc)) {
        Write-Host "[install] Build failed — cannot install." -ForegroundColor Red
        exit 1
    }
    Write-Host "[install] Build succeeded." -ForegroundColor Green
} else {
    Write-Host "[install] Found $ExeSrc" -ForegroundColor Green
}

# ---- 2. Create install directory -------------------------------------------
New-Item -ItemType Directory -Force -Path $InstallDir | Out-Null
New-Item -ItemType Directory -Force -Path $ModelDir   | Out-Null

# ---- 3. Copy files ---------------------------------------------------------
Copy-Item $ExeSrc (Join-Path $InstallDir "geodessical.exe") -Force
Copy-Item (Join-Path $ScriptDir "geod.ps1")  (Join-Path $InstallDir "geod.ps1")  -Force
Copy-Item (Join-Path $ScriptDir "geod.cmd")  (Join-Path $InstallDir "geod.cmd")  -Force
Write-Host "[install] Copied files to $InstallDir" -ForegroundColor Green

# ---- 4. Add to user PATH (idempotent) --------------------------------------
$CurrentPath = [Environment]::GetEnvironmentVariable("PATH", "User")
if ($CurrentPath -notlike "*$InstallDir*") {
    [Environment]::SetEnvironmentVariable("PATH", "$CurrentPath;$InstallDir", "User")
    Write-Host "[install] Added $InstallDir to user PATH" -ForegroundColor Green
    Write-Host "[install] NOTE: Restart your terminal for PATH change to take effect." -ForegroundColor Yellow
} else {
    Write-Host "[install] $InstallDir already in PATH" -ForegroundColor Green
}

# ---- 5. Quick smoke test (current session PATH) ----------------------------
$env:PATH = "$env:PATH;$InstallDir"
Write-Host ""
Write-Host "[install] Smoke test:" -ForegroundColor Cyan
& (Join-Path $InstallDir "geod.cmd") version
Write-Host ""
Write-Host "  Installation complete!" -ForegroundColor Green
Write-Host "  Open a new terminal and try:" -ForegroundColor White
Write-Host ""
Write-Host "    geod list" -ForegroundColor Yellow
Write-Host "    geod run gemma4 -p `"hello`"" -ForegroundColor Yellow
Write-Host "    geod ott gemma4 --axiom-fast" -ForegroundColor Yellow
Write-Host "    geod theorem gemma4" -ForegroundColor Yellow
Write-Host ""
