function Get-CHeader {
    $lines  = @("/*")
    foreach ($l in $logoLines) { $lines += " * $l" }
    $lines += " */"
    return $lines -join "`n"
}

function Get-HashHeader {
    $lines = @()
    foreach ($l in $logoLines) { $lines += "# $l" }
    return $lines -join "`n"
}

$root = "C:\Users\legom\HyperTensor"
$exclude = @('\.venv', '\\archives\\', '\\build\\', '\\build_host\\', '\\benchmarks\\', '\\axiom_vis\\', '\\data\\')

$files = Get-ChildItem -Path $root -Recurse -File | Where-Object {
    $fp = $_.FullName
    $excluded = $false
    foreach ($ex in $exclude) { if ($fp -match [regex]::Escape($ex).Replace([regex]::Escape('\'), '\\')) { $excluded = $true; break } }
    if ($excluded) { return $false }
    $ext = $_.Extension.ToLower()
    return $ext -in @('.c', '.h', '.cpp', '.ps1', '.sh') -or $_.Name -in @('CMakeLists.txt', 'Makefile')
}

$cHeader    = Get-CHeader
$hashHeader = Get-HashHeader
$count = 0

foreach ($f in $files) {
    $ext  = $f.Extension.ToLower()
    $name = $f.Name

    # Determine comment style
    if ($ext -in @('.c', '.h', '.cpp')) {
        $header = $cHeader
    } elseif ($ext -in @('.ps1', '.sh') -or $name -in @('CMakeLists.txt', 'Makefile')) {
        $header = $hashHeader
    } else {
        continue
    }

    $existing = [System.IO.File]::ReadAllText($f.FullName, [System.Text.Encoding]::UTF8)

    # Skip if already has logo (idempotent)
    if ($existing.Contains("::::::::::::::::::::::::::")) { continue }

    $newContent = $header + "`n`n" + $existing
    [System.IO.File]::WriteAllText($f.FullName, $newContent, [System.Text.Encoding]::UTF8)
    $count++
    Write-Host "  + $($f.FullName.Replace($root + '\', ''))"
}

Write-Host "`nDone. Added logo header to $count files."
