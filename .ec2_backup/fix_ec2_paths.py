"""fix_ec2_paths.py — Make all scripts runnable locally by patching EC2 hardcoded paths.

Run once: python scripts/fix_ec2_paths.py

Replaces /home/ubuntu/... paths with auto-created local benchmark dirs.
Backs up original files to .ec2_backup/.
"""
import os, sys, re, shutil
from pathlib import Path

REPO_ROOT = Path(__file__).parent.parent
SCRIPTS_DIR = REPO_ROOT / "scripts"
BACKUP_DIR = REPO_ROOT / ".ec2_backup"
BACKUP_DIR.mkdir(exist_ok=True)

EC2_PATH = "/home/ubuntu/benchmarks"
LOCAL_BASE = str(REPO_ROOT / "benchmarks").replace("\\", "/")

patched = 0
skipped = 0

for py_file in sorted(SCRIPTS_DIR.glob("**/*.py")):
    content = py_file.read_text(encoding='utf-8', errors='ignore')
    
    if EC2_PATH not in content:
        skipped += 1
        continue
    
    # Backup
    rel = py_file.relative_to(SCRIPTS_DIR)
    backup_path = BACKUP_DIR / rel
    backup_path.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(py_file, backup_path)
    
    # Replace /home/ubuntu/benchmarks/... with auto-local path
    # Pattern: /home/ubuntu/benchmarks/something → benchmarks/something
    new_content = content.replace(EC2_PATH + "/", LOCAL_BASE + "/")
    new_content = new_content.replace(EC2_PATH, LOCAL_BASE)
    
    # Also replace os.makedirs("/home/ubuntu/benchmarks/...") patterns
    new_content = new_content.replace(
        f'os.makedirs("{EC2_PATH}', 
        f'os.makedirs("{LOCAL_BASE}'
    )
    
    py_file.write_text(new_content, encoding='utf-8')
    patched += 1
    print(f"  PATCHED: {rel}")

print(f"\n  Patched: {patched}, Skipped: {skipped}")
print(f"  Backups: {BACKUP_DIR}")
print(f"  DONE")
