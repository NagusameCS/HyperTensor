"""Test distilled drafters at gamma=8."""
import sys; sys.path.insert(0, '.')
import json, torch
from hyperretro.bench.spec_decode_sim import simulate_speculative_decode

prompt = "The history of artificial intelligence began in antiquity with myths and stories of artificial beings endowed with intelligence by master craftsmen. In the 1950s a generation of scientists"
model = "Qwen/Qwen2.5-0.5B"

configs = [
    ("k=640 MSE distilled", None, 640, "benchmarks/distill_e2e/qwen05b_k640_s4_r8"),
    ("k=640 KL distilled", None, 640, "benchmarks/distill_e2e/qwen05b_k640_s4_r8_kl"),
    ("k=832 vanilla GRC", None, 832, None),
]

results = {}
for label, _, k, drafter_path in configs:
    print(f"\n--- {label} (gamma=8) ---", flush=True)
    r = simulate_speculative_decode(
        model, prompt, k=k, gamma=8,
        device="cuda", dtype="float16", max_cycles=15,
        drafter_path=drafter_path,
    )
    results[label] = {
        "mean_accept": r["mean_acceptance_length"],
        "accept_rate": r["acceptance_rate"],
        "tokens_per_cycle": r["tokens_per_cycle"],
        "wallclock": r["wallclock_speedup"],
        "draft_ms": r["median_draft_ms"],
    }
    print(f"  accept={r['mean_acceptance_length']:.1f}/8 ({r['acceptance_rate']:.0%}) "
          f"wallclock={r['wallclock_speedup']:.3f}x")
    torch.cuda.empty_cache()

print(f"\n{'Drafter':<30s} {'Accept':>8s} {'Rate':>8s} {'Tok/Cyc':>8s} {'Wall-Clock':>10s}")
print("-" * 70)
for label in ["k=640 MSE distilled", "k=640 KL distilled", "k=832 vanilla GRC"]:
    r = results[label]
    print(f"{label:<30s} {r['mean_accept']:8.2f}  {r['accept_rate']:8.1%}  "
          f"{r['tokens_per_cycle']:8.2f}  {r['wallclock']:10.3f}x")

# Also include gamma=4 reference
print(f"\n{'k=768 vanilla GRC gamma=4':<30s} {'3.55':>8s}  {'88.8%':>8s}  {'4.55':>8s}  {'0.990':>10s}x")
print(f"{'k=768 vanilla GRC gamma=8':<30s} {'6.85':>8s}  {'85.6%':>8s}  {'7.85':>8s}  {'1.002':>10s}x")

with open("benchmarks/gamma8_distilled.json", "w") as f:
    json.dump(results, f, indent=2)
print("\nSaved.")
