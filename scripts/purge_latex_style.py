"""purge_latex_style.py — Remove ALL bold and italic formatting from LaTeX files.

Replaces \\textbf{}, \\textit{}, \\emph{}, \\mathbf{}, \\mathit{}, \\mathsf{}, 
\\mathtt{}, \\mathcal{}, \\mathbb{}, \\bm{}, {\\bf ...}, {\\it ...} with plain text.

Also removes \\renewcommand{\\bfdefault} and \\renewcommand{\\itdefault} macros.

Backs up to .latex_style_backup/ before modifying.
"""
import os, re, shutil
from pathlib import Path

REPO = Path(__file__).parent.parent
BACKUP = REPO / ".latex_style_backup"
BACKUP.mkdir(exist_ok=True)

# Patterns to neutralize (remove formatting, keep content)
PATTERNS = [
    # Standard LaTeX
    (r'\\textbf\{([^}]*)\}', r'\1'),      # \textbf{text} -> text
    (r'\\textit\{([^}]*)\}', r'\1'),      # \textit{text} -> text
    (r'\\emph\{([^}]*)\}', r'\1'),        # \emph{text} -> text
    (r'\\mathbf\{([^}]*)\}', r'\1'),      # \mathbf{text} -> text
    (r'\\mathit\{([^}]*)\}', r'\1'),      # \mathit{text} -> text
    (r'\\mathsf\{([^}]*)\}', r'\1'),      # \mathsf{text} -> text
    (r'\\mathtt\{([^}]*)\}', r'\1'),      # \mathtt{text} -> text
    (r'\\mathcal\{([^}]*)\}', r'\1'),     # \mathcal{text} -> text
    (r'\\mathbb\{([^}]*)\}', r'\1'),      # \mathbb{text} -> text
    (r'\\bm\{([^}]*)\}', r'\1'),          # \bm{text} -> text
    (r'\\underline\{([^}]*)\}', r'\1'),   # \underline{text} -> text
    (r'\\textsc\{([^}]*)\}', r'\1'),      # \textsc{text} -> text
    (r'\\texttt\{([^}]*)\}', r'\1'),      # \texttt{text} -> text (code font)
    
    # Old-style
    (r'\{\\bf\s+([^}]*)\}', r'\1'),       # {\bf text} -> text
    (r'\{\\bfseries\s+([^}]*)\}', r'\1'), # {\bfseries text} -> text
    (r'\{\\it\s+([^}]*)\}', r'\1'),       # {\it text} -> text
    (r'\{\\itshape\s+([^}]*)\}', r'\1'),  # {\itshape text} -> text
]

# Lines to remove entirely
REMOVE_LINES_CONTAINING = [
    r'\\renewcommand\{\\bfdefault\}',
    r'\\renewcommand\{\\itdefault\}',
    r'\\renewcommand\{\\bfdefault\}',
    r'\\renewcommand\{\\itdefault\}',
    r'\\bfdefault',
    r'\\itdefault',
    r'\\mddefault',
    r'\\updefault',
]

purged = 0
for root, dirs, files in os.walk(REPO):
    dirs[:] = [d for d in dirs if d not in {'.git', '.latex_style_backup', '.ec2_backup', '.emoji_backup', '__pycache__', '.venv'}]
    
    for fname in files:
        if not fname.endswith('.tex'):
            continue
        
        fpath = Path(root) / fname
        content = fpath.read_text(encoding='utf-8', errors='ignore')
        original = content
        changed = False
        
        # Remove formatting, keep content
        for pattern, repl in PATTERNS:
            new_content = re.sub(pattern, repl, content)
            if new_content != content:
                content = new_content
                changed = True
        
        # Handle nested cases (run twice)
        for pattern, repl in PATTERNS:
            new_content = re.sub(pattern, repl, content)
            if new_content != content:
                content = new_content
                changed = True
        
        # Remove lines with style-redefining commands
        lines = content.split('\n')
        new_lines = []
        for line in lines:
            should_keep = True
            for remove_pat in REMOVE_LINES_CONTAINING:
                if re.search(remove_pat, line):
                    should_keep = False
                    changed = True
                    break
            if should_keep:
                new_lines.append(line)
        content = '\n'.join(new_lines)
        
        if changed:
            # Backup
            rel = fpath.relative_to(REPO)
            backup_path = BACKUP / rel
            backup_path.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(fpath, backup_path)
            
            fpath.write_text(content, encoding='utf-8')
            purged += 1
            print(f"  PURGED STYLE: {rel}")

print(f"\n  Files purged of bold/italic: {purged}")
print(f"  Backups: {BACKUP}")
print("  DONE")
