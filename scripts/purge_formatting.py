# 
# BOLD/ITALIC PATTERNS
# 
# LaTeX: \textbf{...}, \textit{...}, \emph{...}, \mathbf{...}, \mathit{...}, \mathtt{...}, \mathsf{...}, \mathtt{...}
# Keep the inner content, strip the command wrapper
LATEX_BOLD_ITALIC = [
    (re.compile(r'\\textbf\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\textit\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\emph\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\mathbf\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\mathit\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\mathtt\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\mathsf\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\textsc\{([^}]*)\}'), r'\1'),   # small caps too
    (re.compile(r'\\textup\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\textsl\{([^}]*)\}'), r'\1'),
    (re.compile(r'\\textbf\s*\{([^}]*)\}'), r'\1'),
]

# HTML: <strong>...</strong>, <b>...</b>, <em>...</em>, <i>...</i>
HTML_BOLD_ITALIC = [
    (re.compile(r'<strong>(.*?)</strong>', re.DOTALL | re.IGNORECASE), r'\1'),
    (re.compile(r'<b>(.*?)</b>', re.DOTALL | re.IGNORECASE), r'\1'),
    (re.compile(r'<em>(.*?)</em>', re.DOTALL | re.IGNORECASE), r'\1'),
    (re.compile(r'<i>(.*?)</i>', re.DOTALL | re.IGNORECASE), r'\1'),
]

# Markdown: **bold**, *italic*, __bold__, _italic_
MD_BOLD_ITALIC = [
    (re.compile(r'\*\*(.+?)\*\*'), r'\1'),
    (re.compile(r'__(.+?)__'), r'\1'),
    (re.compile(r'(?<!\*)\*(.+?)\*(?!\*)'), r'\1'),
    (re.compile(r'(?<!_)_(.+?)_(?!_)'), r'\1'),
]

# 
STATS = {'emoji': 0, 'bold_italic': 0, 'files_changed': 0}

def process_file(fpath):
    """Remove emojis and bold/italic from a file. Returns True if changed."""
    ext = os.path.splitext(fpath)[1].lower()
    if ext not in TEXT_EXTS:
        return False
    
    try:
        with open(fpath, 'r', encoding='utf-8', errors='ignore') as f:
            content = f.read()
    except Exception:
        return False
    
    original = content
    emoji_count = 0
    
    # Remove emojis
    new_content, n = EMOJI_PATTERN.subn('', content)
    emoji_count = n
    
    # Remove bold/italic based on file type
    if ext == '.tex' or ext == '.sty' or ext == '.cls' or ext == '.bib':
        for pat, repl in LATEX_BOLD_ITALIC:
            new_content, n = pat.subn(repl, new_content)
            emoji_count += n  # count as formatting changes
    elif ext == '.html' or ext == '.htm':
        for pat, repl in HTML_BOLD_ITALIC:
            new_content, n = pat.subn(repl, new_content)
            emoji_count += n
    elif ext == '.md':
        for pat, repl in MD_BOLD_ITALIC:
            new_content, n = pat.subn(repl, new_content)
            emoji_count += n
        # Also remove HTML tags in .md
        for pat, repl in HTML_BOLD_ITALIC:
            new_content, n = pat.subn(repl, new_content)
            emoji_count += n
    
    if new_content != original:
        try:
            with open(fpath, 'w', encoding='utf-8') as f:
                f.write(new_content)
            return True, emoji_count
        except Exception as e:
            return False, 0
    
    return False, 0


if __name__ == '__main__':
    DRY_RUN = '--dry-run' in sys.argv
    if DRY_RUN:
        print('DRY RUN — no files will be modified')
    
    changed = []
    for root, dirs, files in os.walk(ROOT):
        dirs[:] = [d for d in dirs if d not in EXCLUDE_DIRS and not d.startswith('.')]
        for fname in files:
            fpath = os.path.join(root, fname)
            ext = os.path.splitext(fname)[1].lower()
            if ext not in TEXT_EXTS:
                continue
            changed_flag, count = process_file(fpath)
            if changed_flag:
                rel = os.path.relpath(fpath, ROOT)
                changed.append((rel, count))
    
    print(f'Files with emoji/bold/italic removed: {len(changed)}')
    for rel, cnt in changed[:60]:
        print(f'  [{cnt:>4d}] {rel}')
    if len(changed) > 60:
        print(f'  ... and {len(changed)-60} more files')
    
    total = sum(c for _, c in changed)
    print(f'\nTotal formatting removals: {total}')
    
    if DRY_RUN:
        print('DRY RUN — no changes applied. Remove --dry-run to apply.')
