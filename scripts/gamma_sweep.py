"""Gamma sweep: test different draft lengths."""
import sys; sys.path.insert(0, '.')
import json, torch
from hyperretro.bench.spec_decode_sim import simulate_speculative_decode

prompt = "The history of artificial intelligence began in antiquity with myths and stories of artificial beings endowed with intelligence by master craftsmen. In the 1950s a generation of scientists"
model = "Qwen/Qwen2.5-0.5B"

results = {}
for gamma in [1, 2, 4, 8]:
    print(f"\n--- gamma={gamma} ---", flush=True)
    r = simulate_speculative_decode(
        model, prompt, k=768, gamma=gamma,
        device="cuda", dtype="float16", max_cycles=20,
    )
    results[f"g{gamma}"] = {
        "gamma": gamma,
        "mean_accept": r["mean_acceptance_length"],
        "accept_rate": r["acceptance_rate"],
        "tokens_per_cycle": r["tokens_per_cycle"],
        "wallclock": r["wallclock_speedup"],
        "draft_ms": r["median_draft_ms"],
        "verify_ms": r["median_verify_ms"],
    }
    print(f"  accept={r['mean_acceptance_length']:.1f}/{gamma} "
          f"({r['acceptance_rate']:.0%}) wallclock={r['wallclock_speedup']:.3f}x")
    torch.cuda.empty_cache()

print(f"\n{'gamma':>6s}  {'accept':>8s}  {'rate':>8s}  {'tok/cyc':>8s}  {'wallclock':>10s}  {'draft_ms':>10s}  {'verify_ms':>10s}")
print("-" * 70)
for gamma in [1, 2, 4, 8]:
    r = results[f"g{gamma}"]
    print(f"{gamma:6d}  {r['mean_accept']:8.2f}  {r['accept_rate']:8.1%}  "
          f"{r['tokens_per_cycle']:8.2f}  {r['wallclock']:10.3f}  "
          f"{r['draft_ms']:10.1f}  {r['verify_ms']:10.1f}")

with open("benchmarks/gamma_sweep.json", "w") as f:
    json.dump(results, f, indent=2)
print("\nSaved to benchmarks/gamma_sweep.json")
