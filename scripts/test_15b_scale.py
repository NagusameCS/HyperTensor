"""Test HyperRetro at 1.5B scale."""
import sys; sys.path.insert(0, '.')
import json, time, torch
from hyperretro.bench.real_speculative import run_compressed_drafter

model_id = "Qwen/Qwen2.5-1.5B"
prompt = "The history of artificial intelligence began"
print(f"Testing {model_id} at 1.5B scale (fp16)...")

for k in [1024, 768, 512]:
    print(f"\n--- k={k} ---", flush=True)
    try:
        r = run_compressed_drafter(
            model_id, prompt, k=k, n_drafts=4,
            device="cuda", dtype="float16",
        )
        out = {
            "k": k,
            "top1_accept": r["top1_accept"],
            "top5_hit": r["top5_hit"],
            "median_ms_per_propose": r["median_ms_per_propose"],
        }
        print(json.dumps(out, indent=2), flush=True)
    except torch.cuda.OutOfMemoryError:
        print(f"  OOM at k={k}, trying CPU fallback...")
        torch.cuda.empty_cache()
        r = run_compressed_drafter(
            model_id, prompt, k=k, n_drafts=4,
            device="cpu", dtype="float32",
        )
        out = {
            "k": k,
            "top1_accept": r["top1_accept"],
            "top5_hit": r["top5_hit"],
            "median_ms_per_propose": r["median_ms_per_propose"],
            "note": "CPU fallback (GPU OOM)",
        }
        print(json.dumps(out, indent=2), flush=True)
    except Exception as e:
        print(f"  ERROR: {e}")
    torch.cuda.empty_cache()

print("\nDone.")
