"""GPU speculative bench sweep — all ranks."""
import sys, json, torch
sys.path.insert(0, '.')
from hyperretro.bench.real_speculative import run_compressed_drafter

prompt = (
    "The history of artificial intelligence began in antiquity with myths "
    "and stories of artificial beings endowed with intelligence by master "
    "craftsmen."
)
results = {}

for k in [832, 768, 640, 448]:
    print(f"\n--- k={k} ---", flush=True)
    r = run_compressed_drafter(
        "Qwen/Qwen2.5-0.5B", prompt, k=k, n_drafts=4,
        device="cuda", dtype="float32",
    )
    out = {
        "k": k,
        "top1_accept": r["top1_accept"],
        "top5_hit": r["top5_hit"],
        "median_ms_per_propose": r["median_ms_per_propose"],
    }
    results[f"k{k}"] = out
    print(json.dumps(out, indent=2), flush=True)
    torch.cuda.empty_cache()

print("\n" + "=" * 55)
print(f"{'rank':>6s}  {'top-1':>8s}  {'top-5':>8s}  {'ms/propose':>12s}")
print("-" * 55)
for k in [832, 768, 640, 448]:
    r = results[f"k{k}"]
    print(f"{k:6d}  {r['top1_accept']:8.3f}  {r['top5_hit']:8.3f}  {r['median_ms_per_propose']:12.1f}")

with open("benchmarks/hyperretro_gpu_spec_sweep.json", "w") as f:
    json.dump(results, f, indent=2)
print("\nSaved to benchmarks/hyperretro_gpu_spec_sweep.json")
