#!/usr/bin/env python3
"""Fix all paper .tex files: remove version labels, fix CECI/Part-J, clean up."""
import re
from pathlib import Path

ROOT = Path("c:/Users/legom/HyperTensor/ARXIV_SUBMISSIONS")
FIXES = 0

for paper_dir in sorted(ROOT.glob("paper-*")):
    for tex in paper_dir.glob("*.tex"):
        original = tex.read_text(encoding="utf-8", errors="replace")
        content = original

        # 1. Fix date version labels
        content = re.sub(r'\\date\{May 2026 \(v0\.\d[^}]*\)\}', r'\\date{May 2026}', content)
        content = re.sub(r'\\date\{April 2026 \(v0\.\d[^}]*\)\}', r'\\date{May 2026}', content)
        content = re.sub(r'\\date\{May 2026 \(v\d+\.\d+[^}]*\)\}', r'\\date{May 2026}', content)

        # 2. Fix Part J -> Part X
        content = content.replace('Part~J', 'Part~X').replace('Part J', 'Part X')

        # 3. Fix CMVB/CECI: "Chimeric Model Vector Bridging (CECI)" -> correct
        # CECI = Cross-Embedding Compatibility Index
        content = content.replace(
            'Chimeric Model Vector Bridging (CECI)',
            'Cross-Embedding Compatibility Index (CECI)')
        content = content.replace(
            'Chimeric Model Vector Bridging',
            'Cross-Embedding Compatibility Index')
        content = content.replace(
            'CMVB',
            'CECI')

        # 4. Remove "v0.2 design specification" phrase --- simplify
        content = re.sub(
            r'v0\.\d+ design specification[s]?',
            'design specification',
            content)
        content = re.sub(
            r'deferred to a companion v0\.\d+ release',
            'deferred to a companion measurement',
            content)

        # 5. Remove version strings from body text
        content = content.replace('(v1.0, experiment complete)', '')
        content = content.replace('(v0.4, measured on SmolLM2-135M)', '')
        content = content.replace("v0.6.0 ``Synapse''", "geodessical v0.6")

        # 6. Remove bold from abstract/body where it's just decoration
        # \textbf{X} -> X for common patterns
        content = content.replace('\\textbf{CECI feasibility map}', 'CECI feasibility map')
        content = content.replace('\\textbf{Cross-Embedding Compatibility Index (CECI)}', 'Cross-Embedding Compatibility Index (CECI)')
        content = content.replace('\\textbf{Full CECI splice}', 'Full CECI splice')
        content = content.replace('\\textbf{A CECI feasibility map}', 'A CECI feasibility map')

        # 7. Fix remaining v0.2/v0.6 references in body text
        for old, new in [
            ('the v0.2 measurement', 'the measurement'),
            ('queued for v0.2', 'deferred'),
            ('run queued for v0.2', 'run deferred'),
            ('since v0.6.0)', 'since geodessical v0.6)'),
            ('The calibration question for v0.2', 'The open calibration question'),
            ('a companion v0.2 release', 'a companion measurement'),
            ('Geodessical v0.6.0 ``Synapse\'\'', 'Geodessical v0.6 (Synapse)'),
        ]:
            if old in content:
                content = content.replace(old, new)

        if content != original:
            tex.write_text(content, encoding="utf-8")
            FIXES += 1
            print(f"  Fixed: {tex.relative_to(ROOT)}")

print(f"\n{FIXES} files updated")
