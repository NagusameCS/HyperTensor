"""FIX FUSION: Differential centroids + metric-directed routing.

Key insight: Raw trajectory centroids overlap because models produce
similar hidden states for all prompts. But what makes Goku DIFFERENT
from Yamcha (the baseline) IS domain-specific.

Fix: Use differential centroids — center each Saiyan's trajectories
by subtracting the baseline (Yamcha) centroid. These differential
vectors point in directions where the domain actually matters.

Also: Use metric eigenvectors. Each Saiyan's COG metric tensor grew
differently because domain-specific trajectory outer products
accumulated. The top eigenvector of Goku's metric points in the
"math direction" of k-space. Route queries using metric alignment.
"""
