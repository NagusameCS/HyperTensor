# COMPUTE CENTROIDS (RAW + DIFFERENTIAL + METRIC)
# ============================================================================
raw_centroids = {}
diff_centroids = {}  # Saiyan - Yamcha
metric_top_ev = {}   # Top eigenvector of metric

# Baseline centroid
yamcha_trajs = torch.stack([t["proj"].float() for t in saiyans[BASELINE]["trajectories"]])
baseline_centroid = F.normalize(yamcha_trajs.mean(dim=0).unsqueeze(0), dim=1).squeeze(0)

for name, data in saiyans.items():
    if not data["trajectories"]:
        continue
    
    projs = torch.stack([t["proj"].float() for t in data["trajectories"]])
    raw = F.normalize(projs.mean(dim=0).unsqueeze(0), dim=1).squeeze(0)
    raw_centroids[name] = raw
    
    # Differential: how this Saiyan differs from baseline
    if name != BASELINE:
        diff = raw - baseline_centroid
        diff = F.normalize(diff.unsqueeze(0), dim=1).squeeze(0)
        diff_centroids[name] = diff
    
    # Metric top eigenvector
    metric = data["metric"]
    evals, evecs = torch.linalg.eigh(metric.float())
    top_ev = evecs[:, -1]  # largest eigenvalue
    metric_top_ev[name] = top_ev

# Differential centroids for Yamcha = zero vector (identity)
diff_centroids[BASELINE] = torch.zeros(K)

