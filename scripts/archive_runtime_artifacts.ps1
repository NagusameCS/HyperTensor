param(
    [string]$Root = "",
    [string]$Tag = ""
)

$ErrorActionPreference = "Stop"

if (-not $Root) {
    $Root = Split-Path -Parent $PSScriptRoot
}

if (-not $Tag) {
    $Tag = Get-Date -Format "yyyyMMdd_HHmmss"
}

$archiveRoot = Join-Path $Root "archives"
$destRoot = Join-Path $archiveRoot ("runtime-artifacts_" + $Tag)

$rules = @(
    @{
        bucket = "logs"
        patterns = @(
            "build_output.txt",
            "geo_srv*.log",
            "qemu_*.log",
            "qemu_out*.txt",
            "qemu_sne.txt"
        )
    },
    @{
        bucket = "reports"
        patterns = @(
            "axiom_beta3_report.json",
            "axiom_beta4*.json",
            "axiom_beta_boundary_test.json",
            "axiom_beta_report_geodessic*.json",
            "bench_axiom*.json"
        )
    },
    @{
        bucket = "scratch-text"
        patterns = @(
            "axiom_fresh.txt",
            "bench*.txt",
            "curr_test.txt",
            "dbg_conf*.txt",
            "diag_out.txt",
            "diff_full.txt",
            "fast_test.txt",
            "final_test.txt",
            "long_test.txt",
            "peak*.txt",
            "perf*.txt",
            "rebuild.txt",
            "sdbg*.txt",
            "snap_test*.txt",
            "spec*.txt",
            "survey_out*.txt",
            "timing_test.txt",
            "vdbg*.txt",
            "vel_test.txt",
            "vis_final.txt",
            "vis_run*.txt"
        )
    }
)

$moved = [System.Collections.ArrayList]::new()

foreach ($rule in $rules) {
    $bucketPath = Join-Path $destRoot $rule.bucket
    $null = New-Item -ItemType Directory -Force -Path $bucketPath

    foreach ($pattern in $rule.patterns) {
        $items = @(Get-ChildItem -Path $Root -File -Filter $pattern -ErrorAction SilentlyContinue)
        foreach ($item in $items) {
            if ($item.DirectoryName -like (Join-Path $archiveRoot "*")) {
                continue
            }

            $target = Join-Path $bucketPath $item.Name
            Move-Item -LiteralPath $item.FullName -Destination $target -Force
            [void]$moved.Add([PSCustomObject]@{
                bucket = $rule.bucket
                file = $item.Name
            })
        }
    }
}

$summaryPath = Join-Path $destRoot "archive_manifest.json"
[ordered]@{
    root = $Root
    archive = $destRoot
    moved_files = $moved
} | ConvertTo-Json -Depth 5 | Set-Content -Path $summaryPath -Encoding UTF8

Write-Host ("[archive] moved {0} files into {1}" -f $moved.Count, $destRoot)
Write-Host ("[archive] manifest: {0}" -f $summaryPath)
