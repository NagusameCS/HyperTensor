"""Sweep spec decode simulation across ranks."""
import sys; sys.path.insert(0, '.')
import json, torch
from hyperretro.bench.spec_decode_sim import simulate_speculative_decode

prompt = "The history of artificial intelligence began in antiquity with myths and stories of artificial beings endowed with intelligence by master craftsmen. In the 1950s a generation of scientists"

results = {}
for k in [832, 768, 640, 448]:
    print(f"\n{'='*50}")
    print(f"k={k}")
    r = simulate_speculative_decode(
        "Qwen/Qwen2.5-0.5B", prompt, k=k, gamma=4,
        device="cuda", dtype="float32", max_cycles=20,
    )
    results[f"k{k}"] = {
        "mean_accept": r["mean_acceptance_length"],
        "accept_rate": r["acceptance_rate"],
        "tokens_per_cycle": r["tokens_per_cycle"],
        "speedup_theoretical": r["speedup_vs_autoregressive"],
        "speedup_wallclock": r["wallclock_speedup"],
        "draft_ms": r["median_draft_ms"],
        "verify_ms": r["median_verify_ms"],
    }
    print(f"  accept={r['mean_acceptance_length']:.2f} "
          f"speedup_theo={r['speedup_vs_autoregressive']:.2f}x "
          f"speedup_wall={r['wallclock_speedup']:.2f}x")
    torch.cuda.empty_cache()

print(f"\n{'='*60}")
print(f"SPECULATIVE DECODING SWEEP (γ=4, Qwen2.5-0.5B, GPU fp32)")
print(f"{'k':>6s}  {'accept':>8s}  {'rate':>8s}  {'tok/cyc':>8s}  {'theo sp':>8s}  {'wall sp':>8s}  {'draft ms':>10s}  {'verify ms':>10s}")
print("-" * 75)
for k in [832, 768, 640, 448]:
    r = results[f"k{k}"]
    print(f"{k:6d}  {r['mean_accept']:8.2f}  {r['accept_rate']:8.1%}  "
          f"{r['tokens_per_cycle']:8.2f}  {r['speedup_theoretical']:8.2f}  "
          f"{r['speedup_wallclock']:8.2f}  {r['draft_ms']:10.1f}  {r['verify_ms']:10.1f}")

with open("benchmarks/spec_decode_sweep.json", "w") as f:
    json.dump(results, f, indent=2)
print("\nSaved to benchmarks/spec_decode_sweep.json")
