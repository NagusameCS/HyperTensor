# Comment prefixes per file type
FILE_TYPES = [
    (r'*.tex', '%', None),
    (r'*.py', '#', None),
    (r'*.ps1', '#', None),
    (r'*.sh', '#', None),
    (r'*.c', ' *', '/*\n *\n */\n'),
    (r'*.h', ' *', '/*\n *\n */\n'),
    (r'Makefile', '#', None),
    (r'CMakeLists.txt', '#', None),
]

SKIP_PATTERNS = [
    r'\.venv',
    r'__pycache__',
    r'\.git',
    r'node_modules',
    r'\.latex_style_backup',
    r'releases',
    r'benchmarks',
]

def should_skip(path):
    for pat in SKIP_PATTERNS:
        if pat in path.replace('\\', '/'):
            return True
    return False

def banner_for(filepath):
    """Return (prefix, wrapper_prefix, wrapper_suffix, banner_text) or None."""
    ext = os.path.splitext(filepath)[1].lower()
    basename = os.path.basename(filepath)
    
    for pattern, prefix, wrapper in FILE_TYPES:
        if pattern.startswith('*'):
            if basename.endswith(pattern[1:]) or ext == pattern[1:]:
                return (prefix, wrapper)
        elif pattern == basename:
            return (prefix, wrapper)
    return None

def has_banner(content, prefix):
    """Check if banner already exists."""
    return '::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::' in content

def add_banner(filepath, prefix, wrapper):
    """Prepend banner to file."""
    with open(filepath, 'r', encoding='utf-8', errors='replace') as f:
        content = f.read()
    
    if has_banner(content, prefix):
        return False  # already has it
    
    # Build banner with prefix
    banner_lines = []
    if wrapper:
        # C-style: /*\n * ... */\n
        banner_lines.append('/*')
        banner_lines.append(' * HyperTensor')
        for line in BANNER.split('\n'):
            banner_lines.append(f' *{line}')
        banner_lines.append(' */\n')
    else:
        for line in BANNER.split('\n'):
            banner_lines.append(f'{prefix} {line}')
        banner_lines.append('')
    
    banner_text = '\n'.join(banner_lines) + '\n'
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(banner_text + content)
    
    return True

# Find all files
count = 0
for root_dir, dirs, files in os.walk(ROOT):
    # Skip ignored dirs
    dirs[:] = [d for d in dirs if not any(pat.replace('\\','/') in (root_dir + '/' + d).replace('\\','/') for pat in ['\\.venv','__pycache__','\\.git','node_modules','\\.latex_style_backup','releases','benchmarks'])]
    
    for fname in files:
        fpath = os.path.join(root_dir, fname)
        if should_skip(fpath):
            continue
        
        result = banner_for(fpath)
        if result is None:
            continue
        
        prefix, wrapper = result
        if add_banner(fpath, prefix, wrapper):
            print(f"  Added: {os.path.relpath(fpath, ROOT)}")
            count += 1

print(f"\nBanner added to {count} files")
