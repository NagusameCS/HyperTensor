# Find exact k threshold
param(
    [string]$Model = "Meta-Llama-3.1-8B-Instruct-Q4_K_M.gguf",
    [string]$Exe = ".\build_host\geodessical.exe",
    [int]$Tokens = 128,
    [string]$Prompt = "Write a Python function that returns prime numbers up to n."
)

$now = Get-Date -Format "yyyyMMdd_HHmmss"
$OutDir = ".\benchmarks\gp_threshold_$now"
New-Item -ItemType Directory -Force -Path $OutDir > $null

# Test k values
$test_ks = @(1536, 1600, 1700, 1800, 1900, 1950, 2000, 2048)

Write-Host "K Threshold Diagnostic"
Write-Host "=====================`n"

$baseline_decode = $null
$results = @()

function Run-Test {
    param([int]$k)
    
    $out_file = "$OutDir/k_${k}.txt"
    $cmd = "$Exe `"$Model`" --axex-compress --axex-attn-only --axex-skip-o --axex-weight-pca --axex-compress-rank $k --tokens $Tokens -p `"$Prompt`""
    
    cmd /c "$cmd > `"$out_file`" 2>&1"
    
    $content = Get-Content $out_file -Raw
    if ($content -match "\[GD\] Decode: ([\d.]+) tok/s") {
        return [double]$matches[1]
    }
    return $null
}

foreach ($k in $test_ks) {
    Write-Host "k=${k}... " -NoNewline
    $decode = Run-Test $k
    
    if ($null -ne $decode) {
        if ($null -eq $baseline_decode) {
            Write-Host "$decode tok/s (baseline)"
            $baseline_decode = $decode
        } else {
            $pct = [math]::Round(($decode / $baseline_decode) * 100, 1)
            Write-Host "$decode tok/s ($pct%)"
        }
        $results += @{ k = $k; decode = $decode; pct = $pct }
    } else {
        Write-Host "PARSE FAILED"
    }
}

Write-Host "`nResults saved to: $OutDir"
Write-Host "`nSummary:"
$results | ForEach-Object { Write-Host "  k=$($_.k): $($_.pct)%" }
