# scripts/context_length_sweep.ps1
# -----------------------------------------------------------------------------
# Context-length sweep: decode tok/s vs. prompt length at five points,
# baseline vs. GRC k=1024.  Tests whether the bandwidth-bound win in Paper A
# holds across context regimes (short / medium / long / very long).
#
# Output:
#   docs/data/context_length_sweep.csv
#   docs/data/context_length_sweep.tex   (\input-able table for Paper A)
#
# Honest-or-fail: errors out rather than fabricating any number.
# -----------------------------------------------------------------------------

param(
    [string]$Model    = "C:\Users\legom\models\models--bartowski--Meta-Llama-3.1-8B-Instruct-GGUF\snapshots\bf5b95e96dac0462e2a09145ec66cae9a3f12067\Meta-Llama-3.1-8B-Instruct-Q4_K_M.gguf",
    [string]$Exe      = ".\build_host\geodessical.exe",
    [int[]] $Contexts = @(128, 512, 1024, 2048, 4096),
    [int]   $DecodeTokens = 64,
    [int]   $Repeats  = 5,
    [int]   $Rank     = 1024,
    [int]   $CooldownSec = 30,
    [string]$OutCsv   = "docs/data/context_length_sweep.csv",
    [string]$OutTex   = "docs/data/context_length_sweep.tex"
)

$ErrorActionPreference = "Stop"
if (-not (Test-Path $Exe))   { throw "Executable not found: $Exe" }
if (-not (Test-Path $Model)) { throw "Model not found: $Model" }

function Get-PromptOfLength {
    param([int]$Tokens)
    # Approximate: 1 token ~= 0.75 words for English. Build a deterministic stream.
    $words = "the quick brown fox jumps over the lazy dog and then continues running through the forest looking for adventure under a clear blue summer sky".Split(" ")
    $needed = [int]($Tokens / 0.75) + 16
    $sb = New-Object System.Text.StringBuilder
    for ($i = 0; $i -lt $needed; $i++) { [void]$sb.Append($words[$i % $words.Length]); [void]$sb.Append(" ") }
    return $sb.ToString().Trim()
}

function Invoke-DecodeRun {
    param([int]$Ctx, [string[]]$ExtraArgs)
    $prompt = Get-PromptOfLength $Ctx
    $tps = @()
    for ($r = 0; $r -lt $Repeats; $r++) {
        $args = @($Model, "-p", $prompt, "-n", $DecodeTokens, "--temp", "0") + $ExtraArgs
        $raw  = (& $Exe @args 2>&1) -join "`n"
        if ($LASTEXITCODE -ne 0) { throw "geodessical failed (ctx=$Ctx, repeat=$r): $raw" }
        $mDec = [regex]::Match($raw, 'Decode-only:\s*prefill\s*([\d.]+)\s*ms,\s*([\d.]+)\s*tok/s')
        $mGd  = [regex]::Match($raw, '\[GD\]\s*(\d+)\s+tokens\s+in\s*([\d.]+)\s*ms\s*\(([\d.]+)\s*tok/s\)')
        $val = $null
        if ($mDec.Success) { $val = [double]$mDec.Groups[2].Value }
        elseif ($mGd.Success) { $val = [double]$mGd.Groups[3].Value }
        if ($null -eq $val) { throw "no decode tok/s in output (ctx=$Ctx, repeat=$r): $raw" }
        $tps += $val
        Start-Sleep -Seconds $CooldownSec
    }
    $mean = ($tps | Measure-Object -Average).Average
    $std  = if ($tps.Count -gt 1) {
        [math]::Sqrt((($tps | ForEach-Object { ($_ - $mean) * ($_ - $mean) }) | Measure-Object -Sum).Sum / ($tps.Count - 1))
    } else { 0.0 }
    return [pscustomobject]@{ mean = [math]::Round($mean, 3); std = [math]::Round($std, 3); samples = ($tps -join "|") }
}

# --- run sweep ---------------------------------------------------------------
$rows = @()
foreach ($ctx in $Contexts) {
    Write-Host "ctx=$ctx baseline ..."
    $b = Invoke-DecodeRun $ctx @()
    Write-Host "ctx=$ctx grc_k$Rank ..."
    $g = Invoke-DecodeRun $ctx @('--axex-compress','--axex-attn-only','--axex-weight-pca','--axiom-skip-geodesic','--axex-skip-o','--axex-compress-rank',"$Rank")
    $rows += [pscustomobject]@{
        context_tokens   = $ctx
        baseline_mean    = $b.mean
        baseline_std     = $b.std
        baseline_samples = $b.samples
        grc_mean         = $g.mean
        grc_std          = $g.std
        grc_samples      = $g.samples
        grc_over_baseline = if ($b.mean -gt 0) { [math]::Round($g.mean / $b.mean, 4) } else { 0 }
    }
}

# --- emit CSV ---------------------------------------------------------------
$null = New-Item -ItemType Directory -Force -Path (Split-Path $OutCsv -Parent)
$rows | Export-Csv -NoTypeInformation -Path $OutCsv
Write-Host "Wrote $OutCsv"

# --- emit \input-able LaTeX table -------------------------------------------
$body = $rows | ForEach-Object {
    "$($_.context_tokens) & $($_.baseline_mean) $\pm$ $($_.baseline_std) & $($_.grc_mean) $\pm$ $($_.grc_std) & $($_.grc_over_baseline) \\"
}
$tex = @"
% auto-generated by scripts/context_length_sweep.ps1 -- do not hand-edit.
\begin{tabular}{rrrr}
\toprule
Context (tok) & Baseline tok/s & GRC \$k\!=\!$Rank\$ tok/s & ratio \\
\midrule
$($body -join "`n")
\bottomrule
\end{tabular}
"@
$null = New-Item -ItemType Directory -Force -Path (Split-Path $OutTex -Parent)
Set-Content -Path $OutTex -Value $tex
Write-Host "Wrote $OutTex"
