"""Compare KL vs MSE distillation PPL."""
import sys; sys.path.insert(0, '.')
from scripts.measure_distill_ppl import compute_ppl
from transformers import AutoModelForCausalLM, AutoTokenizer
import torch, json
from pathlib import Path

tokenizer = AutoTokenizer.from_pretrained("Qwen/Qwen2.5-0.5B")
tokenizer.pad_token = tokenizer.eos_token
device = "cuda"

results = {}

# KL distillation
path = "benchmarks/distill_e2e/qwen05b_k448_s4_r8_kl"
print(f"Measuring: k=448 r=8 KL (T=4.0)")
model = AutoModelForCausalLM.from_pretrained(path, torch_dtype=torch.float32)
ppl = compute_ppl(model, tokenizer, max_samples=200, seq_len=256, device=device)
results["k448_kl_r8_s200"] = ppl
print(f"  PPL = {ppl:.4f}")
del model; torch.cuda.empty_cache()

# Reference values from previous runs
results.update({
    "baseline_fp32": 26.56,
    "k448_nodistill": 58.99,
    "k448_mse_r8_s200": 33.83,
    "k448_mse_r16_s500": 31.02,
    "k640_mse_r8_s200": 27.61,
    "bnb_nf4": 31.79,
})

print()
print("KL vs MSE DISTILLATION COMPARISON (k=448, r=8, 200 steps)")
print(f"{'Method':<40s} {'PPL':>8s}  {'delta':>10s}")
print("-" * 60)
order = ["baseline_fp32", "k640_mse_r8_s200", "k448_mse_r16_s500",
         "bnb_nf4", "k448_kl_r8_s200", "k448_mse_r8_s200", "k448_nodistill"]
for label in order:
    ppl_val = results[label]
    delta = (ppl_val - 26.56) / 26.56 * 100
    marker = " <-- KL" if "kl" in label else (" <-- MSE" if "mse" in label else "")
    print(f"{label:<40s} {ppl_val:8.4f}  {delta:+10.1f}%{marker}")

Path("benchmarks/distill_e2e/ppl_kl_vs_mse.json").write_text(json.dumps(results, indent=2))
print("\nSaved.")
