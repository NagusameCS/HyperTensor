#!/bin/bash
# EC2 master runner — downloads models and runs all benchmarks
set -e

# === SETUP ===
export HF_TOKEN=$HF_TOKEN  # set your Hugging Face token here
pip3 install -q transformers accelerate peft bitsandbytes datasets huggingface_hub 2>/dev/null

# === DOWNLOAD MODEL (1.5B — fast, fits easily) ===
python3 -c "
import os; os.environ['HF_TOKEN']=os.environ.get('HF_TOKEN','')
from transformers import AutoModelForCausalLM, AutoTokenizer
import torch
m = AutoModelForCausalLM.from_pretrained('Qwen/Qwen2.5-1.5B-Instruct', torch_dtype=torch.float16, device_map='auto', trust_remote_code=True)
t = AutoTokenizer.from_pretrained('Qwen/Qwen2.5-1.5B-Instruct', trust_remote_code=True)
print(f'MODEL LOADED OK — d={m.config.hidden_size}')
del m; torch.cuda.empty_cache()
"
echo "=== MODEL CACHED ==="

# === RUN BENCHMARKS ===
python3 ~/ec2_benchmark.py 2>&1 | tee ~/ec2_run.log

echo ""
echo "=== RESULTS ==="
python3 -c "import json; d=json.load(open('/home/ubuntu/benchmarks_ec2/results.json')); print(json.dumps(d, indent=2)[:5000])"

echo ""
echo "=== DONE ==="
