"""Validate HyperRetro on Gemma and Falcon (fused QKV architectures)."""
import sys; sys.path.insert(0, '.')
import torch
from transformers import AutoModelForCausalLM, AutoTokenizer
from hyperretro.hf.compress import _group_attn_by_layer, compress_state_dict, CompressConfig

def test_arch(model_id, name, rank_k=None):
    print(f"\n{'='*60}")
    print(f"Testing: {name} ({model_id})")
    try:
        model = AutoModelForCausalLM.from_pretrained(
            model_id, torch_dtype=torch.float32, trust_remote_code=True,
        )
    except Exception as e:
        print(f"  LOAD FAILED: {e}")
        return None

    sd = model.state_dict()
    layer_map = _group_attn_by_layer(sd)
    n_layers = len(layer_map)
    
    # Check what kind of QKV we found
    has_fused = any("qkv" in names for names in layer_map.values())
    has_separate = any(
        all(k in names for k in ("q", "k", "v")) for names in layer_map.values()
    )
    
    print(f"  Layers found: {n_layers}")
    print(f"  Fused QKV: {has_fused}, Separate Q/K/V: {has_separate}")
    
    if n_layers == 0:
        print("  WARNING: No attention layers found!")
        return None
    
    # Show sample
    for li in sorted(layer_map.keys())[:2]:
        names = layer_map[li]
        if "qkv" in names:
            shape = sd[names["qkv"]].shape
            print(f"  Layer {li}: fused QKV shape={list(shape)}")
        else:
            shapes = {k: list(sd[names[k]].shape) for k in names}
            print(f"  Layer {li}: separate {shapes}")
    
    # Try compression if rank specified
    if rank_k and n_layers > 0:
        k = min(rank_k, next(iter(sd[list(layer_map[0].values())[0]].shape)) if layer_map else 768)
        try:
            cfg = CompressConfig(rank_k=k, sink_T=0, dtype="float32")
            stats = compress_state_dict(sd, cfg)
            print(f"  Compression OK: {len(stats)} layers compressed at k={k}")
            # Quick load test
            model.load_state_dict(sd)
            tok = AutoTokenizer.from_pretrained(model_id, trust_remote_code=True)
            ids = tok("Hello world", return_tensors="pt").input_ids
            with torch.no_grad():
                out = model(ids)
                pred = torch.argmax(out.logits[0, -1, :]).item()
            print(f"  Post-compression prediction: {repr(tok.decode([pred]))}")
        except Exception as e:
            print(f"  Compression FAILED: {e}")
    
    del model; torch.cuda.empty_cache()
    return n_layers > 0


# Test Gemma
print("Testing small models for architecture validation...")
test_arch("google/gemma-2b", "Gemma 2B", rank_k=1024)

# Test SmolLM2 (GPT-NeoX style)
test_arch("HuggingFaceTB/SmolLM2-135M-Instruct", "SmolLM2-135M (GPT-NeoX)", rank_k=64)

# Test a tiny model that uses standard Llama arch
test_arch("Qwen/Qwen2.5-0.5B", "Qwen2.5-0.5B (Llama-style, control)", rank_k=448)

print("\nDone. Architecture validation complete.")
