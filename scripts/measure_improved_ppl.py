"""Measure PPL on improved k=448 distillation."""
import sys; sys.path.insert(0, '.')
from scripts.measure_distill_ppl import compute_ppl
from transformers import AutoModelForCausalLM, AutoTokenizer
import torch, json
from pathlib import Path

tokenizer = AutoTokenizer.from_pretrained("Qwen/Qwen2.5-0.5B")
tokenizer.pad_token = tokenizer.eos_token
device = "cuda"

# Test improved model
path = "benchmarks/distill_e2e/qwen05b_k448_s4_r16_s500"
print(f"Measuring: k=448 r=16 steps=500")
model = AutoModelForCausalLM.from_pretrained(path, torch_dtype=torch.float32)
ppl = compute_ppl(model, tokenizer, max_samples=200, seq_len=256, device=device)
print(f"  PPL = {ppl:.4f}")
del model; torch.cuda.empty_cache()

# Compare with previous results
results = {
    "baseline_fp32": 26.56,
    "k448_nodistill": 58.99,
    "k448_r8_s200": 33.83,
    "k448_r16_s500": ppl,
    "k640_r8_s200": 27.61,
    "bnb_nf4": 31.79,
}
print()
print("UPDATED COMPARISON:")
print(f"{'Method':<35s} {'PPL':>8s}  {'delta':>10s}")
print("-" * 55)
for label in ["baseline_fp32", "k640_r8_s200", "bnb_nf4", "k448_r16_s500", "k448_r8_s200", "k448_nodistill"]:
    ppl_val = results[label]
    delta = (ppl_val - 26.56) / 26.56 * 100
    print(f"{label:<35s} {ppl_val:8.4f}  {delta:+10.1f}%")

Path("benchmarks/distill_e2e/ppl_results_v2.json").write_text(json.dumps(results, indent=2))
print("\nSaved.")
