"""Download WikiText-2 and prepare diverse calibration corpus."""
import datasets, os
from pathlib import Path

out_path = Path("data/wikitext2_train.txt")
out_path.parent.mkdir(parents=True, exist_ok=True)

print("Downloading WikiText-2 raw...")
ds = datasets.load_dataset("wikitext", "wikitext-2-raw-v1", split="train")
texts = [t for t in ds["text"] if t.strip()]
word_count = sum(len(t.split()) for t in texts)
print(f"WikiText-2 train: {len(texts)} non-empty articles, ~{word_count:,} words")

corpus = "\n\n".join(texts)
out_path.write_text(corpus, encoding="utf-8")
size_mb = os.path.getsize(out_path) / 1e6
print(f"Wrote {out_path}: {size_mb:.1f} MB")
print(f"First 300 chars: {corpus[:300]}")
