# scripts/benchmark_rank_pareto.ps1
# -----------------------------------------------------------------------------
# Granular rank-Pareto sweep at k in {512, 768, 1024, 1280, 1536}.
# Reuses the same locked decode-protocol path as
# scripts/benchmark_whitepaper_finalize.ps1 but covers a wider, finer k-grid
# so Paper A can show a proper quality / throughput Pareto frontier instead
# of only the two points (k=1024, k=1536).
#
# Output:
#   docs/data/rank_pareto.csv
#   docs/data/rank_pareto.tex   (\input-able table for Paper A)
#
# Honest-or-fail: errors out rather than fabricating any number.
# -----------------------------------------------------------------------------

param(
    [string]$Model    = "C:\Users\legom\models\models--bartowski--Meta-Llama-3.1-8B-Instruct-GGUF\snapshots\bf5b95e96dac0462e2a09145ec66cae9a3f12067\Meta-Llama-3.1-8B-Instruct-Q4_K_M.gguf",
    [string]$Exe      = ".\build_host\geodessical.exe",
    [int[]] $Ranks    = @(512, 768, 1024, 1280, 1536),
    [int]   $DecodeTokens = 64,
    [int]   $Repeats  = 7,
    [string]$Prompt   = "The quick brown fox jumps over the lazy dog.",
    [int]   $CooldownSec = 30,
    [string]$OutCsv   = "docs/data/rank_pareto.csv",
    [string]$OutTex   = "docs/data/rank_pareto.tex"
)

$ErrorActionPreference = "Stop"
if (-not (Test-Path $Exe))   { throw "Executable not found: $Exe" }
if (-not (Test-Path $Model)) { throw "Model not found: $Model" }

function Invoke-DecodeRun {
    param([string[]]$ExtraArgs)
    $tps = @()
    for ($r = 0; $r -lt $Repeats; $r++) {
        $args = @("-m", $Model, "-p", $Prompt, "-n", $DecodeTokens, "--report-tps") + $ExtraArgs
        $out  = & $Exe @args 2>&1
        if ($LASTEXITCODE -ne 0) { throw "geodessical failed: $out" }
        $line = $out | Where-Object { $_ -match "decode_tps=" } | Select-Object -First 1
        if (-not $line) { throw "no decode_tps in output: $out" }
        $tps += [double]([regex]::Match($line, "decode_tps=([0-9.]+)").Groups[1].Value)
        Start-Sleep -Seconds $CooldownSec
    }
    $mean = ($tps | Measure-Object -Average).Average
    $std  = if ($tps.Count -gt 1) {
        [math]::Sqrt((($tps | ForEach-Object { ($_ - $mean) * ($_ - $mean) }) | Measure-Object -Sum).Sum / ($tps.Count - 1))
    } else { 0.0 }
    return [pscustomobject]@{ mean = [math]::Round($mean, 3); std = [math]::Round($std, 3) }
}

# --- baseline ---------------------------------------------------------------
Write-Host "baseline ..."
$base = Invoke-DecodeRun @()

# --- per-rank ---------------------------------------------------------------
$rows = @()
foreach ($k in $Ranks) {
    Write-Host "k=$k ..."
    $r = Invoke-DecodeRun @("--grc-rank", $k)
    $rows += [pscustomobject]@{
        rank        = $k
        decode_mean = $r.mean
        decode_std  = $r.std
        ratio_to_baseline = if ($base.mean -gt 0) { [math]::Round($r.mean / $base.mean, 4) } else { 0 }
    }
}

# prepend baseline as rank=0 row for easy plotting
$all = @([pscustomobject]@{ rank = 0; decode_mean = $base.mean; decode_std = $base.std; ratio_to_baseline = 1.0 }) + $rows

# --- emit CSV ---------------------------------------------------------------
$null = New-Item -ItemType Directory -Force -Path (Split-Path $OutCsv -Parent)
$all | Export-Csv -NoTypeInformation -Path $OutCsv
Write-Host "Wrote $OutCsv"

# --- emit \input-able LaTeX table -------------------------------------------
$body = $rows | ForEach-Object {
    "$($_.rank) & $($_.decode_mean) $\pm$ $($_.decode_std) & $($_.ratio_to_baseline) \\"
}
$tex = @"
% auto-generated by scripts/benchmark_rank_pareto.ps1 -- do not hand-edit.
\begin{tabular}{rrr}
\toprule
\$k\$ & GRC tok/s & ratio vs.\ baseline \\
\midrule
baseline & $($base.mean) $\pm$ $($base.std) & 1.000 \\
$($body -join "`n")
\bottomrule
\end{tabular}
"@
$null = New-Item -ItemType Directory -Force -Path (Split-Path $OutTex -Parent)
Set-Content -Path $OutTex -Value $tex
Write-Host "Wrote $OutTex"
