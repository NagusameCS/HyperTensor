import os
os.environ["HF_TOKEN"] = os.environ.get("HF_TOKEN", "")
from transformers import AutoModelForCausalLM, AutoTokenizer
import torch

print("Loading Qwen2.5-1.5B-Instruct (fp16)...")
m = AutoModelForCausalLM.from_pretrained(
    "Qwen/Qwen2.5-1.5B-Instruct",
    torch_dtype=torch.float16,
    device_map="auto",
    trust_remote_code=True,
)
t = AutoTokenizer.from_pretrained("Qwen/Qwen2.5-1.5B-Instruct", trust_remote_code=True)
print(f"LOADED — d_model={m.config.hidden_size}, params={sum(p.numel() for p in m.parameters())/1e9:.1f}B")
del m, t
torch.cuda.empty_cache()
print("DONE")
