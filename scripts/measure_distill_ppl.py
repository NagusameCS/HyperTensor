"""Measure PPL of HyperRetro-distilled models on WikiText-2."""
import sys
import json
import torch
import numpy as np
from pathlib import Path
from transformers import AutoModelForCausalLM, AutoTokenizer
from datasets import load_dataset

def compute_ppl(model, tokenizer, dataset_split="test", max_samples=500, seq_len=512, device="cuda"):
    """Compute perplexity on WikiText-2."""
    model.eval()
    model = model.to(device)
    
    ds = load_dataset("wikitext", "wikitext-2-raw-v1", split=dataset_split)
    text = "\n\n".join(ds["text"])
    enc = tokenizer.encode(text)
    
    stride = seq_len // 2
    nlls = []
    prev_end_loc = 0
    
    for i, begin_loc in enumerate(range(0, len(enc) - seq_len, stride)):
        if i >= max_samples:
            break
        end_loc = begin_loc + seq_len
        target_end_loc = end_loc
        if prev_end_loc > begin_loc:
            # Don't double-count overlapping tokens
            begin_loc = prev_end_loc
        if begin_loc >= end_loc:
            continue
            
        input_ids = torch.tensor(enc[begin_loc:end_loc], dtype=torch.long).unsqueeze(0).to(device)
        target_ids = input_ids.clone()
        
        with torch.no_grad():
            outputs = model(input_ids, labels=target_ids)
            neg_log_likelihood = outputs.loss * seq_len  # total NLL for the sequence
        
        nlls.append(neg_log_likelihood.item())
        prev_end_loc = target_end_loc
        if i > 0 and i % 50 == 0:
            ppl_sofar = torch.exp(torch.tensor(sum(nlls) / (len(nlls) * seq_len))).item()
            print(f"  [{i}/{max_samples}] partial PPL={ppl_sofar:.4f}", flush=True)
    
    if not nlls:
        return float("inf")
    
    total_nll = sum(nlls)
    total_tokens = len(nlls) * seq_len
    avg_nll = total_nll / total_tokens
    ppl = float(np.exp(avg_nll))
    return ppl


def main():
    models = {
        "baseline (fp32, uncompressed)": "Qwen/Qwen2.5-0.5B",
        "GRC k=448 sink=4 (no distill)": "benchmarks/distill_e2e/qwen05b_k448_s4_nodistill",
        "GRC k=448 sink=4 + distill r=8": "benchmarks/distill_e2e/qwen05b_k448_s4_r8",
        "GRC k=640 sink=4 (no distill)": "benchmarks/distill_e2e/qwen05b_k640_s4_nodistill",
        "GRC k=640 sink=4 + distill r=8": "benchmarks/distill_e2e/qwen05b_k640_s4_r8",
    }
    
    tokenizer = AutoTokenizer.from_pretrained("Qwen/Qwen2.5-0.5B")
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token
    
    device = "cuda" if torch.cuda.is_available() else "cpu"
    results = {}
    
    for label, path in models.items():
        print(f"\n{'='*60}")
        print(f"Measuring: {label}")
        print(f"Path: {path}")
        
        model = AutoModelForCausalLM.from_pretrained(path, torch_dtype=torch.float32)
        ppl = compute_ppl(model, tokenizer, max_samples=200, seq_len=256, device=device)
        results[label] = ppl
        print(f"  PPL = {ppl:.4f}")
        
        del model
        torch.cuda.empty_cache()
    
    print(f"\n{'='*60}")
    print("SUMMARY")
    print(f"{'='*60}")
    for label, ppl in sorted(results.items(), key=lambda x: x[1]):
        print(f"  {label:50s}  PPL={ppl:.4f}")
    
    # Save
    out = Path("benchmarks/distill_e2e/ppl_results.json")
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(results, indent=2))
    print(f"\nSaved to {out}")


if __name__ == "__main__":
    main()
