import torch, numpy as np, warnings
warnings.filterwarnings('ignore')
from transformers import AutoModelForCausalLM, AutoTokenizer

MODEL='Qwen/Qwen2.5-0.5B-Instruct'
model = AutoModelForCausalLM.from_pretrained(MODEL, torch_dtype=torch.float16, device_map='auto', trust_remote_code=True)
tok = AutoTokenizer.from_pretrained(MODEL, trust_remote_code=True)

zone_prompts = {
    'Science': ['Photosynthesis converts CO2 and water into glucose.','The mitochondria is the powerhouse of the cell.','Newton second law F equals ma.','Speed of light is 3e8 m/s.'],
    'Math': ['Pythagorean theorem a squared plus b squared equals c squared.','A prime is divisible only by 1 and itself.','Derivative of x cubed is 3x squared.','Euler identity e to i pi plus 1 equals 0.'],
    'Literature': ['Shakespeare wrote Hamlet and Macbeth.','Gatsby explores wealth and American Dream.','Mockingbird addresses racial injustice.','1984 depicts totalitarian state.'],
    'Code': ['A for loop iterates over array elements.','Recursion is function calling itself.','Binary search is O log n.','Hash table has constant time lookup.'],
    'History': ['WWII ended in 1945 with German surrender.','French Revolution began 1789 Bastille.','Roman Empire fell 476 CE.','Industrial Revolution 18th century.'],
    'Philosophy': ['Descartes I think therefore I am.','Kant categorical imperative universalize.','Existentialism emphasizes freedom.','Plato cave allegory appearance reality.'],
    'Law': ['Constitution establishes three branches.','Habeas corpus requires court appearance.','Stare decisis follows precedent.','Due process guarantees fair treatment.'],
    'Art': ['Renaissance revived classical learning.','Impressionism captured light with brushstrokes.','Picasso Guernica depicts war horrors.','Bauhaus integrated art and technology.'],
}

all_hidden = []; zone_labels = []
for zone, prompts in zone_prompts.items():
    for p in prompts:
        enc = tok(p, return_tensors='pt', truncation=True, max_length=64).to(model.device)
        with torch.no_grad(): out = model(**enc, output_hidden_states=True)
        all_hidden.append(out.hidden_states[-1][0,-1,:].float().cpu()); zone_labels.append(zone)

hs = torch.stack(all_hidden); hs_c = hs - hs.mean(0)
U, S, V = torch.linalg.svd(hs_c.T, full_matrices=False)
basis = U[:,:32].float()

zones = list(zone_prompts.keys()); n = len(zones)
projs = {}
for z in zones:
    idxs = [i for i,l in enumerate(zone_labels) if l==z]
    c = torch.stack([all_hidden[i] for i in idxs]).mean(0)
    projs[z] = (c.float() @ basis).numpy()

print()
print('Pairwise Centroid Cosine Similarity:')
header = '            ' + ' '.join(f'{z[:7]:>7s}' for z in zones)
print(header)
for z1 in zones:
    vals = []
    for z2 in zones:
        v1=projs[z1]; v2=projs[z2]
        s=np.dot(v1,v2)/(np.linalg.norm(v1)*np.linalg.norm(v2)+1e-8)
        vals.append(f'{s:.3f}')
    print(f'{z1:>12s} ' + ' '.join(f'{v:>7s}' for v in vals))

print()
print('Entangled centroids (cos_sim > 0.85):')
for i in range(n):
    for j in range(i+1,n):
        v1=projs[zones[i]]; v2=projs[zones[j]]
        s=np.dot(v1,v2)/(np.linalg.norm(v1)*np.linalg.norm(v2)+1e-8)
        if s>.85: print(f'  {zones[i]:>12s} -- {zones[j]:<12s}  sim={s:.4f}')
print('Well-separated (cos_sim < 0.70):')
for i in range(n):
    for j in range(i+1,n):
        v1=projs[zones[i]]; v2=projs[zones[j]]
        s=np.dot(v1,v2)/(np.linalg.norm(v1)*np.linalg.norm(v2)+1e-8)
        if s<.70: print(f'  {zones[i]:>12s} -- {zones[j]:<12s}  sim={s:.4f}')

del model; torch.cuda.empty_cache()
