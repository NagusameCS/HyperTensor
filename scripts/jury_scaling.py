"""Quick jury gate scaling benchmark."""
import torch, time, sys
from pathlib import Path
sys.path.insert(0, str(Path(__file__).parent))
from ott_engine import JuryDraftGate

torch.manual_seed(42)
K = 64

def run_benchmark():
    n_jurors_list = [4, 8, 16, 32, 64, 128, 256, 512]
    results = []

    for n_j in n_jurors_list:
        jury = JuryDraftGate(threshold=0.85, n_jurors=7, coverage_radius=0.5)
        trajs = [{"proj": torch.randn(K)*0.3, "label": f"t{i}"} for i in range(n_j)]
        jury.calibrate(trajs)
        q = torch.randn(K)*0.5
        t0 = time.time()
        for _ in range(1000):
            jury.jury_confidence(q)
            jury.should_accept(q)
        ms_per = (time.time() - t0)*1000/1000
        results.append((n_j, ms_per))
        print(f"  n_jurors={n_j:4d}: {ms_per:.4f}ms/query")
    return results

if __name__ == "__main__":
    results = run_benchmark()
    print()
    print("Jury gate scaling:")
    for n_j, ms in results:
        print(f"  {n_j:4d} jurors: {ms:.4f}ms")
    print()
    print(f"Transformer verify (est): ~30ms")
    print(f"Jury speedup at 128 jurors: {30/results[5][1]:.0f}x")
    print(f"Jury speedup at 512 jurors: {30/results[-1][1]:.0f}x")
