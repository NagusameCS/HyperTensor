"""Re-cast a factored checkpoint's safetensors to bfloat16 in-place.

Round 12 helper: the first distilled factored save used cfg.dtype=float32
for storage, producing 4895 MB. This re-casts to bf16 → ~2.4 GB.
"""

from __future__ import annotations

import sys
from pathlib import Path

import torch
from safetensors.torch import load_file, save_file


def main() -> None:
    if len(sys.argv) != 2:
        print("usage: python recast_factored_to_bf16.py <ckpt_dir>")
        sys.exit(1)
    d = Path(sys.argv[1])
    src = d / "model.safetensors"
    if not src.exists():
        print(f"missing {src}")
        sys.exit(1)
    print(f"[recast] loading {src} ({src.stat().st_size/1e6:.1f} MB)")
    sd = load_file(str(src))
    new_sd = {k: v.to(torch.bfloat16).contiguous().clone() for k, v in sd.items()}
    del sd
    import gc; gc.collect()
    tmp = src.with_suffix(".bf16.tmp")
    save_file(new_sd, str(tmp))
    del new_sd; gc.collect()
    src.unlink()
    tmp.rename(src)
    print(f"[recast] wrote {src} ({src.stat().st_size/1e6:.1f} MB)")


if __name__ == "__main__":
    main()
