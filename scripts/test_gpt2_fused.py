"""Validate fused QKV compression on real GPT-2 model."""
import sys; sys.path.insert(0, '.')
import torch, numpy as np
from transformers import AutoModelForCausalLM, AutoTokenizer
from hyperretro.hf.compress import _group_attn_by_layer, compress_state_dict, CompressConfig

model_id = "openai-community/gpt2"
print("Loading GPT-2...")
model = AutoModelForCausalLM.from_pretrained(model_id, torch_dtype=torch.float32)
sd = model.state_dict()

# Test fused QKV detection
layer_map = _group_attn_by_layer(sd)
print(f"Found {len(layer_map)} layers")
for li in sorted(layer_map.keys())[:3]:
    names = layer_map[li]
    has_qkv = "qkv" in names
    has_sep = all(k in names for k in ("q", "k", "v"))
    print(f"  layer {li}: fused_qkv={has_qkv}, separate_qkv={has_sep}, keys={list(names.keys())}")
    if "qkv" in names:
        shape = sd[names["qkv"]].shape
        print(f"    qkv shape: {shape}")

# Apply compression
print("\nApplying GRC compression (k=384)...")
cfg = CompressConfig(rank_k=384, sink_T=0, dtype="float32")
stats = compress_state_dict(sd, cfg)
print(f"Compressed {len(stats)} layers")
for k, v in list(stats.items())[:2]:
    fused = v.get("fused", False)
    print(f"  {k}: fused={fused}, frob_relerr_q={v['frob_relerr_q']:.4f}")

# Verify model loads with compressed weights
print("\nVerifying compressed model works...")
model.load_state_dict(sd)
model.eval()
tok = AutoTokenizer.from_pretrained(model_id)
tok.pad_token = tok.eos_token
ids = tok("The capital of France is", return_tensors="pt").input_ids
with torch.no_grad():
    out = model(ids)
    pred = torch.argmax(out.logits[0, -1, :]).item()
    word = tok.decode([pred])
print(f"Next token prediction: {repr(word)}")
print("\nGPT-2 fused QKV compression: PASSED")
