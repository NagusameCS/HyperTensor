# Quick diagnostic: Check W_proj matrix properties at k=2048
param(
    [string]$OutDir = ".\benchmarks\wp_diagnostic"
)

New-Item -ItemType Directory -Force -Path $OutDir > $null

Write-Host "W_proj Quality Diagnostic"
Write-Host "========================="
Write-Host ""
Write-Host "This diagnostic loads and inspects manifold W_proj matrices"
Write-Host "to identify precision/quantization issues at k=2048"
Write-Host ""

# Run a k=2048 test with verbose logging
Write-Host "Running k=2048 with max verbosity..."
$env:AXEX_VERBOSE_MANIFOLD = "1"
$env:AXEX_MANIFOLD_DEBUG = "1"

$out_file = "$OutDir/verbose_k2048.txt"
$cmd = '.\build_host\geodessical.exe "Meta-Llama-3.1-8B-Instruct-Q4_K_M.gguf" --axex-compress --axex-attn-only --axex-skip-o --axex-weight-pca --axex-compress-rank 2048 --tokens 32 -p "test" --log-level 4'

cmd /c "$cmd > `"$out_file`" 2>&1"

Write-Host "Check output in: $out_file"
Write-Host ""

# Look for diagnostic info
$content = Get-Content $out_file -Raw
if ($content -match "W_proj|manifold|quantization|precision") {
    Write-Host "=== Diagnostics Found ==="
    Get-Content $out_file | Select-String -Pattern "(W_proj|manifold|quantization|precision|error|rank)" -CaseSensitive
} else {
    Write-Host "No manifold diagnostics in output (verbose flags may not be implemented)"
    Write-Host "First 50 lines of output:"
    Get-Content $out_file | Select-Object -First 50
}

Write-Host ""
Write-Host "Output saved to: $out_file"
