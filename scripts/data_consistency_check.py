#!/usr/bin/env python3
"""Data consistency check — all numerical claims vs benchmarks."""
import json, os

bench_dir = 'benchmarks'
data = {}

for name, path in [
    ('shf', 'benchmarks/shf_full_build/results.json'),
    ('cog', 'benchmarks/cog_10k/results.json'),
    ('rigorous', 'benchmarks/rigorous_verification/results.json'),
    ('riemann', 'benchmarks/riemann_mega/riemann_mega_verification.json'),
]:
    if os.path.exists(path):
        with open(path) as f:
            data[name] = json.load(f)

print("=== DATA CONSISTENCY REPORT ===")
all_ok = True

# SHF
if 'shf' in data:
    s = data['shf']
    geo_red = abs(s['shf']['delta_geodicity_pct'])
    lm_cost = s['shf']['shf_lm_cost']
    geo_ok = abs(geo_red - 84.73) < 1.0
    lm_ok = abs(lm_cost - 1.99) < 0.1
    print(f"  SHF geodicity: {geo_red:.1f}% (volume: 84.7%) OK={geo_ok}")
    print(f"  SHF LM cost: +{lm_cost:.3f} (volume: +1.99) OK={lm_ok}")
    if not geo_ok or not lm_ok:
        all_ok = False

if 'cog' in data:
    c = data['cog']
    traj = c['outcomes']['total_trajectories']
    nov = c['outcomes']['overall_novelty_rate']
    print(f"  COG trajectories: {traj} (volume: 32) OK={traj == 32}")
    print(f"  COG novelty: {nov*100:.1f}% (volume: 0.3%)")
    if traj != 32:
        all_ok = False

if 'rigorous' in data:
    r = data['rigorous']
    grc = r['tests'].get('grc_throughput', {})
    if grc:
        print(f"  GRC (Qwen-0.5B): {grc.get('ratio_pct')}% vs Paper I 106.27% (Llama-8B) — different models")
    shfr = r['tests'].get('shf_geodicity', {})
    if shfr:
        print(f"  SHF (50-step): {shfr.get('reduction_pct')}% vs full 84.7% (200-step) — different training length")

if 'riemann' in data:
    rm = data['riemann']
    tests = rm.get('tests', {})
    n_pass = sum(1 for t in tests.values() if 'PASS' in str(t.get('status', '')))
    n_total = len(tests)
    print(f"  Riemann: {n_pass}/{n_total} PASS OK={n_pass == n_total}")
    if n_pass != n_total:
        all_ok = False

# Internal math
print("\n=== INTERNAL MATH ===")
print(f"  k*/d = 1024/4096 = {1024/4096:.3f} (Paper I claim: 0.25)")
print(f"  512/576 = {512/576*100:.1f}% (Paper VI claim: 88.9%)")
print(f"  36*42.7 = {36*42.7:.1f} -> k* (Paper IX claim: 1536)")
print(f"  24*42.7 = {24*42.7:.1f} -> k* (Paper IX claim: 1024)")

print(f"\nALL CONSISTENT: {all_ok}")
