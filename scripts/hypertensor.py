# Imported lazily to avoid circular deps with isagi_chat
def _get_gtc():
    from isagi_chat import GTCCache
    return GTCCache

import sys as _sys
class _LazyModule:
    """Lazy attribute accessor that imports only when accessed."""
    def __init__(self, name, getter):
        self._name = name
        self._getter = getter
        self._obj = None
    def _load(self):
        if self._obj is None:
            self._obj = self._getter()
        return self._obj
    def __call__(self, *args, **kwargs):
        return self._load()(*args, **kwargs)
    def __getattr__(self, attr):
        return getattr(self._load(), attr)

_sys.modules[__name__].GTCCache = _LazyModule("GTCCache", _get_gtc)
