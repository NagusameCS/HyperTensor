#!/usr/bin/env python3
"""Minimal GRC benchmark stub for Nsight Compute profiling.
Loads Qwen2.5-0.5B, runs a few tokens of generation, prints throughput.
Designed to be profiled with: ncu --set full -o grc_ncu python scripts/grc_benchmark_stub.py
"""
import torch, time, os
os.environ['TOKENIZERS_PARALLELISM'] = 'false'

from transformers import AutoModelForCausalLM, AutoTokenizer
MODEL = 'Qwen/Qwen2.5-0.5B-Instruct'
DEVICE = 'cuda'

model = AutoModelForCausalLM.from_pretrained(MODEL, torch_dtype=torch.float16, device_map='auto', trust_remote_code=True)
tok = AutoTokenizer.from_pretrained(MODEL, trust_remote_code=True)
if tok.pad_token is None: tok.pad_token = tok.eos_token

prompt = "The capital of France is"
enc = tok(prompt, return_tensors='pt').to(DEVICE)

# Warmup
with torch.no_grad():
    model.generate(**enc, max_new_tokens=5, do_sample=False)

# Timed run
torch.cuda.synchronize()
t0 = time.time()
with torch.no_grad():
    out = model.generate(**enc, max_new_tokens=20, do_sample=False)
torch.cuda.synchronize()
elapsed = time.time() - t0
tokens = out.shape[1] - enc['input_ids'].shape[1]
print(f"Tokens: {tokens}, Time: {elapsed:.3f}s, Throughput: {tokens/elapsed:.1f} tok/s")
