import sys; sys.path.insert(0, '.')
import json, torch
from hyperretro.bench.spec_decode_sim import simulate_speculative_decode
prompt = "The history of artificial intelligence began in antiquity with myths and stories of artificial beings endowed with intelligence by master craftsmen. In the 1950s a generation of scientists"
model = "Qwen/Qwen2.5-0.5B"
configs = [
    ("MSE", "benchmarks/distill_e2e/qwen05b_k448_s4_r8"),
    ("KL",  "benchmarks/distill_e2e/qwen05b_k448_s4_r8_kl"),
    ("MARGIN", "benchmarks/distill_e2e/qwen05b_k448_s4_r8_margin"),
    ("BR",  "benchmarks/distill_e2e/qwen05b_k448_s4_r8_br"),
]
results = {}
for label, p in configs:
    print(f"--- {label} ---", flush=True)
    r = simulate_speculative_decode(model, prompt, k=448, gamma=8, device="cuda", dtype="float16", max_cycles=15, drafter_path=p)
    results[label] = {"accept": r["mean_acceptance_length"], "rate": r["acceptance_rate"], "wall": r["wallclock_speedup"]}
    torch.cuda.empty_cache()
print(f"{'Drafter':<10s}{'Accept':>8s}{'Rate':>8s}{'Wall':>10s}")
for k,v in results.items():
    print(f"{k:<10s}{v['accept']:8.2f}{v['rate']:8.1%}{v['wall']:10.3f}x")
with open('benchmarks/loss_compare_4way.json','w') as f: json.dump(results,f,indent=2)
