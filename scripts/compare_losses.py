"""Compare MSE, KL, Margin drafters at k=448 gamma=8."""
import sys; sys.path.insert(0, '.')
import json, torch
from hyperretro.bench.spec_decode_sim import simulate_speculative_decode

prompt = "The history of artificial intelligence began in antiquity with myths and stories of artificial beings endowed with intelligence by master craftsmen. In the 1950s a generation of scientists"
model = "Qwen/Qwen2.5-0.5B"

configs = [
    ("k=448 MSE r=8", "benchmarks/distill_e2e/qwen05b_k448_s4_r8"),
    ("k=448 KL  r=8", "benchmarks/distill_e2e/qwen05b_k448_s4_r8_kl"),
    ("k=448 MARGIN r=8", "benchmarks/distill_e2e/qwen05b_k448_s4_r8_margin"),
    ("k=448 no distill", None),
]

results = {}
for label, drafter_path in configs:
    print(f"\n--- {label} (gamma=8) ---", flush=True)
    r = simulate_speculative_decode(
        model, prompt, k=448, gamma=8,
        device="cuda", dtype="float16", max_cycles=15,
        drafter_path=drafter_path,
    )
    results[label] = {
        "mean_accept": r["mean_acceptance_length"],
        "accept_rate": r["acceptance_rate"],
        "wallclock": r["wallclock_speedup"],
    }
    print(f"  accept={r['mean_acceptance_length']:.1f}/8 ({r['acceptance_rate']:.0%}) "
          f"wallclock={r['wallclock_speedup']:.3f}x")
    torch.cuda.empty_cache()

print(f"\n{'Drafter':<25s} {'Accept':>8s} {'Rate':>8s} {'Wall-Clock':>10s}")
print("-" * 55)
for label in ["k=448 MSE r=8", "k=448 KL  r=8", "k=448 MARGIN r=8", "k=448 no distill"]:
    r = results[label]
    print(f"{label:<25s} {r['mean_accept']:8.2f}  {r['accept_rate']:8.1%}  {r['wallclock']:10.3f}x")

with open("benchmarks/loss_compare_k448.json", "w") as f:
    json.dump(results, f, indent=2)
print("\nSaved.")
