# scripts/benchmark_ncu_l2_profile.ps1
# -----------------------------------------------------------------------------
# NCU L2 cache profile: baseline vs. k=1024 GRC, 10-token decode each.
# Captures l2_tex_hit_rate and dram__bytes_read.sum to support the
# Cache-Fit Hypothesis in Paper A (§ Cache-Fit Hypothesis).
#
# Prereqs:
#   - NVIDIA Nsight Compute CLI (`ncu`) on PATH
#   - build_host\geodessical.exe present (run .\build_host.ps1 first)
#   - GGUF model file with --grc-rank flag wired in
#
# Output:
#   docs/data/ncu_l2_profile.csv
#   docs/data/ncu_l2_profile.tex   (\input-able booktabs table for Paper A)
#
# This script does NOT fabricate measurements. If `ncu` is missing or the
# binary fails, it errors out and writes nothing.
# -----------------------------------------------------------------------------

param(
    [string]$Model    = "C:\Users\legom\models\models--bartowski--Meta-Llama-3.1-8B-Instruct-GGUF\snapshots\bf5b95e96dac0462e2a09145ec66cae9a3f12067\Meta-Llama-3.1-8B-Instruct-Q4_K_M.gguf",
    [string]$Exe      = ".\build_host\geodessical.exe",
    [int]   $Tokens   = 10,
    [string]$Prompt   = "The quick brown fox jumps over the lazy dog.",
    [string]$OutCsv   = "docs/data/ncu_l2_profile.csv",
    [string]$OutTex   = "docs/data/ncu_l2_profile.tex",
    [int]   $CooldownSec = 30
)

$ErrorActionPreference = "Stop"

# --- prereq checks ----------------------------------------------------------
$ncu = (Get-Command ncu -ErrorAction SilentlyContinue)
if (-not $ncu) {
    throw "Nsight Compute CLI 'ncu' not found on PATH. Install Nsight Compute and re-run."
}
if (-not (Test-Path $Exe)) {
    throw "Executable not found: $Exe. Run .\build_host.ps1 first."
}
if (-not (Test-Path $Model)) {
    throw "Model not found: $Model"
}

$metrics = "lts__t_sector_hit_rate.pct,dram__bytes_read.sum,dram__bytes_write.sum,gpc__cycles_elapsed.max"

# --- run helper --------------------------------------------------------------
function Invoke-NcuRun {
    param([string]$Label, [string[]]$ExtraArgs)

    $tmp = New-TemporaryFile
    $args = @(
        "--metrics", $metrics,
        "--target-processes", "all",
        "--csv",
        "--log-file", $tmp.FullName,
        "--",
        $Exe, $Model, "-p", $Prompt, "-n", $Tokens, "--temp", "0"
    ) + $ExtraArgs

    Write-Host "[$Label] ncu $($args -join ' ')"
    & ncu @args
    if ($LASTEXITCODE -ne 0) {
        throw "ncu run failed for $Label (exit $LASTEXITCODE)"
    }

    # Parse the per-kernel CSV; aggregate L2 hit rate (mean) and DRAM bytes (sum).
    $rows = Import-Csv $tmp.FullName
    $hit  = ($rows | Where-Object { $_.'Metric Name' -eq 'lts__t_sector_hit_rate.pct' }   | Measure-Object -Property 'Metric Value' -Average).Average
    $dram = ($rows | Where-Object { $_.'Metric Name' -eq 'dram__bytes_read.sum' }         | Measure-Object -Property 'Metric Value' -Sum).Sum
    Remove-Item $tmp.FullName -Force

    return [pscustomobject]@{
        condition         = $Label
        l2_tex_hit_rate   = [math]::Round([double]$hit, 4)
        dram_bytes_read   = [int64]$dram
    }
}

# --- run both conditions ----------------------------------------------------
$rows = @()
$rows += Invoke-NcuRun "baseline" @()
Start-Sleep -Seconds $CooldownSec
$rows += Invoke-NcuRun "grc_k1024" @('--axex-compress','--axiom-skip-geodesic','--axex-skip-o','--axex-compress-rank','1024')

# --- emit CSV ---------------------------------------------------------------
$null = New-Item -ItemType Directory -Force -Path (Split-Path $OutCsv -Parent)
$rows | Export-Csv -NoTypeInformation -Path $OutCsv
Write-Host "Wrote $OutCsv"

# --- emit \input-able LaTeX table -------------------------------------------
$base = $rows | Where-Object { $_.condition -eq "baseline" }
$grc  = $rows | Where-Object { $_.condition -eq "grc_k1024" }
$dramRatio = if ($base.dram_bytes_read -gt 0) { [math]::Round($grc.dram_bytes_read / $base.dram_bytes_read, 3) } else { 0 }

$tex = @"
% auto-generated by scripts/benchmark_ncu_l2_profile.ps1 -- do not hand-edit.
\begin{tabular}{lrrr}
\toprule
Condition & L2 tex hit rate & DRAM bytes read & DRAM ratio vs.\ baseline \\
\midrule
Baseline (uncompressed) & $($base.l2_tex_hit_rate) & $($base.dram_bytes_read) & 1.000 \\
GRC, \$k\!=\!1024\$        & $($grc.l2_tex_hit_rate)  & $($grc.dram_bytes_read)  & $dramRatio \\
\bottomrule
\end{tabular}
"@
$null = New-Item -ItemType Directory -Force -Path (Split-Path $OutTex -Parent)
Set-Content -Path $OutTex -Value $tex
Write-Host "Wrote $OutTex"
