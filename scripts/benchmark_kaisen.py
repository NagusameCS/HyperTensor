"""Kaisen benchmark v3: Euclidean jury, proper J-decay, frontier expansion."""
import torch, json, time, math, sys
from pathlib import Path
sys.path.insert(0, str(Path(__file__).parent))
from instinct_horizon import InstinctHorizon
torch.manual_seed(42)

OUT=Path("benchmarks/kaisen"); OUT.mkdir(parents=True,exist_ok=True)
K=8; n=256
centers=[torch.tensor([c*2.0]+[0.0]*(K-1)) for c in range(4)]
trajs=[{"proj":centers[i%4]+torch.randn(K)*0.5,"label":f"topic_{i%4}"} for i in range(n)]
h=InstinctHorizon(creativity=0.3); h.calibrate(trajs); d_h=h.instinct_horizon_distance
print("="*60)
print(f"  KAISEN v3 | {n} traj | k={K} | R={h.R:.3f} | d_h={d_h:.3f} | J_thr={h.J_threshold:.3f}")
print("="*60)
for dr in[0,0.3,0.7,1.0,1.5,2.0,3.0,5.0]:
 q=centers[0]+torch.randn(K)*0.1+torch.tensor([dr*d_h]+[0.0]*(K-1))
 J,s=h.jury_confidence(q)
 st="FAMILIAR" if J>=0.85 else("HORIZON" if J>=0.5 else("SCOUT" if J>=0.25 else("EXPLORE" if J>=0.05 else"VOID")))
 print(f"  {dr:4.1f}d_h J={J:.4f} {st}")
ft=centers[0]+torch.tensor([2.5*d_h]+[0.0]*(K-1))
J_pre,_=h.jury_confidence(ft)
trajs.append({"proj":ft,"label":"scouted"})
h2=InstinctHorizon(creativity=0.3); h2.calibrate(trajs)
J_post,_=h2.jury_confidence(ft)
print(f"  EXPAND: J={J_pre:.3f}->{J_post:.3f} | ADVANCED={J_post>J_pre} | R={h.R:.3f}->{h2.R:.3f}")
print(f"  Verdict: {'KAISEN WORKS' if J_post>J_pre else 'STATIC'}")
with open(OUT/f"kaisen_v3_{time.strftime('%Y%m%d_%H%M%S')}.json","w") as f:
 json.dump({"R":round(h.R,4),"d_h":round(d_h,4),"J_pre":round(J_pre,4),"J_post":round(J_post,4),"advanced":J_post>J_pre},f)
print("="*60)
