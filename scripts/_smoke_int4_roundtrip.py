"""Quick smoke test: int4 factored roundtrip."""
import json, shutil, sys
from pathlib import Path

_THIS = Path(__file__).resolve()
sys.path.insert(0, str(_THIS.parents[1]))

from safetensors.torch import load_file
from hyperretro.hf.factor_int4 import save_int4_factored_checkpoint, dequant_int4_checkpoint

src = Path("outputs/_ffn_only_aware_r768")
if not src.exists():
    print("Source not found, skipping")
    sys.exit(0)

manifest = json.loads((src / "hyperretro_factored.json").read_text())
orig_sd = load_file(str(src / "model.safetensors"))

attn_entries = []
ffn_entries = manifest.get("ffn", [])

import torch
from transformers import AutoConfig
cfg = AutoConfig.from_pretrained("Qwen/Qwen2.5-1.5B")

out_int4 = Path("outputs/_test_int4_r768")
if out_int4.exists():
    shutil.rmtree(out_int4)

report = save_int4_factored_checkpoint(
    orig_sd, attn_entries, ffn_entries,
    out_dir=out_int4, hf_config=cfg, n_bits=4, quantize_non_factored=False,
)
print(f"Int4 saved: {report['on_disk_mb']:.1f} MB")

out_dq = Path("outputs/_test_int4_r768_dq")
if out_dq.exists():
    shutil.rmtree(out_dq)
dequant_int4_checkpoint(out_int4, out_dq)

dq_sd = load_file(str(out_dq / "model.safetensors"))
n_compared = 0
max_diff = 0.0
for key in orig_sd:
    if key in dq_sd:
        orig = orig_sd[key].float()
        dq = dq_sd[key].float()
        diff = (orig - dq).abs().max().item()
        if diff > max_diff:
            max_diff = diff
        n_compared += 1

orig_mb = (src / "model.safetensors").stat().st_size / 1e6
int4_mb = (out_int4 / "model.safetensors").stat().st_size / 1e6
dq_mb = (out_dq / "model.safetensors").stat().st_size / 1e6

print(f"Compared {n_compared} tensors, max abs diff: {max_diff:.6f}")
print(f"Original: {orig_mb:.1f} MB")
print(f"Int4:     {int4_mb:.1f} MB  ({orig_mb/int4_mb:.1f}x shrink)")
print(f"Dequant:  {dq_mb:.1f} MB")
print("Roundtrip OK!" if max_diff < 0.1 else f"WARNING: max diff {max_diff:.4f}")
