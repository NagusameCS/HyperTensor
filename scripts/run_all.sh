#!/bin/bash
# EC2 benchmark runner — downloads models, runs all experiments, saves results, terminates
set -e
export HF_TOKEN=$HF_TOKEN  # set your Hugging Face token here

echo "=== Downloading models ==="
pip3 install huggingface_hub -q 2>/dev/null
python3 ~/download_models.py 2>&1 | tail -5

echo "=== Running benchmarks ==="
python3 ~/ec2_benchmark.py 2>&1 | tee ~/ec2_run.log

echo "=== Results ==="
cat ~/benchmarks_ec2/results.json 2>/dev/null || echo "No results file"

echo "=== Terminating instance ==="
# sudo shutdown -h now
