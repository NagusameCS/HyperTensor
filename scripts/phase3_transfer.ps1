<#
.SYNOPSIS
    Phase 3 Transfer Benchmark — run GRC rank sweep + CI + PPL on any model/hardware
    and validate against the same gate protocol used for the primary pack.

.DESCRIPTION
    Wraps benchmark_whitepaper_finalize.ps1 with a model-labelled output pack,
    then auto-runs validation_cycle.ps1 on the result.
    Designed to be run on any GPU/model to test transfer of Phase 2 findings.

.PARAMETER Model
    Path to the GGUF model file to benchmark.

.PARAMETER ModelTag
    Short label for output directory naming (e.g. "mistral7b", "gemma2_9b").

.PARAMETER Exe
    Path to the geodessical binary. Defaults to .\build_host\geodessical.exe.

.PARAMETER CooldownSec
    Seconds to pause between benchmark runs. Default 30 (required for thermal stability).

.PARAMETER OutBase
    Base directory for benchmark packs. Defaults to .\benchmarks.

.EXAMPLE
    .\scripts\phase3_transfer.ps1 `
        -Model "C:\models\mistral-7b-v0.1.Q4_K_M.gguf" `
        -ModelTag "mistral7b_v0.1"
#>

param(
    [Parameter(Mandatory = $true)]
    [string]$Model,

    [Parameter(Mandatory = $true)]
    [string]$ModelTag,

    [string]$Exe = ".\build_host\geodessical.exe",
    [int]$CooldownSec = 30,
    [string]$OutBase = ".\benchmarks"
)

$ErrorActionPreference = "Stop"
Set-Location $PSScriptRoot\..

$stamp = Get-Date -Format "yyyyMMdd_HHmmss"
$packDir = Join-Path $OutBase "phase3_${ModelTag}_${stamp}"
New-Item -ItemType Directory -Path $packDir -Force | Out-Null

Write-Host ""
Write-Host "=== Phase 3 Transfer Benchmark ==="
Write-Host "Model   : $Model"
Write-Host "Tag     : $ModelTag"
Write-Host "Pack    : $packDir"
Write-Host "Cooldown: ${CooldownSec}s"
Write-Host ""

if (-not (Test-Path $Model)) {
    Write-Error "Model file not found: $Model"
    exit 1
}
if (-not (Test-Path $Exe)) {
    Write-Error "Executable not found: $Exe"
    exit 1
}

# Record hardware profile at run time
$hwFile = Join-Path $packDir "hardware_profile.txt"
@"
Phase 3 Transfer — Hardware Profile
Date    : $(Get-Date -Format "yyyy-MM-dd HH:mm:ss")
Model   : $Model
ModelTag: $ModelTag
Exe     : $Exe

--- nvidia-smi ---
"@ | Set-Content $hwFile
nvidia-smi --query-gpu=name,driver_version,memory.total,power.limit,clocks.max.graphics `
    --format=csv,noheader 2>&1 | Add-Content $hwFile
"" | Add-Content $hwFile
"--- CPU/OS ---" | Add-Content $hwFile
(Get-WmiObject Win32_Processor | Select-Object -First 1 -ExpandProperty Name) | Add-Content $hwFile
(Get-WmiObject Win32_OperatingSystem | Select-Object -ExpandProperty Caption) | Add-Content $hwFile

Write-Host "Hardware profile written to: $hwFile"

# Run the full benchmark harness (same protocol as Phase 2)
Write-Host ""
Write-Host "--- Running full benchmark harness (this will take 30-60 minutes) ---"
Write-Host ""

$benchScript = ".\scripts\benchmark_whitepaper_finalize.ps1"
& $benchScript -Model $Model -Exe $Exe -OutDir $packDir -CooldownSec $CooldownSec

Write-Host ""
Write-Host "--- Running gate validation ---"
Write-Host ""

$validateScript = ".\scripts\validation_cycle.ps1"
& $validateScript -PackDir $packDir

Write-Host ""
Write-Host "=== Phase 3 pack complete ==="
Write-Host "Pack directory: $packDir"
Write-Host ""
Write-Host "Compare with primary pack (Phase 2):"
Write-Host "  Primary   : benchmarks\whitepaper_pack_20260427_121815"
Write-Host "  This run  : $packDir"
Write-Host ""
Write-Host "Key transfer criterion: same qualitative ordering (k=1024 fastest or within 5%)"
Write-Host "and no gate-critical metric in contradiction with primary pack."
