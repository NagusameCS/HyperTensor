param(
    [string]$Exe = ".\\build_host\\geodessical.exe",
    [Parameter(Mandatory = $true)]
    [string]$Model,
    [string]$Prompt = "Write a Python function that returns prime numbers up to n.",
    [int]$Tokens = 180,
    [int]$Rank = 2048,
    [string]$OutDir = ".\\benchmarks\\decode_nopipe"
)

$ErrorActionPreference = "Stop"

function Get-DecodeTokPerSec {
    param([string]$Text)

    $m = [regex]::Match($Text, "Decode-only:\s*prefill\s*[\d.]+\s*ms,\s*([\d.]+)\s*tok/s")
    if ($m.Success) { return [double]$m.Groups[1].Value }

    $m2 = [regex]::Match($Text, "\[(\d+)\s+tok,\s*([\d.]+)\s*tok/s")
    if ($m2.Success) { return [double]$m2.Groups[2].Value }

    return $null
}

function Invoke-DecodeRun {
    param(
        [string]$Label,
        [string[]]$RunArgs,
        [string]$StdOut,
        [string]$StdErr
    )

    Write-Host "[bench] Running $Label ..." -ForegroundColor Cyan
    & $Exe @RunArgs 1> $StdOut 2> $StdErr
    $exitCode = $LASTEXITCODE

    if ($exitCode -ne 0) {
        throw "Run '$Label' failed with exit code $exitCode. See $StdErr"
    }

    $raw = Get-Content -Raw -Path $StdOut
    $decode = Get-DecodeTokPerSec -Text $raw
    if ($null -eq $decode) {
        throw "Could not parse decode tok/s for '$Label' from $StdOut"
    }

    $stats = Select-String -Path $StdOut -Pattern "Decode-only|\[[0-9]+ tok|\[GD\].*tok/s" | ForEach-Object { $_.Line }
    return [pscustomobject]@{
        Label = $Label
        DecodeTokPerSec = $decode
        StatLines = ($stats -join "`n")
        StdOut = $StdOut
        StdErr = $StdErr
    }
}

New-Item -ItemType Directory -Force -Path $OutDir | Out-Null
$stamp = Get-Date -Format "yyyyMMdd_HHmmss"

$baselineOut = Join-Path $OutDir ("baseline_" + $stamp + ".txt")
$baselineErr = Join-Path $OutDir ("baseline_" + $stamp + "_err.txt")
$grcOut = Join-Path $OutDir ("grc_k" + $Rank + "_" + $stamp + ".txt")
$grcErr = Join-Path $OutDir ("grc_k" + $Rank + "_" + $stamp + "_err.txt")

$baseArgs = @(
    $Model,
    "-p", $Prompt,
    "-n", "$Tokens"
)

$grcArgs = @(
    $Model,
    "--axex-compress",
    "--axex-attn-only",
    "--axex-skip-o",
    "--axex-weight-pca",
    "--axex-compress-rank", "$Rank",
    "-p", $Prompt,
    "-n", "$Tokens"
)

$baseline = Invoke-DecodeRun -Label "baseline" -RunArgs $baseArgs -StdOut $baselineOut -StdErr $baselineErr
$grc = Invoke-DecodeRun -Label ("grc_k" + $Rank) -RunArgs $grcArgs -StdOut $grcOut -StdErr $grcErr

$delta = $grc.DecodeTokPerSec - $baseline.DecodeTokPerSec
$ratio = if ($baseline.DecodeTokPerSec -gt 0) { $grc.DecodeTokPerSec / $baseline.DecodeTokPerSec } else { 0 }

Write-Host ""
Write-Host "=== Decode Benchmark (no live pipe) ===" -ForegroundColor Green
Write-Host ("baseline decode tok/s: {0:N1}" -f $baseline.DecodeTokPerSec)
Write-Host ("grc k={0} decode tok/s: {1:N1}" -f $Rank, $grc.DecodeTokPerSec)
Write-Host ("delta tok/s: {0:+0.0;-0.0;0.0}" -f $delta)
Write-Host ("grc/baseline: {0:P1}" -f $ratio)
Write-Host ""
Write-Host "[baseline stats]"
Write-Host $baseline.StatLines
Write-Host ""
Write-Host "[grc stats]"
Write-Host $grc.StatLines
Write-Host ""
Write-Host "Saved outputs:"
Write-Host "  $($baseline.StdOut)"
Write-Host "  $($baseline.StdErr)"
Write-Host "  $($grc.StdOut)"
Write-Host "  $($grc.StdErr)"
