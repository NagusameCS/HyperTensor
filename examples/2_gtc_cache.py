"""
EXAMPLE 2 — GTC Semantic Cache with Jury Voting
=================================================
Cache model responses by meaning (k-space), not by text.
Jury voting prevents false cache hits.

Run: python examples/2_gtc_cache.py
"""
import torch, sys
from pathlib import Path
sys.path.insert(0, str(Path(__file__).parent.parent))
from scripts.isagi_chat import GTCCache

torch.manual_seed(42)
K = 30

# 1. Create cache and calibrate
gtc = GTCCache(max_size=1000)
# Calibrate with some random projections to set the radius
calib = [torch.randn(K) for _ in range(32)]
gtc.calibrate(calib)

# 2. Store some query-response pairs
qa_pairs = [
    ("What is the capital of France?", "The capital of France is Paris."),
    ("How does photosynthesis work?", "Photosynthesis converts CO2 and H2O into glucose using sunlight."),
    ("Explain Newton's second law.", "F = ma: force equals mass times acceleration."),
    ("What is Python?", "Python is a high-level programming language."),
]
for q, a in qa_pairs:
    # In practice, these would be k-space projections of hidden states
    q_k = torch.randn(K)  # placeholder
    a_k = torch.randn(K)  # placeholder
    gtc.store(q_k, a_k, a, q)

print(f"Cache: {len(gtc.responses)} entries, radius={gtc.radius:.3f}")
print()

# 3. Query with jury voting
query_k = torch.randn(K)  # placeholder
hit, response, J, sim, n_jurors = gtc.query_jury(query_k)
print(f"Query result:")
print(f"  Hit: {hit}")
print(f"  Jury confidence J: {J:.4f}")
print(f"  Best similarity: {sim:.4f}")
print(f"  Jurors consulted: {n_jurors}")
if hit:
    print(f"  Response: {response[:80]}...")

print()
print("Stats:", gtc.stats())
