# Fix 1: change `continue` to `goto rollout_done_steps` in the step 0 primed fast path
# This stops the rollout from running steps 1+ after primed emission

old = '''        /* Step 0 fast path: if transformer logits are already primed,
         * emit the argmax directly (verifier will always accept) and skip
         * the expensive GRC lookup + RK4 Christoffel integration. */
        if (step == 0) {
            const float *primed_fast = llm_get_logits_primed(n_context);
            if (primed_fast) {
                int   p_best = 0;
                float p_bv   = primed_fast[0];
                for (int t = 1; t < vocab; t++) {
                    if (primed_fast[t] > p_bv) { p_bv = primed_fast[t]; p_best = t; }
                }
                out_tokens[*n_out] = p_best;
                out_conf   [*n_out] = 8.0f;
                (*n_out)++;
                for (int j = 0; j < k; j++) pos[j] += vel[j];
                continue;
            }
        }'''

new = '''        /* Step 0 fast path: if transformer logits are already primed,
         * emit the argmax directly (verifier will always accept) and stop.
         * Do NOT continue to steps 1+ --- they use pos=vel=zeros which produces
         * bad drafts that burn fallback step_fast CPU budget for no gain. */
        if (step == 0) {
            const float *primed_fast = llm_get_logits_primed(n_context);
            if (primed_fast) {
                int   p_best = 0;
                float p_bv   = primed_fast[0];
                for (int t = 1; t < vocab; t++) {
                    if (primed_fast[t] > p_bv) { p_bv = primed_fast[t]; p_best = t; }
                }
                out_tokens[*n_out] = p_best;
                out_conf   [*n_out] = 8.0f;
                (*n_out)++;
                goto rollout_done_steps;  /* n_out=1, primed only */
            }
        }'''

if old in content:
    content = content.replace(old, new, 1)
    print('SUCCESS: changed continue to goto in step0 primed path')
else:
    print('NOT FOUND: step0 primed fast path')

