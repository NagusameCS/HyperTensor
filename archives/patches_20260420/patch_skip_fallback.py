# Skip the fallback when we only have the primed logit (roll_n=1, conf=8.0)
# The primed logit is the transformer's exact prediction; adding PCA-based
# fallback drafts causes the verifier to do extra forward passes AND prevents
# the bonus correction token (2 tokens per 1 forward pass).
old = '''        /* Fallback: fill remaining slots with individual step_fast calls */
        for (int d = n_drafts; d < dyn_batch && n_ctx + d < max_ctx - 1; d++) {'''

new = '''        /* Fallback: fill remaining slots with individual step_fast calls.
         * Skip when the rollout produced only the primed logit (conf>=8.0, roll_n==1):
         * PCA-based fallback drafts are typically wrong, causing extra verifier forward
         * passes AND preventing the bonus correction token (net loss vs n_drafts=1). */
        if (n_drafts == 1 && min_conf >= 7.9f) goto skip_fallback;
        for (int d = n_drafts; d < dyn_batch && n_ctx + d < max_ctx - 1; d++) {'''

if old in content:
    content = content.replace(old, new, 1)
    print('SUCCESS: skip fallback when primed logit only')
else:
    print('NOT FOUND')

# Add skip_fallback label before skip_geodesic
old2 = '''        skip_geodesic:;'''
new2 = '''        skip_fallback:;
        skip_geodesic:;'''

if old2 in content:
    content = content.replace(old2, new2, 1)
    print('SUCCESS: added skip_fallback label')
else:
    print('skip_geodesic label NOT FOUND')

with open('C:/Users/legom/TensorOS/HyperTensor/host/main.c', 'w', encoding='utf-8', newline='') as f:
    f.write(content)
