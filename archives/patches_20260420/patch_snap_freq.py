with open('C:/Users/legom/TensorOS/HyperTensor/host/main.c', 'r', encoding='utf-8') as f:
    content = f.read()

# Change the snapshot threshold from 4 to 1 so we always have a fresh snapshot
old = '''            if (n_ctx - last_snapshot_ctx >= 4 || n_ctx <= 4) {
                llm_kv_snapshot_prefix(ctx, n_ctx);
                last_snapshot_ctx = n_ctx;
            }
            if (dyn_batch > 1)
                ott_update_generation_context(ctx, n_ctx);'''

new = '''            /* Take snapshot on every token when dyn_batch > 1 so ott_get_hidden_state
             * always finds an exact match.  GPU download is ~0.3ms; CPU memcpy ~1ms.
             * When dyn_batch <= 1 (pure transformer fallback), keep the 4-token
             * throttle to avoid unnecessary overhead. */
            if (n_ctx != last_snapshot_ctx &&
                (dyn_batch > 1 || n_ctx - last_snapshot_ctx >= 4 || n_ctx <= 4)) {
                llm_kv_snapshot_prefix(ctx, n_ctx);
                last_snapshot_ctx = n_ctx;
            }
            if (dyn_batch > 1)
                ott_update_generation_context(ctx, n_ctx);'''

if old in content:
    content = content.replace(old, new, 1)
    print('SUCCESS: changed snapshot threshold to per-token in geodesic mode')
else:
    # Try alternate
    idx = content.find('n_ctx - last_snapshot_ctx >= 4')
    if idx >= 0:
        print('Found threshold at idx:', idx)
        print(repr(content[idx-100:idx+200]))
    else:
        print('NOT FOUND')

with open('C:/Users/legom/TensorOS/HyperTensor/host/main.c', 'w', encoding='utf-8', newline='') as f:
    f.write(content)
