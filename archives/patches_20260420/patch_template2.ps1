$f = "C:\Users\legom\TensorOS\HyperTensor\host\main.c"
$content = Get-Content $f -Raw

# Replace the existing template block with one that injects tok=107 directly
$old = @'
    /* Apply Gemma4 IT chat template when prompt is plain text.
     * Without <turn|>user\n...\n<turn|>model\n wrapping, the model
     * emits <end_of_turn> as its first token (garbage output). */
    static char spec_tmpl_buf[8192];
    const char *tokenize_prompt = prompt;
    int spec_is_gemma = (strstr(m->arch, "gemma") != NULL);
    int spec_needs_tmpl = spec_is_gemma &&
                          (strstr(prompt, "<turn|>") == NULL) &&
                          (strstr(prompt, "<start_of_turn>") == NULL);
    if (spec_needs_tmpl) {
        const char *pfx = "<turn|>user\n";
        const char *sfx = "\n<turn|>model\n";
        int pl = (int)strlen(pfx), sl = (int)strlen(sfx), tl = (int)strlen(prompt);
        if (pl + tl + sl < (int)sizeof(spec_tmpl_buf) - 1) {
            memcpy(spec_tmpl_buf,           pfx,    pl);
            memcpy(spec_tmpl_buf + pl,      prompt, tl);
            memcpy(spec_tmpl_buf + pl + tl, sfx,    sl);
            spec_tmpl_buf[pl + tl + sl] = '\0';
            tokenize_prompt = spec_tmpl_buf;
            kprintf("[SPEC] Chat template applied (Gemma instruct)\n");
            /* Debug: show pieces for Gemma special tokens */
            { char dbg1[32]={0}, dbg2[32]={0}, dbg3[32]={0};
              llm_test_decode_token(106, dbg1, 30);
              llm_test_decode_token(107, dbg2, 30);
              llm_test_decode_token(m->bos_id, dbg3, 30);
              kprintf("[SPEC-DBG] bos=%d piece='%s' tok106='%s' tok107='%s'\n",
                      m->bos_id, dbg3, dbg1, dbg2); }
        }
    }

    /* Tokenize (possibly template-wrapped) prompt with BOS prepended. */
    int n_ctx;
    {
        int n_raw = llm_test_tokenize(tokenize_prompt, (int)strlen(tokenize_prompt),
                                      ctx + 1, max_ctx - 1);
        if (n_raw <= 0 || n_raw >= max_ctx - 1) {
            free(ctx); free(drafts); return -1;
        }
        if (m->bos_id >= 0) {
            ctx[0] = m->bos_id;
            n_ctx = n_raw + 1;
        } else {
            memmove(ctx, ctx + 1, (size_t)n_raw * sizeof(int));
            n_ctx = n_raw;
        }
    }
    if (n_ctx <= 0 || n_ctx >= max_ctx) {
        free(ctx); free(drafts); return -1;
    }
'@

$new = @'
    /* Apply Gemma4 IT chat template when prompt is plain text.
     * tok=107 (<end_of_turn>) has piece='\n' so cannot be injected as text.
     * We tokenize in two parts and inject tok=107 directly between them.
     * Full template: BOS + SOT(106) + "user\n" + text + EOT(107) + "\n" + SOT(106) + "model\n" */
    int spec_is_gemma = (strstr(m->arch, "gemma") != NULL);
    int spec_needs_tmpl = spec_is_gemma &&
                          (strstr(prompt, "<turn|>") == NULL) &&
                          (strstr(prompt, "<start_of_turn>") == NULL);
    int n_ctx;
    if (spec_needs_tmpl) {
        /* Part 1: "<turn|>user\n" + prompt */
        static char spec_part1[8192];
        const char *pfx = "<turn|>user\n";
        int pl = (int)strlen(pfx), tl = (int)strlen(prompt);
        if (pl + tl >= (int)sizeof(spec_part1)) tl = (int)sizeof(spec_part1) - pl - 1;
        memcpy(spec_part1,      pfx,    pl);
        memcpy(spec_part1 + pl, prompt, tl);
        spec_part1[pl + tl] = '\0';

        /* Part 2: "\n<turn|>model\n" */
        const char *sfx = "\n<turn|>model\n";

        /* Assemble into ctx[] with BOS prepended:
         * ctx[0]=BOS, ctx[1..n1]=tokenized_part1, ctx[n1+1]=EOT(107),
         * ctx[n1+2..n_ctx-1]=tokenized_part2 */
        int n1 = llm_test_tokenize(spec_part1, (int)strlen(spec_part1), ctx + 1, max_ctx - 10);
        if (n1 <= 0) { free(ctx); free(drafts); return -1; }
        int off = 1 + n1;
        if (m->bos_id >= 0) { ctx[0] = m->bos_id; } else { off = n1; memmove(ctx, ctx + 1, (size_t)n1 * sizeof(int)); }
        /* Inject <end_of_turn> = tok 107 (Gemma4 constant; safe: arch is gemma) */
        if (m->is_gemma4 && off < max_ctx - 10) { ctx[off++] = 107; }
        int n2 = llm_test_tokenize(sfx, (int)strlen(sfx), ctx + off, max_ctx - off - 1);
        if (n2 > 0) off += n2;
        n_ctx = off;
        kprintf("[SPEC] Chat template applied (Gemma4 instruct, %d tokens)\n", n_ctx);
    } else {
        int n_raw = llm_test_tokenize(prompt, (int)strlen(prompt), ctx + 1, max_ctx - 1);
        if (n_raw <= 0 || n_raw >= max_ctx - 1) {
            free(ctx); free(drafts); return -1;
        }
        if (m->bos_id >= 0) {
            ctx[0] = m->bos_id;
            n_ctx = n_raw + 1;
        } else {
            memmove(ctx, ctx + 1, (size_t)n_raw * sizeof(int));
            n_ctx = n_raw;
        }
    }
    if (n_ctx <= 0 || n_ctx >= max_ctx) {
        free(ctx); free(drafts); return -1;
    }
'@

if ($content.Contains($old)) {
    $newContent = $content.Replace($old, $new)
    [System.IO.File]::WriteAllText($f, $newContent, [System.Text.Encoding]::UTF8)
    Write-Host "Template rebuild applied"
} else {
    Write-Host "ERROR: Could not find old template block"
    # Show partial match
    $idx = $content.IndexOf("Apply Gemma4 IT chat template")
    Write-Host "Index: $idx"
}
