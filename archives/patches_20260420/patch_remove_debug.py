import re

# Remove all debug prints from host/main.c
with open('C:/Users/legom/TensorOS/HyperTensor/host/main.c', 'r', encoding='utf-8') as f:
    content = f.read()

# Remove these debug patterns:
patterns_to_remove = [
    # [SPEC-DBG] print
    r'\s*if \(generated < 8\) kprintf\("\[SPEC-DBG\].*?\\n".*?\);\n',
    # [GEO-DBG] rollout print
    r'\s*if \(generated < 20\) kprintf\("\[GEO-DBG\] rollout.*?\\n".*?\);\n',
    # [GEO-DBG] verify print - inserted by patch_geo_debug.py
    r'\s*if \(generated < 20\) kprintf\("\[GEO-DBG\] verify.*?\\n".*?\);\n',
    # [PRE-ROLL] print
    r'\s*\{ const float \*_p=llm_get_logits_primed\(n_ctx\); if \(generated < 20\) kprintf\("\[PRE-ROLL\].*?\\n".*?\); \}\n',
]

original_len = len(content)
for p in patterns_to_remove:
    content = re.sub(p, '\n', content)
    
print(f'Removed {original_len - len(content)} chars of debug from main.c')

with open('C:/Users/legom/TensorOS/HyperTensor/host/main.c', 'w', encoding='utf-8', newline='') as f:
    f.write(content)

