with open('C:/Users/legom/TensorOS/HyperTensor/runtime/nn/llm.c', 'r', encoding='utf-8') as f:
    content = f.read()

old = '''            /* End graph capture BEFORE download (cudaMemcpy is synchronous) */
            if (capturing) {
                if (cuda_graph_end_capture() == 0) {
                    cuda_graph_captured = 1;
                    cuda_graph_ctx_len  = pos; /* record position for validity check */
                    kprintf("[GRAPH] captured successfully at pos=%d\\n", pos);
                    cuda_graph_launch();'''

new = '''            /* End graph capture BEFORE download (cudaMemcpy is synchronous) */
            if (capturing) {
                uint64_t _cap_t0 = hal_timer_us();
                int _cap_rc = cuda_graph_end_capture();
                uint64_t _cap_t1 = hal_timer_us();
                if (_cap_rc == 0) {
                    cuda_graph_captured = 1;
                    cuda_graph_ctx_len  = pos; /* record position for validity check */
                    kprintf("[GRAPH] captured at pos=%d in %llums\\n", pos, (unsigned long long)(_cap_t1-_cap_t0)/1000);
                    cuda_graph_launch();'''

if old in content:
    content = content.replace(old, new, 1)
    print('SUCCESS: graph timing added')
else:
    print('NOT FOUND')

