lines = open('C:/Users/legom/TensorOS/HyperTensor/host/main.c', 'r', encoding='utf-8').readlines()

# Find the geodesic rollout call and add debug before the threshold check
# Looking for: "int ro_rc = axiom_beta_geodesic_rollout("
insert_after_rollout = -1
for i in range(1350, 1400):
    if 'axiom_beta_geodesic_rollout(' in lines[i] and 'int ro_rc' in lines[i]:
        insert_after_rollout = i
        break

print('rollout call at line:', insert_after_rollout)

# Find the threshold check loop and add print after the n_drafts++ line
insert_after_ndrafts = -1
for i in range(1355, 1440):
    if 'n_drafts++' in lines[i] and 'drafts[n_drafts' not in lines[i]:
        insert_after_ndrafts = i
        break

print('n_drafts++ at line:', insert_after_ndrafts)

# Find the n_acc result from llm_speculative_verify_topk in the geodesic path
insert_after_nacc = -1
for i in range(1450, 1480):
    if 'n_acc = llm_speculative_verify_topk' in lines[i]:
        insert_after_nacc = i + 1
        break

print('n_acc verifier at line:', insert_after_nacc - 1)

modifications = []

# Add debug after rollout call (print roll_n and roll_conf[0])
if insert_after_rollout >= 0:
    # Find the end of the rollout call (closing semicolon line)
    for j in range(insert_after_rollout, insert_after_rollout + 3):
        if ';' in lines[j]:
            debug = '            if (generated < 20) kprintf("[GEO-DBG] rollout: roll_n=%d roll_conf[0]=%.3f dyn_batch=%d n_ctx=%d\\n", roll_n, roll_n>0?roll_conf[0]:0.0f, dyn_batch, n_ctx);\n'
            modifications.append((j + 1, debug))
            break

# Add debug after n_acc from verifier  
if insert_after_nacc >= 0:
    debug = '            if (generated < 20) kprintf("[GEO-DBG] verify: n_acc=%d n_drafts=%d correction=%d\\n", n_acc, n_drafts, correction);\n'
    modifications.append((insert_after_nacc + 1, debug))

# Apply modifications in reverse order (to preserve line numbers)
for idx, text in sorted(modifications, reverse=True):
    lines.insert(idx, text)
    print(f'Inserted debug at line {idx}')

