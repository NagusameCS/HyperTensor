# Move rollout_done_steps label to AFTER the for loop closing brace
# Currently: 
#     memcpy(vel, vel_new, ...)
#     rollout_done_steps:;
# }                              <-- for loop ends here
# tensor_free(pos);
#
# We need:
#     memcpy(vel, vel_new, ...)
# }                              <-- for loop ends here  
# rollout_done_steps:;           <-- label is now OUTSIDE the for loop
# tensor_free(pos);

old = '''        /* Advance position and velocity (RK4/Heun) */
        memcpy(pos, pos_new, (uint64_t)k * sizeof(double));
        memcpy(vel, vel_new, (uint64_t)k * sizeof(double));
        rollout_done_steps:;
    }

    tensor_free(pos);'''

new = '''        /* Advance position and velocity (RK4/Heun) */
        memcpy(pos, pos_new, (uint64_t)k * sizeof(double));
        memcpy(vel, vel_new, (uint64_t)k * sizeof(double));
    }
    rollout_done_steps:;  /* stepped exit: goto here breaks out of for(step) loop */

    tensor_free(pos);'''

if old in content:
    content = content.replace(old, new, 1)
    print('SUCCESS: moved rollout_done_steps outside for loop')
else:
    print('NOT FOUND')
    idx = content.find('rollout_done_steps:;')
    print(repr(content[idx-200:idx+100]))

