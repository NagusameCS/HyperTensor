$f = "C:\Users\legom\TensorOS\HyperTensor\host\main.c"
$content = Get-Content $f -Raw

# Replace the two-part template tokenization with the simpler production approach
$old = @'
        /* Part 1: "<turn|>user\n" + prompt */
        static char spec_part1[8192];
        const char *pfx = "<turn|>user\n";
        int pl = (int)strlen(pfx), tl = (int)strlen(prompt);
        if (pl + tl >= (int)sizeof(spec_part1)) tl = (int)sizeof(spec_part1) - pl - 1;
        memcpy(spec_part1,      pfx,    pl);
        memcpy(spec_part1 + pl, prompt, tl);
        spec_part1[pl + tl] = '\0';

        /* Part 2: "\n<turn|>model\n" */
        const char *sfx = "\n<turn|>model\n";

        /* Assemble into ctx[] with BOS prepended:
         * ctx[0]=BOS, ctx[1..n1]=tokenized_part1, ctx[n1+1]=EOT(107),
         * ctx[n1+2..n_ctx-1]=tokenized_part2 */
        int n1 = llm_test_tokenize(spec_part1, (int)strlen(spec_part1), ctx + 1, max_ctx - 10);
        if (n1 <= 0) { free(ctx); free(drafts); return -1; }
        int off = 1 + n1;
        if (m->bos_id >= 0) { ctx[0] = m->bos_id; } else { off = n1; memmove(ctx, ctx + 1, (size_t)n1 * sizeof(int)); }
        /* Inject <end_of_turn> = tok 107 (Gemma4 constant; safe: arch is gemma) */
        if (m->is_gemma4 && off < max_ctx - 10) { ctx[off++] = 107; }
        int n2 = llm_test_tokenize(sfx, (int)strlen(sfx), ctx + off, max_ctx - off - 1);
        if (n2 > 0) off += n2;
        n_ctx = off;
'@

$new = @'
        /* Build full Gemma IT chat template: <turn|>user\n{prompt}\n<turn|>model\n
         * This matches the production llm_chat_turn_tokens() template exactly.
         * <turn|> is the shorthand recognised by llm_tokenize() -> tok=106 (SOT).
         * \n -> tok=107 naturally; no manual EOT injection needed. */
        static char spec_tmpl[8192];
        int _pl = (int)strlen("<turn|>user\n");
        int _tl = (int)strlen(prompt);
        int _sl = (int)strlen("\n<turn|>model\n");
        int _total = _pl + _tl + _sl;
        if (_total >= (int)sizeof(spec_tmpl) - 1) _tl = (int)sizeof(spec_tmpl) - _pl - _sl - 2;
        memcpy(spec_tmpl, "<turn|>user\n", _pl);
        memcpy(spec_tmpl + _pl, prompt, _tl);
        memcpy(spec_tmpl + _pl + _tl, "\n<turn|>model\n", _sl);
        spec_tmpl[_pl + _tl + _sl] = '\0';

        int n_tmpl = llm_test_tokenize(spec_tmpl, (int)strlen(spec_tmpl), ctx + 1, max_ctx - 2);
        if (n_tmpl <= 0) { free(ctx); free(drafts); return -1; }
        if (m->bos_id >= 0) {
            ctx[0] = m->bos_id;
            n_ctx = n_tmpl + 1;
        } else {
            memmove(ctx, ctx + 1, (size_t)n_tmpl * sizeof(int));
            n_ctx = n_tmpl;
        }
'@

if ($content.Contains($old)) {
    $content = $content.Replace($old, $new)
    [System.IO.File]::WriteAllText($f, $content, [System.Text.Encoding]::UTF8)
    Write-Host "Template patch applied"
} else {
    Write-Host "String not found - checking for partial match..."
    if ($content.Contains("spec_part1")) { Write-Host "Found spec_part1" } else { Write-Host "No spec_part1" }
    if ($content.Contains("Part 1:")) { Write-Host "Found Part 1 comment" } else { Write-Host "No Part 1 comment" }
}
