"""
Fix approach:
1. Remove the EOT injection block (causes model confusion)
2. Change tok=107 stop condition to require generated >= 5 
   (allows the model's initial format-fix newline through)
"""
path = "host/main.c"
with open(path, "r", encoding="utf-8") as f:
    src = f.read()

# 1. Remove EOT injection block
old_eot = """    /* Gemma4 chat template fix: the text template "<turn|>user\\n...\\n<turn|>model\\n"
     * tokenizes as [106 user_tokens \\n_tok 106 model_tok \\n_tok] but is missing
     * tok=107 (end_of_turn, empty piece, model-only control token) before the second
     * tok=106.  Insert it now so the model sees a properly-formed chat context. */
    if (strstr(m->arch, "gemma4") != NULL) {
        int first_106 = -1;
        for (int _i = 0; _i < n_ctx && n_ctx < max_ctx - 1; _i++) {
            if (ctx[_i] == 106) {
                if (first_106 >= 0) {
                    int has_107 = 0;
                    for (int _j = first_106 + 1; _j < _i; _j++)
                        if (ctx[_j] == 107) { has_107 = 1; break; }
                    if (!has_107) {
                        memmove(ctx + _i + 1, ctx + _i, (size_t)(n_ctx - _i) * sizeof(int));
                        ctx[_i] = 107;
                        n_ctx++;
                        _i++;
                    }
                }
                first_106 = _i;
            }
        }
        kprintf("[SPEC] Gemma4 chat template: n_ctx=%d (after EOT inject)\\n", n_ctx);
    }

    /* Reset KV cache and prime generation context"""

new_eot = """    /* Reset KV cache and prime generation context"""

if old_eot in src:
    src = src.replace(old_eot, new_eot, 1)
    print("OK: EOT injection removed")
else:
    print("FAIL: EOT injection block not found")
    idx = src.find("Gemma4 chat template fix")
    if idx >= 0:
        print(repr(src[idx:idx+200]))

# 2. Change tok=107 stop to only fire after generated >= 5
# The pattern: `if (is_gemma4 && (tok == 106 || tok == 107)) goto spec_done;`
# Replace with two separate checks

old_stop = "if (is_gemma4 && (tok == 106 || tok == 107)) goto spec_done;"
new_stop = ("if (is_gemma4 && tok == 106) goto spec_done; /* start_of_turn = new turn */\n"
            "                if (is_gemma4 && tok == 107 && generated >= 5) goto spec_done; /* end_of_turn after response */")

count = src.count(old_stop)
print(f"Found {count} stop condition instances to update")
src = src.replace(old_stop, new_stop)

with open(path, "w", encoding="utf-8") as f:
    f.write(src)
print("OK: stop condition updated (tok=107 requires generated >= 5)")
