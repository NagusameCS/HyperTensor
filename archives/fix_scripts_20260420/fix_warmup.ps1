$f = "C:\Users\legom\TensorOS\HyperTensor\host\main.c"
$content = Get-Content $f -Raw

# Move the warmup skip BEFORE the geodesic rollout (before dyn_batch check)
# Currently there is: if (dyn_batch <= 1) goto skip_geodesic;
# We need: if (skip_geodesic_warmup) goto skip_geodesic;  BEFORE the rollout
$old = '        /* Skip geodesic drafting while batch=1 (pure transformer fallback mode).
         * Rollout does 16 vocab-scan steps (~100ms CPU) to produce a single draft
         * that gets rejected with 0%% acceptance --- pure overhead.  Jump directly
         * to the verifier-as-sampler path below. */
        if (dyn_batch <= 1) goto skip_geodesic;'

# Note: In C source the %% is just % (PowerShell needs double %)
$old_actual = '        /* Skip geodesic drafting while batch=1 (pure transformer fallback mode).
         * Rollout does 16 vocab-scan steps (~100ms CPU) to produce a single draft
         * that gets rejected with 0% acceptance --- pure overhead.  Jump directly
         * to the verifier-as-sampler path below. */
        if (dyn_batch <= 1) goto skip_geodesic;'

$new_actual = '        /* Skip geodesic during warmup phase --- dyn_batch would decrement to 1
         * from all-zero drafts while warmup discards results, permanently disabling geodesic. */
        if (skip_geodesic_warmup) goto skip_geodesic;
        /* Skip geodesic drafting while batch=1 (pure transformer fallback mode).
         * Rollout does 16 vocab-scan steps (~100ms CPU) to produce a single draft
         * that gets rejected with 0% acceptance --- pure overhead.  Jump directly
         * to the verifier-as-sampler path below. */
        if (dyn_batch <= 1) goto skip_geodesic;'

if ($content.Contains($old_actual)) {
    $content = $content.Replace($old_actual, $new_actual)
    Write-Host "Added early warmup skip before rollout"
} else {
    # Try with the % as-is 
    Write-Host "Trying alternate pattern..."
    $idx = $content.IndexOf("if (dyn_batch <= 1) goto skip_geodesic;")
    if ($idx -gt 0) { Write-Host "Found at offset $idx" } else { Write-Host "Not found" }
}

# Also remove the old skip_geodesic_warmup_label section since it's no longer needed
$old2 = '        skip_geodesic_warmup_label:;
        if (skip_geodesic_warmup) n_drafts = 0; /* warmup: force verifier-only */

        skip_geodesic:;'
$new2 = '        skip_geodesic:;'

if ($content.Contains($old2)) {
    $content = $content.Replace($old2, $new2)
    Write-Host "Removed old skip_geodesic_warmup_label"
} else {
    Write-Host "Old label not found - checking..."
    if ($content -match "skip_geodesic_warmup_label") { Write-Host "Label exists" } else { Write-Host "Label missing" }
}

[System.IO.File]::WriteAllText($f, $content, [System.Text.Encoding]::UTF8)
Write-Host "Done"
