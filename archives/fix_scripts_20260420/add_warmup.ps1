$f = "C:\Users\legom\TensorOS\HyperTensor\host\main.c"
$content = Get-Content $f -Raw

# Add warmup_done flag initialization after 'int dyn_batch = batch_size;'
$old_init = '    int dyn_batch     = batch_size;  /* start at full batch --- geometry is valid */'
$new_init = '    int dyn_batch     = batch_size;  /* start at full batch --- geometry is valid */
    /* Warmup: geodesic uses raw token embeddings (position-independent) so it
     * cannot predict from chat-template markers like <start_of_turn>model\n.
     * Run verifier-only for the first 4 tokens until real response tokens are
     * in context; then enable geodesic drafting (it will see word tokens). */
    int spec_warmup_done = 0;
    int spec_warmup_tokens = 0;
    const int SPEC_WARMUP_N = 4; /* transformer-only tokens before geodesic starts */'

if ($content.Contains($old_init)) {
    $content = $content.Replace($old_init, $new_init)
    Write-Host "Added warmup init"
} else {
    Write-Host "ERROR: init string not found"
}

# Add warmup skip at top of geodesic drafting section
$old_drafts = '        int n_drafts = 0;
        float min_conf = 1.0f;

        if (perfect_day) {'
$new_drafts = '        int n_drafts = 0;
        float min_conf = 1.0f;

        /* Warmup phase: skip geodesic until we have real response tokens */
        int skip_geodesic_warmup = (!spec_warmup_done && spec_warmup_tokens < SPEC_WARMUP_N);

        if (perfect_day) {'

if ($content.Contains($old_drafts)) {
    $content = $content.Replace($old_drafts, $new_drafts)
    Write-Host "Added warmup skip flag"
} else {
    Write-Host "ERROR: drafts init string not found"
}

# Find the geodesic rollout section and skip it during warmup
# The rollout starts with: if (!perfect_day && dyn_batch > 0 && ...) or the rollout call
# We need to wrap the geodesic section with: if (!skip_geodesic_warmup) {
$old_rollout = '        skip_geodesic:;
        /*  Step 2: verify drafts (or get a fresh transformer token)  */'
$new_rollout = '        skip_geodesic_warmup_label:;
        if (skip_geodesic_warmup) n_drafts = 0; /* warmup: force verifier-only */

        skip_geodesic:;
        /*  Step 2: verify drafts (or get a fresh transformer token)  */'

if ($content.Contains($old_rollout)) {
    $content = $content.Replace($old_rollout, $new_rollout)
    Write-Host "Added warmup goto label"
} else {
    Write-Host "ERROR: rollout end string not found"
}

# After generating a token via verifier (xfmr_accepted++), if warmup not done, track it
# Find the no-drafts path's xfmr_accepted++ and add warmup tracking after it
$old_xfmr_warmup = '            if (n_ctx < max_ctx) ctx[n_ctx++] = tok;
            generated++;
            xfmr_accepted++;
        }
    }

spec_done:'
$new_xfmr_warmup = '            if (n_ctx < max_ctx) ctx[n_ctx++] = tok;
            generated++;
            xfmr_accepted++;
            if (!spec_warmup_done) {
                spec_warmup_tokens++;
                if (spec_warmup_tokens >= SPEC_WARMUP_N) spec_warmup_done = 1;
            }
        }
    }

spec_done:'

if ($content.Contains($old_xfmr_warmup)) {
    $content = $content.Replace($old_xfmr_warmup, $new_xfmr_warmup)
    Write-Host "Added warmup tracking in no-drafts path"
} else {
    Write-Host "ERROR: xfmr warmup tracking string not found - checking context..."
    # Print lines around spec_done
    $lines = $content -split "`n"
    $specIdx = 0
    for ($i = 0; $i -lt $lines.Length; $i++) {
        if ($lines[$i] -match "^spec_done:") { $specIdx = $i; break }
    }
    if ($specIdx -gt 0) {
        Write-Host "spec_done at line $specIdx. Lines before:"
        for ($i = [Math]::Max(0,$specIdx-10); $i -lt $specIdx; $i++) {
            Write-Host "  $($i+1): $($lines[$i])"
        }
    }
}

