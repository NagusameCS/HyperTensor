#!/usr/bin/env python3
"""Apply OTT improvement batch 3 to host/main.c:
1. Add is_gemma4 flag (stop on tok=106/107)
2. Add GRC feedback at accepted geodesic tokens
"""
import re, sys

path = "host/main.c"
with open(path, "r", encoding="utf-8") as f:
    src = f.read()

orig = src

#  1. Add is_gemma4 after `int eos = m->eos_id;` in speculative function 
# The speculative function has geo_accepted/xfmr_accepted declared right before eos
old1 = "    int xfmr_accepted = 0;\n    int eos = m->eos_id;\n"
new1 = ("    int xfmr_accepted = 0;\n"
        "    int eos = m->eos_id;\n"
        "    /* Gemma4: stop on <start_of_turn>=106 or <end_of_turn>=107 */\n"
        "    int is_gemma4 = (m->arch && strstr(m->arch, \"gemma4\") != NULL);\n")
if old1 in src:
    src = src.replace(old1, new1, 1)
    print("OK: added is_gemma4 declaration")
else:
    print("FAIL: is_gemma4 target not found")

#  2. EOS checks: add Gemma4 stop after each existing EOS check 
# We insert `if (is_gemma4 ...) goto spec_done;` after each EOS check
# that is followed by a `char piece[256];` line (accepted/correction paths)
# Use regex to find all such patterns within the spec function body
# after the is_gemma4 declaration was added.

# Pattern: EOS check line followed by (optionally empty lines then) char piece
# We'll do targeted replacements for each unique context

# 2a. no_verifier loop
old2a = ("                for (int i = 0; i < n_drafts && generated < max_tokens; i++) {\n"
         "                    int tok = drafts[i];\n"
         "                    if (eos >= 0 && tok == eos) goto spec_done;\n"
         "                    char piece[256];")
new2a = ("                for (int i = 0; i < n_drafts && generated < max_tokens; i++) {\n"
         "                    int tok = drafts[i];\n"
         "                    if (eos >= 0 && tok == eos) goto spec_done;\n"
         "                    if (is_gemma4 && (tok == 106 || tok == 107)) goto spec_done;\n"
         "                    char piece[256];")
if old2a in src:
    src = src.replace(old2a, new2a, 1)
    print("OK: no_verifier EOS check patched")
else:
    print("FAIL: no_verifier EOS check target not found")

# 2b. Accepted geodesic token loop (verify_topk path)
# Also add GRC feedback before ctx[n_ctx++] = tok
old2b = ("            /* Emit accepted draft tokens */\n"
         "            for (int i = 0; i < n_acc && generated < max_tokens; i++) {\n"
         "                int tok = drafts[i];\n"
         "                if (eos >= 0 && tok == eos) goto spec_done;\n"
         "\n"
         "                char piece[256];\n"
         "                int pn = llm_test_decode_token(tok, piece, (int)sizeof(piece));\n"
         "                if (pn > 0) {\n"
         "                    int room = max_output - out_len - 1;\n"
         "                    if (room <= 0) goto spec_done;\n"
         "                    if (pn > room) pn = room;\n"
         "                    memcpy(output + out_len, piece, (size_t)pn);\n"
         "                    out_len += pn;\n"
         "                    output[out_len] = '\\0';\n"
         "                }\n"
         "                if (n_ctx < max_ctx) ctx[n_ctx++] = tok;\n"
         "                generated++;\n"
         "                geo_accepted++;\n"
         "            }")
new2b = ("            /* Emit accepted draft tokens */\n"
         "            for (int i = 0; i < n_acc && generated < max_tokens; i++) {\n"
         "                int tok = drafts[i];\n"
         "                if (eos >= 0 && tok == eos) goto spec_done;\n"
         "                if (is_gemma4 && (tok == 106 || tok == 107)) goto spec_done;\n"
         "\n"
         "                char piece[256];\n"
         "                int pn = llm_test_decode_token(tok, piece, (int)sizeof(piece));\n"
         "                if (pn > 0) {\n"
         "                    int room = max_output - out_len - 1;\n"
         "                    if (room <= 0) goto spec_done;\n"
         "                    if (pn > room) pn = room;\n"
         "                    memcpy(output + out_len, piece, (size_t)pn);\n"
         "                    out_len += pn;\n"
         "                    output[out_len] = '\\0';\n"
         "                }\n"
         "                axiom_beta_grc_feedback(ctx, n_ctx, tok); /* GRC: accepted token */\n"
         "                if (n_ctx < max_ctx) ctx[n_ctx++] = tok;\n"
         "                generated++;\n"
         "                geo_accepted++;\n"
         "            }")
if old2b in src:
    src = src.replace(old2b, new2b, 1)
    print("OK: accepted token loop EOS + GRC feedback patched")
else:
    print("FAIL: accepted token loop target not found")
    # Debug: show lines around "Emit accepted draft tokens"
    idx = src.find("/* Emit accepted draft tokens */")
    if idx >= 0:
        print("  Context found at char offset", idx)
        ctx_snip = src[idx:idx+600]
        print("  Snippet:\n" + ctx_snip[:400])

# 2c. Correction token (n_acc < n_drafts path)
old2c = ("                if (eos >= 0 && tok == eos) goto spec_done;\n"
         "                /* GRC feedback: teach the geodesic what the correct token was */\n"
         "                axiom_beta_grc_feedback(ctx, n_ctx, tok);")
new2c = ("                if (eos >= 0 && tok == eos) goto spec_done;\n"
         "                if (is_gemma4 && (tok == 106 || tok == 107)) goto spec_done;\n"
         "                /* GRC feedback: teach the geodesic what the correct token was */\n"
         "                axiom_beta_grc_feedback(ctx, n_ctx, tok);")
if old2c in src:
    src = src.replace(old2c, new2c, 1)
    print("OK: correction token EOS check patched")
else:
    print("FAIL: correction token EOS check target not found")

# 2d. Bonus token (n_acc == n_drafts path) - find by unique context
old2d = ("                /* Bonus token: all drafts accepted, correction is next token */\n"
         "                int tok = correction;\n"
         "                if (eos >= 0 && tok == eos) goto spec_done;\n"
         "                char piece[256];\n"
         "                int pn = llm_test_decode_token(tok, piece, (int)sizeof(piece));\n"
         "                if (pn > 0) {\n"
         "                    int room = max_output - out_len - 1;\n"
         "                    if (room > 0) {")
new2d = ("                /* Bonus token: all drafts accepted, correction is next token */\n"
         "                int tok = correction;\n"
         "                if (eos >= 0 && tok == eos) goto spec_done;\n"
         "                if (is_gemma4 && (tok == 106 || tok == 107)) goto spec_done;\n"
         "                char piece[256];\n"
         "                int pn = llm_test_decode_token(tok, piece, (int)sizeof(piece));\n"
         "                if (pn > 0) {\n"
         "                    int room = max_output - out_len - 1;\n"
         "                    if (room > 0) {")
if old2d in src:
    src = src.replace(old2d, new2d, 1)
    print("OK: bonus token EOS check patched")
else:
    print("FAIL: bonus token EOS check target not found")

# 2e. Fallback single token (no drafts path)
old2e = ("            int tok = out_tok;\n"
         "            if (eos >= 0 && tok == eos) goto spec_done;\n"
         "            char piece[256];\n"
         "            int pn = llm_test_decode_token(tok, piece, (int)sizeof(piece));\n"
         "            if (pn > 0) {\n"
         "                int room = max_output - out_len - 1;\n"
         "                if (room <= 0) goto spec_done;")
new2e = ("            int tok = out_tok;\n"
         "            if (eos >= 0 && tok == eos) goto spec_done;\n"
         "            if (is_gemma4 && (tok == 106 || tok == 107)) goto spec_done;\n"
         "            char piece[256];\n"
         "            int pn = llm_test_decode_token(tok, piece, (int)sizeof(piece));\n"
         "            if (pn > 0) {\n"
         "                int room = max_output - out_len - 1;\n"
         "                if (room <= 0) goto spec_done;")
if old2e in src:
    src = src.replace(old2e, new2e, 1)
    print("OK: fallback single token EOS check patched")
else:
    print("FAIL: fallback single token EOS check target not found")

if src == orig:
    print("ERROR: No changes were made!")
    sys.exit(1)

with open(path, "w", encoding="utf-8") as f:
    f.write(src)
print("Saved host/main.c")
