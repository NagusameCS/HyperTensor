"""Fix vis_write_model_meta placement in axiom_vis.c.

Move each call from BEFORE fprintf(f, "{\\n") to AFTER the "title" line,
so the JSON output is valid.
"""
import re

f = r'C:\Users\legom\HyperTensor\runtime\nn\axiom_vis.c'
t = open(f, 'r', encoding='utf-8').read()

# Step 1: Remove all standalone vis_write_model_meta(f); call lines
lines = t.split('\n')
new_lines = []
skip_next_blank = False
for i, line in enumerate(lines):
    stripped = line.strip()
    if stripped == 'vis_write_model_meta(f);':
        if i + 1 < len(lines) and lines[i + 1].strip() == '':
            skip_next_blank = True
        continue
    if skip_next_blank and stripped == '':
        skip_next_blank = False
        continue
    skip_next_blank = False
    new_lines.append(line)
t = '\n'.join(new_lines)

# Step 2: Add vis_write_model_meta(f) after each title line
lines = t.split('\n')
new_lines = []
for line in lines:
    new_lines.append(line)
    if '"title":' in line and 'fprintf(f,' in line:
        new_lines.append('    vis_write_model_meta(f);')
t = '\n'.join(new_lines)

open(f, 'w', encoding='utf-8').write(t)
print('Fixed: moved vis_write_model_meta after title lines')
count = t.count('vis_write_model_meta(f)')
print(f'Total vis_write_model_meta calls: {count}')
