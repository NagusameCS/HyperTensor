"""Build the CUDA fused dual-Q8_0 GEMV kernel as a standalone DLL.

NVCC compiles .cu → .dll without needing MSVC (CUDA 12+ bundles host compiler).
Loads via ctypes at runtime.  Much faster than the pure-PyTorch GPU path.
"""
import os, subprocess, sys, shutil
from pathlib import Path

HERE = Path(__file__).resolve().parent
SRC = HERE / "gemv_dual_q8_0.cu"
OUT = HERE / "gemv_dual_q8_0.dll"

# Find NVCC
nvcc = shutil.which("nvcc")
if not nvcc:
    print("NVCC not found")
    sys.exit(1)

# Find PyTorch includes
import torch
torch_include = Path(torch.__path__[0]) / "include"
cuda_home = os.environ.get("CUDA_PATH", os.path.dirname(os.path.dirname(nvcc)))

print(f"NVCC:     {nvcc}")
print(f"Torch:    {torch_include}")
print(f"CUDA:     {cuda_home}")
print(f"Source:   {SRC}")
print(f"Output:   {OUT}")

cmd = [
    nvcc,
    str(SRC),
    "-O3",
    "-shared",
    "-o", str(OUT),
    f"-I{torch_include}",
    f"-I{torch_include / 'torch' / 'csrc' / 'api' / 'include'}",
    f"-I{cuda_home}/include",
    "--gpu-architecture=sm_89",  # RTX 4070 = compute 8.9
    "-Xcompiler", "/MD",  # dynamic CRT
    "--compiler-options", "/EHsc",  # enable C++ exceptions
]

print(f"\nRunning: {' '.join(cmd)}")
result = subprocess.run(cmd, capture_output=True, text=True, cwd=str(HERE))

if result.returncode != 0:
    print(f"\nNVCC FAILED (exit {result.returncode}):")
    print(result.stdout[-2000:] if len(result.stdout) > 2000 else result.stdout)
    print(result.stderr[-2000:] if len(result.stderr) > 2000 else result.stderr)
    sys.exit(1)
else:
    print(f"\nNVCC OK")
    print(result.stdout[-500:] if result.stdout else "")
    print(f"Output size: {OUT.stat().st_size / 1024:.1f} KB" if OUT.exists() else "NOT FOUND")
