"""CUDA kernel: fused dual Q8_0 GEMV (shared input load).

This is a direct port of ``runtime/nn/cuda_kernels.cu::kernel_gemv_dual_q8_0``.

Build with::

    nvcc -O3 -shared -o gemv_dual_q8_0.cuda.so \\
         gemv_dual_q8_0.cu \\
         -I${TORCH_INCLUDE} -I${TORCH_INCLUDE}/torch/csrc/api/include \\
         -I${CUDA_INCLUDE} \\
         --gpu-architecture=sm_89

Or via setuptools CUDAExtension (Linux CI); see setup.py in package root.

On Windows without MSVC host compiler, use the torch GPU backend
(``hyperretro.kernels.gpu``) which runs purely in PyTorch.
"""

#include <torch/extension.h>
#include <cuda_runtime.h>
#include <cstdint>

// ---------------------------------------------------------------------------
// Q8_0 layout: 32-element blocks, fp32 scale per block, int8 codes.
// ---------------------------------------------------------------------------
static constexpr int BLOCK_SIZE = 32;

// ---------------------------------------------------------------------------
// GPU kernel: fused dual Q8_0 GEMV.
//
// Grid:  (rows_a, 1)  — one block per row of W_a (W_b has same shape).
// Block: (BLOCK_SIZE, 1)  — one thread per element of x (32 threads).
//
// Shared memory: x_shared[32] fp32 — the entire input vector, loaded once.
// ---------------------------------------------------------------------------
__global__ void kernel_gemv_dual_q8_0(
    const float *__restrict__ x,           // [in_dim]
    const float *__restrict__ scale_a,     // [rows, n_blocks]
    const int8_t *__restrict__ codes_a,    // [rows, n_blocks, 32]
    const float *__restrict__ scale_b,     // [rows, n_blocks]
    const int8_t *__restrict__ codes_b,    // [rows, n_blocks, 32]
    float *__restrict__ out_a,             // [rows]
    float *__restrict__ out_b,             // [rows]
    int rows,                              // output dimension
    int n_blocks                           // in_dim / 32
) {
    // --- Shared memory: load x once per row-block ---
    __shared__ float x_shared[BLOCK_SIZE];

    int row = blockIdx.x;
    int tid = threadIdx.x;

    // Cooperative load of x into shared memory
    if (tid < BLOCK_SIZE) {
        x_shared[tid] = x[tid];
    }
    __syncthreads();

    if (row >= rows) return;

    // Per-row partial sums (in registers)
    float sum_a = 0.0f;
    float sum_b = 0.0f;

    int row_offset = row * n_blocks * BLOCK_SIZE;

    for (int b = 0; b < n_blocks; ++b) {
        int block_base = b * BLOCK_SIZE;
        float s_a = scale_a[row * n_blocks + b];
        float s_b = scale_b[row * n_blocks + b];

        float dot_a = 0.0f;
        float dot_b = 0.0f;

        // Thread-cooperative dot product over 32 elements
        for (int i = tid; i < BLOCK_SIZE; i += blockDim.x) {
            float xi = x_shared[i];
            dot_a += static_cast<float>(codes_a[row_offset + block_base + i]) * xi;
            dot_b += static_cast<float>(codes_b[row_offset + block_base + i]) * xi;
        }
        __syncthreads();

        // Warp-level reduction (simplified: use shared memory for full reduction)
        // For BLOCK_SIZE=32 with 32 threads, each thread handles one element
        // and we reduce via warp shuffle.
        #pragma unroll
        for (int offset = 16; offset > 0; offset >>= 1) {
            dot_a += __shfl_down_sync(0xffffffff, dot_a, offset);
            dot_b += __shfl_down_sync(0xffffffff, dot_b, offset);
        }

        // Thread 0 holds the block sum
        if (tid == 0) {
            sum_a += dot_a * s_a;
            sum_b += dot_b * s_b;
        }
        __syncthreads();
    }

    // Write result for this row
    if (tid == 0) {
        out_a[row] = sum_a;
        out_b[row] = sum_b;
    }
}


// ---------------------------------------------------------------------------
// PyTorch binding
// ---------------------------------------------------------------------------

std::tuple<torch::Tensor, torch::Tensor> gemv_dual_q8_0(
    torch::Tensor x,
    torch::Tensor scale_a,
    torch::Tensor codes_a,
    torch::Tensor scale_b,
    torch::Tensor codes_b
) {
    // Validate inputs
    TORCH_CHECK(x.is_contiguous(), "x must be contiguous");
    TORCH_CHECK(x.dim() == 1, "x must be 1-D");
    TORCH_CHECK(scale_a.dim() == 2, "scale_a must be [rows, n_blocks]");
    TORCH_CHECK(codes_a.dim() == 3, "codes_a must be [rows, n_blocks, 32]");
    TORCH_CHECK(scale_b.dim() == 2, "scale_b must be [rows, n_blocks]");

    int rows = static_cast<int>(scale_a.size(0));
    int n_blocks = static_cast<int>(scale_a.size(1));
    int in_dim = n_blocks * BLOCK_SIZE;

    TORCH_CHECK(x.size(0) == in_dim, "x dim mismatch");
    TORCH_CHECK(codes_a.size(0) == rows && codes_a.size(1) == n_blocks,
                "codes_a shape mismatch");

    auto out_a = torch::empty({rows}, x.options());
    auto out_b = torch::empty({rows}, x.options());

    // 1 block per row, 32 threads per block
    dim3 grid(rows, 1);
    dim3 block(BLOCK_SIZE, 1);

    kernel_gemv_dual_q8_0<<<grid, block>>>(
        x.data_ptr<float>(),
        scale_a.data_ptr<float>(),
        codes_a.data_ptr<int8_t>(),
        scale_b.data_ptr<float>(),
        codes_b.data_ptr<int8_t>(),
        out_a.data_ptr<float>(),
        out_b.data_ptr<float>(),
        rows, n_blocks
    );

    return std::make_tuple(out_a, out_b);
}


PYBIND11_MODULE(TORCH_EXTENSION_NAME, m) {
    m.def("gemv_dual_q8_0", &gemv_dual_q8_0,
          "Fused dual Q8_0 GEMV (CUDA) — shares input load",
          py::arg("x"),
          py::arg("scale_a"),
          py::arg("codes_a"),
          py::arg("scale_b"),
          py::arg("codes_b"));
}
