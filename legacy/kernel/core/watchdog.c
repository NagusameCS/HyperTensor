/* =============================================================================
 * TensorOS - Watchdog Timer & Tick Counter
 *
 * x86_64-only: ARM64 uses ARM Generic Timer + GIC instead.
 * =============================================================================*/

#ifndef __aarch64__
/* =============================================================================
 * PIT timer ISR at 1000 Hz provides:
 *   1. Monotonic tick counter (for timekeeping and timeouts)
 *   2. Software watchdog: if no heartbeat within timeout, panic
 *
 * Usage:
 *   watchdog_set(5000)   — set 5 second timeout
 *   watchdog_kick()      — reset watchdog (call from main loop)
 *   watchdog_disable()   — turn off (for known-long operations)
 * =============================================================================*/

#include "kernel/core/kernel.h"

/* =============================================================================
 * Tick Counter (incremented by PIT ISR at 1000 Hz)
 * =============================================================================*/

volatile uint64_t watchdog_ticks = 0;

/* =============================================================================
 * Watchdog State
 * =============================================================================*/

static volatile uint64_t wdog_deadline = 0;     /* 0 = disabled */
static volatile uint64_t wdog_last_kick = 0;
static volatile uint64_t wdog_timeout_ms = 0;
static volatile int wdog_enabled = 0;

/* =============================================================================
 * Timer ISR — called from PIT IRQ0 handler at 1000 Hz
 *
 * This is called from the timer_irq_handler asm stub.
 * Must be fast: increment counter, check watchdog, send EOI.
 * =============================================================================*/

void timer_tick_handler(void)
{
    watchdog_ticks++;

    /* Check watchdog */
    if (wdog_enabled && wdog_deadline > 0) {
        if (watchdog_ticks >= wdog_deadline) {
            /* Watchdog expired — halt the system */
            kprintf("\n!!! WATCHDOG TIMEOUT: No heartbeat for %lu ms !!!\n",
                    wdog_timeout_ms);
            kprintf("!!! Last kick at tick %lu, deadline was %lu, now %lu !!!\n",
                    wdog_last_kick, wdog_deadline, watchdog_ticks);
            /* Don't call kpanic (might be in ISR context) — just halt */
            __asm__ volatile("cli");
            outb(0x501, 0x31);  /* QEMU debug exit */
            for (;;) __asm__ volatile("hlt");
        }
    }
}

/* =============================================================================
 * PIT Timer ISR Assembly Stub
 * Replaces the generic isr_stub for IRQ0 (vector 0x20)
 * =============================================================================*/

extern void isr_timer(void);
__asm__(
    ".globl isr_timer\n"
    ".type isr_timer, @function\n"
    "isr_timer:\n"
    "  pushq %rax\n"
    "  pushq %rcx\n"
    "  pushq %rdx\n"
    "  pushq %rsi\n"
    "  pushq %rdi\n"
    "  pushq %r8\n"
    "  pushq %r9\n"
    "  pushq %r10\n"
    "  pushq %r11\n"
    "  call timer_tick_handler\n"
    "  movb $0x20, %al\n"
    "  outb %al, $0x20\n"        /* Send EOI to PIC */
    "  popq %r11\n"
    "  popq %r10\n"
    "  popq %r9\n"
    "  popq %r8\n"
    "  popq %rdi\n"
    "  popq %rsi\n"
    "  popq %rdx\n"
    "  popq %rcx\n"
    "  popq %rax\n"
    "  iretq\n"
);

/* =============================================================================
 * Watchdog API
 * =============================================================================*/

void watchdog_set(uint64_t timeout_ms)
{
    wdog_timeout_ms = timeout_ms;
    wdog_last_kick = watchdog_ticks;
    wdog_deadline = watchdog_ticks + timeout_ms;
    wdog_enabled = 1;
}

void watchdog_kick(void)
{
    wdog_last_kick = watchdog_ticks;
    wdog_deadline = watchdog_ticks + wdog_timeout_ms;
}

void watchdog_disable(void)
{
    wdog_enabled = 0;
    wdog_deadline = 0;
}

/* Get current uptime in milliseconds */
uint64_t watchdog_uptime_ms(void)
{
    return watchdog_ticks;  /* 1 tick = 1 ms at 1000 Hz */
}

/* Install the timer ISR into IDT slot 0x20 (IRQ0) */
void watchdog_install(void)
{
    extern void idt_set_gate(int num, uint64_t handler);
    idt_set_gate(0x20, (uint64_t)(uintptr_t)isr_timer);
    kprintf("[WDOG] Timer ISR installed (1000 Hz tick counter)\n");
    kprintf("[WDOG] Watchdog timer available (set/kick/disable)\n");
}

#else /* __aarch64__ */

#include "kernel/core/kernel.h"
#include <stdint.h>

volatile uint64_t watchdog_ticks = 0;

void timer_tick_handler(void) { watchdog_ticks++; }
void watchdog_set(uint64_t timeout_ms) { (void)timeout_ms; }
void watchdog_kick(void) {}
void watchdog_disable(void) {}
uint64_t watchdog_uptime_ms(void) { return watchdog_ticks; }
void watchdog_install(void)
{
    kprintf("[WDOG] ARM64 generic timer watchdog available\n");
}

#endif /* __aarch64__ */
