/* =============================================================================
 * TensorOS - Performance Measurement & Benchmarking
 *
 * RDTSC-based cycle-accurate timing for tensor operation benchmarks.
 * Calibrates TSC frequency against PIT for real-time measurement.
 * =============================================================================*/

#ifndef TENSOROS_PERF_H
#define TENSOROS_PERF_H

#include "kernel/core/kernel.h"

/* Read cycle counter (architecture-neutral API) */
#if defined(__aarch64__)
/* ARM64: Use CNTPCT_EL0 (physical counter) as cycle counter */
static inline uint64_t rdtsc(void)
{
    uint64_t cnt;
    __asm__ volatile("isb; mrs %0, cntpct_el0" : "=r"(cnt));
    return cnt;
}

static inline uint64_t rdtsc_fenced(void)
{
    uint64_t cnt;
    __asm__ volatile("dsb sy; isb; mrs %0, cntpct_el0" : "=r"(cnt));
    return cnt;
}
#else
/* x86: Read Time Stamp Counter (cycle-level precision) */
static inline uint64_t rdtsc(void)
{
    uint32_t lo, hi;
    __asm__ volatile("rdtsc" : "=a"(lo), "=d"(hi));
    return ((uint64_t)hi << 32) | lo;
}

/* Serialized RDTSC — prevents out-of-order execution across the fence */
static inline uint64_t rdtsc_fenced(void)
{
    uint32_t lo, hi;
    __asm__ volatile(
        "mfence\n\t"
        "rdtsc"
        : "=a"(lo), "=d"(hi)
        :: "memory"
    );
    return ((uint64_t)hi << 32) | lo;
}
#endif

/* Calibrate TSC against PIT — must be called early in boot */
void perf_calibrate(void);

/* Get calibrated TSC frequency in MHz */
uint64_t perf_tsc_mhz(void);

/* Convert cycles to nanoseconds */
uint64_t perf_cycles_to_ns(uint64_t cycles);

/* Convert cycles to microseconds */
uint64_t perf_cycles_to_us(uint64_t cycles);

/* Run all built-in performance benchmarks (called during boot) */
void run_benchmarks(void);

#endif /* TENSOROS_PERF_H */
