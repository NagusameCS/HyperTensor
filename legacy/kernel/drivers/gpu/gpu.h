/* =============================================================================
 * TensorOS - GPU Driver Interface
 * Abstraction layer for GPU compute devices (NVIDIA, AMD, Intel)
 * Provides unified API for tensor operations on GPU hardware
 * =============================================================================*/

#ifndef TENSOROS_GPU_H
#define TENSOROS_GPU_H

#include "kernel/core/kernel.h"

/* Maximum supported GPUs */
#define GPU_MAX_DEVICES     16

/* GPU vendor IDs */
#define GPU_VENDOR_NVIDIA   0x10DE
#define GPU_VENDOR_AMD      0x1002
#define GPU_VENDOR_INTEL    0x8086

/* GPU capability flags */
#define GPU_CAP_FP32        (1 << 0)
#define GPU_CAP_FP16        (1 << 1)
#define GPU_CAP_BF16        (1 << 2)
#define GPU_CAP_FP8         (1 << 3)
#define GPU_CAP_INT8        (1 << 4)
#define GPU_CAP_INT4        (1 << 5)
#define GPU_CAP_TENSOR_CORE (1 << 6)    /* NVIDIA Tensor Cores */
#define GPU_CAP_MATRIX_CORE (1 << 7)    /* AMD Matrix Cores */
#define GPU_CAP_UNIFIED_MEM (1 << 8)    /* Unified CPU/GPU memory */
#define GPU_CAP_P2P         (1 << 9)    /* Peer-to-peer GPU transfers */
#define GPU_CAP_NVLINK      (1 << 10)   /* NVLink interconnect */

/* GPU information structure */
struct gpu_info {
    uint32_t    device_id;
    uint16_t    vendor_id;
    uint16_t    product_id;
    char        name[64];
    uint32_t    vram_mb;
    uint32_t    compute_units;      /* CUDA cores / Stream processors */
    uint32_t    tensor_units;       /* Tensor cores / Matrix cores */
    uint32_t    clock_mhz;
    uint32_t    mem_clock_mhz;
    uint32_t    bus_width_bits;
    uint64_t    capabilities;       /* GPU_CAP_* flags */
    uint32_t    pci_bus;
    uint32_t    pci_device;
    uint32_t    pci_function;
    void       *mmio_base;          /* Memory-mapped I/O base address */
    uint64_t    mmio_size;
};

/* GPU command queue */
typedef struct {
    uint32_t    gpu_id;
    void       *ring_buffer;
    uint64_t    ring_size;
    uint64_t    write_ptr;
    uint64_t    read_ptr;
    uint32_t    pending_ops;
} gpu_cmd_queue_t;

/* =============================================================================
 * GPU Driver API
 * =============================================================================*/

/* Detection and initialization */
int  gpu_detect_and_init(void);
struct gpu_info *gpu_get_info(uint32_t gpu_id);

/* Memory operations */
void *gpu_vram_alloc(uint32_t gpu_id, uint64_t size, uint32_t alignment);
void  gpu_vram_free(uint32_t gpu_id, void *ptr);
int   gpu_memcpy_h2d(uint32_t gpu_id, uint64_t dst_vram, const void *src, uint64_t size);
int   gpu_memcpy_d2h(void *dst, uint32_t gpu_id, uint64_t src_vram, uint64_t size);
int   gpu_memcpy_d2d(uint32_t dst_gpu, uint64_t dst_vram,
                      uint32_t src_gpu, uint64_t src_vram, uint64_t size);

/* Tensor operations dispatched to GPU */
int   gpu_tensor_matmul(uint32_t gpu_id, tensor_desc_t *C,
                         const tensor_desc_t *A, const tensor_desc_t *B);
int   gpu_tensor_conv2d(uint32_t gpu_id, tensor_desc_t *output,
                         const tensor_desc_t *input, const tensor_desc_t *kernel,
                         uint32_t stride, uint32_t padding);
int   gpu_tensor_attention(uint32_t gpu_id, tensor_desc_t *output,
                            const tensor_desc_t *Q, const tensor_desc_t *K,
                            const tensor_desc_t *V, float scale);
int   gpu_tensor_softmax(uint32_t gpu_id, tensor_desc_t *output,
                          const tensor_desc_t *input, int axis);
int   gpu_tensor_layernorm(uint32_t gpu_id, tensor_desc_t *output,
                            const tensor_desc_t *input,
                            const tensor_desc_t *gamma,
                            const tensor_desc_t *beta, float epsilon);
int   gpu_tensor_elementwise(uint32_t gpu_id, tensor_desc_t *output,
                              const tensor_desc_t *a, const tensor_desc_t *b,
                              int op); /* 0=add, 1=mul, 2=sub, 3=div */

/* Command queue management */
gpu_cmd_queue_t *gpu_queue_create(uint32_t gpu_id, uint64_t size);
int   gpu_queue_submit(gpu_cmd_queue_t *queue);
int   gpu_queue_wait(gpu_cmd_queue_t *queue);
void  gpu_queue_destroy(gpu_cmd_queue_t *queue);

/* Power and thermal */
uint32_t gpu_get_temperature(uint32_t gpu_id);
uint32_t gpu_get_power_watts(uint32_t gpu_id);
float    gpu_get_power_mw(uint32_t gpu_id);    /* milliwatt precision — use in preference to gpu_get_power_watts */
uint32_t gpu_get_utilization(uint32_t gpu_id);
int      gpu_set_power_limit(uint32_t gpu_id, uint32_t watts);

/* VRAM usage */
uint64_t gpu_get_vram_used(uint32_t gpu_id);   /* bytes allocated via gpu_vram_alloc */
uint64_t gpu_get_vram_total(uint32_t gpu_id);  /* total VRAM bytes for this device */

#endif /* TENSOROS_GPU_H */
