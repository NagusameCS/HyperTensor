/* =============================================================================
 * TensorOS - TPU Driver Interface
 * Support for Tensor Processing Unit style accelerators
 * =============================================================================*/

#ifndef TENSOROS_TPU_H
#define TENSOROS_TPU_H

#include "kernel/core/kernel.h"

#define TPU_MAX_DEVICES  8

struct tpu_info {
    uint32_t    device_id;
    char        name[64];
    uint32_t    matrix_units;    /* Systolic array units */
    uint32_t    clock_mhz;
    uint64_t    hbm_bytes;       /* High-bandwidth memory */
    uint64_t    peak_tops;       /* Peak TOPS (INT8) */
    uint64_t    peak_tflops;     /* Peak TFLOPS (BF16) */
};

int  tpu_detect_and_init(void);
struct tpu_info *tpu_get_info(uint32_t tpu_id);

/* TPU-optimized operations */
int tpu_tensor_matmul(uint32_t tpu_id, tensor_desc_t *C,
                       const tensor_desc_t *A, const tensor_desc_t *B);
int tpu_tensor_conv2d(uint32_t tpu_id, tensor_desc_t *output,
                       const tensor_desc_t *input, const tensor_desc_t *kernel);

#endif /* TENSOROS_TPU_H */
