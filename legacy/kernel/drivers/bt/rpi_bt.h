/* =============================================================================
 * TensorOS — Bluetooth SPP Console Interface
 *
 * Connect from any PC Bluetooth settings to "TensorOS", then open
 * the created COM port in PuTTY / minicom / screen at any baud rate.
 * =============================================================================*/

#ifndef TENSOROS_RPI_BT_H
#define TENSOROS_RPI_BT_H

/* Initialize Bluetooth SPP console.  Returns 0 on success, <0 on failure. */
int  bt_init(void);

/* Poll for BT events/data.  Call regularly from main loop. Non-blocking. */
void bt_poll(void);

/* Send a character over Bluetooth SPP (buffered, sent on next bt_poll). */
void bt_putchar(char c);

/* Check if Bluetooth has received data from the remote terminal. */
int  bt_has_data(void);

/* Read one received character. Blocks if no data. */
char bt_getchar(void);

/* Returns 1 if an SPP connection is active. */
int  bt_connected(void);

#endif /* TENSOROS_RPI_BT_H */
