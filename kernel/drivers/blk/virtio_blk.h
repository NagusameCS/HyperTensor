/* =============================================================================
 * TensorOS - Virtio Block Device Driver
 * PCI-based virtio-blk for QEMU disk access
 * Enables loading GGUF models from persistent storage
 * =============================================================================*/

#ifndef TENSOROS_VIRTIO_BLK_H
#define TENSOROS_VIRTIO_BLK_H

#include <stdint.h>

/* Virtio-blk device IDs */
#define VIRTIO_BLK_SUBSYS   0x0002

/* Virtio-blk request types */
#define VIRTIO_BLK_T_IN     0  /* Read */
#define VIRTIO_BLK_T_OUT    1  /* Write */
#define VIRTIO_BLK_T_FLUSH  4  /* Flush */

/* Virtio-blk status codes */
#define VIRTIO_BLK_S_OK      0
#define VIRTIO_BLK_S_IOERR   1
#define VIRTIO_BLK_S_UNSUPP  2

/* Virtio-blk config (at device-specific offset after BAR0 registers) */
struct virtio_blk_config {
    uint64_t capacity;     /* In 512-byte sectors */
    uint32_t size_max;
    uint32_t seg_max;
    struct {
        uint16_t cylinders;
        uint8_t  heads;
        uint8_t  sectors;
    } geometry;
    uint32_t blk_size;     /* Block size (usually 512) */
} __attribute__((packed));

/* Virtio-blk request header */
struct virtio_blk_req {
    uint32_t type;
    uint32_t reserved;
    uint64_t sector;
} __attribute__((packed));

/* Block device state */
typedef struct {
    uint16_t io_base;
    uint8_t  pci_bus;
    uint8_t  pci_slot;
    uint8_t  irq;
    int      initialized;
    
    uint64_t capacity_sectors;
    uint32_t blk_size;
    
    /* Virtqueue */
    void    *vq_mem;
    uint16_t vq_size;
    
    /* Stats */
    uint64_t reads;
    uint64_t writes;
    uint64_t bytes_read;
    uint64_t bytes_written;
} virtio_blk_dev_t;

/* =============================================================================
 * API
 * =============================================================================*/

/**
 * Initialize virtio-blk driver.
 * Returns 0 on success, -1 if no device found.
 */
int virtio_blk_init(void);

/**
 * Read sectors from disk.
 * sector: starting sector (512 bytes each)
 * count: number of sectors to read
 * buf: destination buffer (must be count * 512 bytes)
 * Returns 0 on success.
 */
int virtio_blk_read(uint64_t sector, uint32_t count, void *buf);

/**
 * Write sectors to disk.
 */
int virtio_blk_write(uint64_t sector, uint32_t count, const void *buf);

/**
 * Get total capacity in bytes.
 */
uint64_t virtio_blk_capacity(void);

/**
 * Print block device info.
 */
void virtio_blk_print_info(void);

#endif /* TENSOROS_VIRTIO_BLK_H */
