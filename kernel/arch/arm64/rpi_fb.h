/* ---------- Draw one 8x8 glyph at text position (col, row) ---------- */
static inline void fb_draw_char(int col, int row, char c, uint32_t fg, uint32_t bg)
{
    if (!fb_base) return;
    int idx = (int)(unsigned char)c - 32;
    if (idx < 0 || idx >= 95) idx = 0;  /* unknown → space */

    /* Pixel coordinates */
    uint32_t px = col * 8;
    uint32_t py = row * 8;

    for (int y = 0; y < 8; y++) {
        uint8_t bits = font8x8[idx][y];
        volatile uint32_t *row_ptr = (volatile uint32_t *)
            ((uint8_t *)fb_base + (py + y) * fb_pitch);
        for (int x = 0; x < 8; x++) {
            row_ptr[px + x] = (bits & (0x80 >> x)) ? fg : bg;
        }
    }
}

/* ---------- Scroll the entire framebuffer up by one text row (8 px) -------- */
static inline void fb_scroll(void)
{
    if (!fb_base) return;
    uint8_t *dst = (uint8_t *)fb_base;
    uint8_t *src = dst + 8 * fb_pitch;
    uint32_t copy_bytes = (fb_height - 8) * fb_pitch;

    /* Byte-level copy (no memcpy in freestanding) */
    for (uint32_t i = 0; i < copy_bytes; i++)
        dst[i] = src[i];

    /* Clear the last row */
    uint8_t *last = dst + (fb_height - 8) * fb_pitch;
    for (uint32_t i = 0; i < 8 * fb_pitch; i++)
        last[i] = 0;

    fb_row = fb_rows - 1;
}

/* ---------- Public character output ---------- */
static inline void fb_putchar(char c)
{
    if (!fb_ready) return;

    if (c == '\n' || c == '\r') {
        if (c == '\n') { fb_col = 0; fb_row++; }
        if (c == '\r') { fb_col = 0; }
    } else if (c == '\t') {
        fb_col = (fb_col + 8) & ~7;
    } else if (c == '\b') {
        if (fb_col > 0) fb_col--;
    } else {
        fb_draw_char(fb_col, fb_row, c, FB_FG, FB_BG);
        fb_col++;
    }

    if (fb_col >= fb_cols) {
        fb_col = 0;
        fb_row++;
    }
    if (fb_row >= fb_rows)
        fb_scroll();
}

static inline void fb_puts(const char *s)
{
    while (*s) {
        if (*s == '\n') fb_putchar('\r');
        fb_putchar(*s++);
    }
}

/* ---------- Initialise HDMI framebuffer via VideoCore mailbox -------------- */
static inline int fb_init(void)
{
    /* Mailbox buffer — must be 16-byte aligned, in first 1 GB */
    volatile uint32_t __attribute__((aligned(16))) mbox[36];

    mbox[0]  = 36 * 4;         /* total buffer size */
    mbox[1]  = 0;              /* request code */

    /* Tag: Set physical (display) size */
    mbox[2]  = 0x00048003;
    mbox[3]  = 8;              /* value buffer size */
    mbox[4]  = 0;              /* request indicator */
    mbox[5]  = 1280;           /* width */
    mbox[6]  = 720;            /* height */

    /* Tag: Set virtual (buffer) size */
    mbox[7]  = 0x00048004;
    mbox[8]  = 8;
    mbox[9]  = 0;
    mbox[10] = 1280;
    mbox[11] = 720;

    /* Tag: Set virtual offset (0, 0) */
    mbox[12] = 0x00048009;
    mbox[13] = 8;
    mbox[14] = 0;
    mbox[15] = 0;
    mbox[16] = 0;

    /* Tag: Set depth (32 bpp) */
    mbox[17] = 0x00048005;
    mbox[18] = 4;
    mbox[19] = 0;
    mbox[20] = 32;

    /* Tag: Set pixel order (RGB) */
    mbox[21] = 0x00048006;
    mbox[22] = 4;
    mbox[23] = 0;
    mbox[24] = 1;              /* 0 = BGR, 1 = RGB */

    /* Tag: Allocate buffer */
    mbox[25] = 0x00040001;
    mbox[26] = 8;
    mbox[27] = 0;
    mbox[28] = 4096;           /* alignment */
    mbox[29] = 0;              /* size (filled by response) */

    /* Tag: Get pitch */
    mbox[30] = 0x00040008;
    mbox[31] = 4;
    mbox[32] = 0;
    mbox[33] = 0;              /* pitch (filled by response) */

    mbox[34] = 0;              /* end tag */
    mbox[35] = 0;              /* padding */

    if (!mbox_call(MBOX_CHANNEL_PM, mbox))
        return -1;             /* mailbox call failed */

    if (mbox[20] != 32)
        return -2;             /* depth not 32 bpp */

    if (mbox[28] == 0)
        return -3;             /* no buffer allocated */

    /* Convert GPU bus address → ARM physical address */
    fb_base   = (volatile uint32_t *)(uint64_t)(mbox[28] & 0x3FFFFFFF);
    fb_width  = mbox[10];      /* actual width granted */
    fb_height = mbox[11];      /* actual height granted */
    fb_pitch  = mbox[33];      /* bytes per row */
    fb_cols   = fb_width / 8;
    fb_rows   = fb_height / 8;
    fb_col    = 0;
    fb_row    = 0;

    /* Clear the framebuffer to black */
    uint32_t total_px = (fb_pitch / 4) * fb_height;
    for (uint32_t i = 0; i < total_px; i++)
        fb_base[i] = FB_BG;

    fb_ready = 1;

    return 0;
}

